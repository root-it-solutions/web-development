<p><strong>Keine Verletzung<br /></strong>Von: Ralf Lohe</p>
<p>Sicherlich haben auch Sie in Ihrem Freundes- oder Bekanntenkreis Menschen, bei denen Sie Ihre Worte auf die Goldwaage legen m&uuml;ssen. Ein falsches Wort und sie sind beleidigt oder reagieren auf eine Weise, die uns unangenehm ist. Instinktiv konzentriert man sich darauf, nur nichts Falsches zu sagen und w&auml;gt seine Worte entsprechend ab. Und schon laufen wir Gefahr, in dem was wir sagen nicht mehr authentisch zu sein. Dieses kommunikative Verbiegen f&uuml;hrt auch oft dazu, dass wir uns nicht wohl in der Kommunikation mit der betroffenen Person f&uuml;hlen, und m&ouml;glicher Weise werden wir den Kontakt zu dieser Person reduzieren.</p>
<p>Besonders problematisch wird eine solche Situation, wenn wir vom Gegen&uuml;ber zu h&ouml;ren bekommen: &bdquo;Du hast mich verletzt&ldquo;. Das ist wahrscheinlich das Letzte, was wir wollten, und schon bekommen wir ein schlechtes Gewissen. Doch das brauchen Sie wirklich nicht zu haben. Wenn Sie nicht verletzen wollten, haben Sie es auch nicht getan. Hier nun die dazugeh&ouml;rigen Zusammenh&auml;nge.</p>
<p>Unser Gespr&auml;chspartner, der sich durch unsere &Auml;u&szlig;erungen verletzt f&uuml;hlt, kennt die Intention oder die augenblickliche Erregtheit von uns nicht aber interpretiert unsere Worte. Und hier hat er allerdings die Wahl. F&uuml;hlt er sich z.B. schwach, ist es sehr wahrscheinlich, dass er unsere Worte als Verletzung empfindet. Er h&auml;tte aber auch die Wahl, statt zu interpretieren, sich zu vergewissern, wie wir das, was wir gesagt haben, gemeint haben. Dann sorgt er f&uuml;r Verst&auml;ndnis und kommt um seine Interpretation der Verletzung herum.</p>
<p>Genau so geht es uns im Umgang mit Personen, bei denen die Kommunikation nicht so einfach ist. Nehmen wir mal das Beispiel der &bdquo;Nervens&auml;ge&ldquo;. H&ouml;ren wir mal wieder die gleiche nervige Leier, so k&ouml;nnen wir uns genervt zeigen. Sind wir jedoch gut drauf und offen f&uuml;r die Menschen um uns herum, so k&ouml;nnen wir auch den Stress unseres Gespr&auml;chspartners sp&uuml;ren, der ihn verleitet andere zu nerven. Im Fall der Person, die uns eine Verletzung vorwirft, bedeutet das, dass Sie zu dem was Sie gesagt haben stehen und so signalisieren, dass die vorgenommene Interpretation falsch ist und Ihnen nicht in die Schuhe geschoben werden darf.</p>
<p>Und Sie k&ouml;nnen dann sogar noch einen Schritt weiter gehen und ein aufrichtiges Mitgef&uuml;hl mit der gestressten Person empfinden. Sie werden erstaunt sein, wie das Ihre Wortwahl und Ihre Artikulation positiv beeinflusst. Es kann durchaus dazu f&uuml;hren, dass Sie dadurch ihrem Gegen&uuml;ber dazu verhelfen das Verhalten zu &auml;ndern.</p>
<p>Um so besonnen und einf&uuml;hlsam einem (nervenden) Menschen zu begegnen, bedarf es jedoch einiger Voraussetzungen, die f&uuml;r beide zuvor beschriebenen Situationen gelten.</p>
<p>Zun&auml;chst ben&ouml;tigen Sie ein ausgepr&auml;gtes echtes Selbstvertrauen. Das bewahrt Sie davor, alles pers&ouml;nlich zu nehmen und dadurch falsch zu interpretieren. Zudem gibt Ihnen dieses Selbstvertrauen auch den Mut, die richtigen Fragen zu stellen, die Sie vor einer Fehlinterpretation bewahren.</p>
<p>Und damit haben Sie zugleich die Voraussetzung, um in der entsprechenden Kommunikation zu sich und zu dem was Sie gesagt haben zu stehen. Allein diese Haltung signalisiert Ihrem Gegen&uuml;ber (auch ohne Worte), dass Sie keine Verletzung im Sinn hatten und gibt ihm die M&ouml;glichkeit seine Interpretation zu &uuml;berdenken.</p>
