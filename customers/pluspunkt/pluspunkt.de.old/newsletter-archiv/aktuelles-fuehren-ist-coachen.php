                  <p>Aktuelles<br>
                    <br>
                    <b>Führen ist coachen</b><br />Von: Ralf Lohe</p>
                  <p>
Schon seit ca. zehn Jahren gestalten wir unser Führungstraining so, dass Führungskräfte lernen ihre Mitarbeiter zu entwickeln. Das ist sehr vernünftig, da jede Entwicklung eines Unternehmens nur durch die Entwicklung der Mitarbeiter parallel zur Unternehmensentwicklung stattfindet. Und so werden so manche Gespräche mit einem Mitarbeiter zu einem Coaching, das die Entwicklung des Mitarbeiters zum Ziel hat. In diesem Zusammenhang vermittle ich Ihnen heute einige inspirierende Gedanken aus dem Buch: Christopher Rauen (Hrsg.): Coaching-Tools II, Erfolgreiche Coaches präsentieren Interventionstechniken aus ihrer Praxis, managerSeminare Verlags GmbH, Bonn, 2007, das Sie sich nur dann anschaffen sollten, wenn Coaching einen wesentlichen Teil Ihrer Arbeit ausfüllt.
</p>
<ol>
	<li>Aussage: "Die Arbeit (des Coaches) besteht... darin, zu verstehen, wie jemand bremst und ihm zu helfen, dies zu unterlassen, nicht ihn beim Gasgeben zu unterstützen." Dies ist die Botschaft erst zu verstehen, bevor man in irgendeiner Art intervenierend handelt. Die richtige Methode zur Entwicklung eines Menschen ergibt sich aus dessen Persönlichkeit heraus, die ich als Coach (oder auch Führungskraft) erst richtig wahrnehmen muss.</li>
	<li>Aussage: "Entwicklung und Wachstum eines Menschen kann man nicht "machen", man kann es zulassen und fördern." Oder anders ausgedrückt: ist der Schüler bereit, erscheint der Lehrer. Das fordert die Geduld der Führungskraft, so lange zu warten, bis unser Mitarbeiter bereit ist, sein Coaching zu erfahren. Beschleunigen kann man diesen Prozess dadurch, dass man den Nutzen des Coachings herausstellt, ggf. verstärkt durch ein Szenario, das deutlich macht, was alles Negatives passieren wird, wenn die gewünschte Veränderung nicht eintrifft. Leidensdruck ist ein starker Motivator.</li>
	<li>Aussage: "Damit ist die Art, wie man die Welt wahrnimmt und sie gestaltet, wie man auf die Umwelt antwortet und sie prägt, nicht Schicksal, sondern selbst erzeugt und daher veränderbar." "Jeder, der sein eigenes Wohlbefinden vom Verhalten eines anderen Menschen abhängig macht, gibt dem anderen Macht und schafft eine Situation, in der er andere Menschen zu manipulieren versucht."  In diesem Thema stecken die meisten Probleme. Solange ein Mitarbeiter seine Verantwortung für sein Leben noch nicht vollständig angenommen hat, wird er immer wieder Schuldige finden, die seine Situation herbeigeführt haben. Solange er so denkt, bewegt er sich nicht und ist nicht coachbar. Folglich ist es die Aufgabe der Führungskraft durch Vorbild und richtige Kommunikation dafür zu sorgen, dass der Mitarbeiter die Verantwortung für sich und sein Leben akzeptiert. Dann kann Coaching erfolgreich werden.</li>
	<li>Aussage: "Wer sich verändern möchte, muss bereit sein, sich zu erleben und sich wahrnehmen zu lassen. Ohne Emotionen und ohne körperliches Spüren kann keine Veränderung stattfinden." Erst durch Tun wird ein neues Verhalten erlebbar. Erst jetzt wird die rechte Gehirnhälfte aktiviert und kann dazu beitragen das neue Verhalten zu verankern und zur Regel werden zulassen. Deshalb muss die Führungskraft mit dazu beitragen, dass die besprochenen und vereinbarten Verhaltensweisen auch umgesetzt werden. </li>
	<li>Aussage: "Wenn nun der Coach etwas will, was nur der Klient wollen kann, wird er Teil einer unseligen Verstrickung." "Wann immer der Coach mehr will als der Klient, droht er ihm zu schaden. Wann immer der Coach sich von der Veränderung des Klienten abhängig macht oder fühlt, kann er ihm nicht mehr helfen." Ist die Führungskraft zu ungeduldig, läuft Sie Gefahr Druck auszuüben oder zu manipulieren. Bedauerlicher Weise fällt die Verantwortung vom Mitarbeiter dabei auf sie zurück, wodurch das Führen noch schwerer wird. Deshalb gilt: der Mitarbeiter muss die Veränderung wollen, sonst ändert sich nichts. Jeder Coach ist so erfolgreich, wie er es vermag die Verantwort für die Veränderung bei seinem Klienten zu belassen.</li>
Wenn Sie als Führungskraft diese fünf Regeln berücksichtigen, werden Sie die gängigsten Führungsfehler vermeiden. Das ist die Basis für Motivation und Wachstum nicht nur bei Ihren Mitarbeitern sondern auch bei Ihnen; denn auch Sie werden Freude empfinden, wenn Sie Ihre Coaching-Kompetenzen erweitern werden.</li>
</ol>
