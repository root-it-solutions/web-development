Jeder Mensch hat ganz spezifische Fähigkeiten und Fertigkeiten. Der Koch versteht sich aufs Kochen, der Maler aufs Malen, der Schreiner aufs Schreinern und der Bäcker aufs Backen. Solange der Koch, der Maler, der Schreiner und der Bäcker nichts anderes tun muss als Kochen, Malen, Schreinern und Backen, macht ihm so schnell keiner was vor.

Fallen ihm aber noch weitere Aufgaben zu oder ist er demotiviert, weil er Probleme mit den Mitarbeitern und Kollegen hat oder nie eigene Ideen mit einbringen darf, arbeitet er gleich nicht mehr am Optimum.

Jetzt sind die Führungsqualitäten des Chefs gefragt. Nicht selten wird aber gerade im Handwerksbetrieb nicht genug getan, um sich unternehmerisch weiterzubilden. Den Chefs und Unternehmer geht es wie ihrem Mitarbeitern. Doch: Kein Meister ist vom Himmel gefallen! Oft wissen die Führungskräfte in den mittelständischen Betrieben zwar viel über das spezifische Handwerk, weil sie es selber erlernt haben und als einer der Besten stetig aufgestiegen sind. Doch wie man als Führungskraft seine Mitarbeiter motiviert, das wissen bei weitem nicht alle.

Allerdings haben die meisten Führungskräfte dafür das notwendige Potenzial. Sie müssen es nur entdecken, entwickeln und wirklich ausschöpfen. Mit unserer Unternehmensberatung haben wir schon zahlreiche Handwerksbetriebe beraten, wie sie ihr Personal besser leiten und führen können. Bäcker, Maler und andere Handwerker in Führungspositionen haben an einem unserer Seminare und Schulungen teilgenommen, weil sie nicht wussten, wie sie ihre Mitarbeiter zu Bestleistungen motivieren konnten. Anschließend an unsere Beratung sind ihr Betrieb und ihre Mitarbeiter schier aufgeblüht. Eine Schulung oder ein Workshop kann schon die Wende bringen.

Andere Betriebe begleiten wir aber auch über Wochen und Monate. Für einige Kunden arbeiten wir in verschiedenen Filialen gleichzeitig. Oder wir arbeiten nach und nach Filiale für Filiale ab. Ganz nach den Wünschen der Betriebe und Kunden.

Wenn Sie oder Ihre Führungskräfte an einem unserer Seminare für Führungskräfte teilnehmen wollen, dann melden Sie sich für einen Workshop  bzw. Beratung bei uns an. Wir freuen uns auf Sie.

