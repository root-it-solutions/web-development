        </b></font><em>F&uuml;hrungstraining auf Fuerteventura: Pluspunkt biete 
        Weiterbildung unter s&uuml;dlicher Sonne an</em>
      <p>Zur Weiterbildung nach Fuerteventura? &quot;Nat&uuml;rlich haben viele Bekannte 
        gedacht, dass wir dort Urlaub machen,&quot; schmunzelt Simone Puls. Der 
        Aufenthalt auf der Kanaren-Insel hatte f&uuml;r die junge Frau und ihren 
        Mann, den B&auml;ckermeister Andreas Puls, allerdings nichts mit Ferien 
        zu tun. In einem sechst&auml;gigem F&uuml;hrungsseminar gewann das B&auml;cker-Ehepaar 
        aus Salzbergen - gemeinsam mit anderen Interessenten - neue Erkenntnisse 
        &uuml;ber Motivation und Delegation, &uuml;ber eigenverantwortliches Handeln 
        der Mitarbeiter und die Entwicklung und Umsetzung von Visionen.</p>
      <p>und das kann man am besten auf Fuerteventura? Jetzt schmunzeln Ralf Lohe 
        und Manfred Rumi. Die beiden Gesch&auml;ftsf&uuml;hrer der Firma Pluspunkt 
        Unternehmensentwicklung aus dem m&uuml;nsterlandischem Senden veranstalten 
        ihre k&uuml;rzeren Seminare &uuml;berwiegend in Deutschland, auf jeden 
        Fall aber &quot;weit weg vom betrieblichem Alltag&quot;. Das schaffe eine 
        entspannte Atmosph&auml;re und in einem Feriengebiet wie auf der Kanareninsel 
        im Robinson-Club Esquinzo Playa - zugleich etwas Urlaubsgef&uuml;hl, was 
        sich posotiv auf die Seminararbeit auswirke.</p>
      <p>Und die steht nat&uuml;rlich im Vordergrund und ist bei Pluspunkt darauf 
        ausgerichtet, dass die Teilnehmer - &uuml;berwiegend mittelst&auml;ndische 
        Handwerksunternehmer mittelst&auml;ndischer Betriebe - ihre eigenen Werte 
        erkennen und ihr Unternehmen danach ausrichten sollen. Lohe und Rumi geht 
        es darum, das Selbstvertrauen und die Kommunikationsf&auml;higkeit ihrer 
        Kunden zu st&auml;rken.</p>
      <p>Verantwortung, Offenheit, Vertrauen - &quot;das steckt in den meisten 
        Betriebsinhabern und F&uuml;hrungskr&auml;ften drin&quot; wissen die beiden 
        Trainer aus langj&auml;hriger Erfahrung. Vielen sei dieses Potenzial aber 
        nicht bewusst, &quot;und deshalb entwicklen wir es und bringen damit einen 
        Prozess in Gang, der positives Denken vermittelt.&quot; Das sei nciht 
        nur erlernbar, sondern auch auf Mitarbeiter &uuml;bertragbar.</p>
      <p>Ein Ansatz, der auch Alexander Puls &uuml;berzeugt. Gerade der kleine 
        und mittelst&auml;ndische Unternehmer sei h&auml;ufig zu sehr auf betriebliche 
        Abl&auml;ufe konzentriert, ohne die Mitarbeiter eigenverantwortlich einzubinden, 
        unterstreicht der Handwerksunternehmer, der die B&auml;ckerei mit vier 
        Filialen und 35 Mitarbeitern gemeinsam mit seinre Ehefrau leitet. Und 
        weil sie beide - wie so h&auml;ufig in einem Familienbetrieb - in der 
        Verantwortung stehen, haben sie auch gemeinsam das Seminar besucht. EIne 
        Erkenntnis: Es sei ganz wichtig, gemeinsam Visionen zu entwickeln, besonders 
        dann, wenn es schon einmal unterschiedliche Ansichten &uuml;ber die betriebliche 
        Entwicklung gebe.</p>
      <p>Visionen zu haben und diese weiter zu entwickeln - dazu lie&szlig;en 
        sich auch Ina Kr&uuml;ger und Frank K&ouml;nemann auf Fuerteventura inspirieren. 
        Beide wolen ihr Potenzial als F&uuml;hrungskr&auml;fte in &quot;Karlchen's 
        Backstube&quot;, einer grossen B&auml;ckerei in L&ouml;hne mit 28 Filialen, 
        ausbauen. Dazu m&uuml;sse &quot;der Kopf frei sein&quot;, betont der gelernte 
        Industriekaufmann, deshalb spiele die Umgebung eine wichtige Rolle.</p>
      <p>von Hubertus Kost</p>
