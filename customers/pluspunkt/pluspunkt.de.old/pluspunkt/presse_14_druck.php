      <p><b><font color="#000000" size="3">Die eigenen Werte gezielt erkennen</font></b></p>
      <p><font color="#000000"><b>Pluspunkt Unternehmensentwicklung GmbH: Offenheit und
        Vertrauen durch Kommunikation - eigenes Potenzial entwickeln</b></font></p>
      <p><font color="#000000">Als Unternehmensberater, die zum Beispiel Marketing-
        oder Finanzierungsprobleme l&ouml;sen, sehen sich Ralf Lohe und Manfred
        Rumi nicht. Ihre Beratung hat einen anderen Ansatz: Der Unternehmer soll
        seine eigenen Werte erkennen und sein Unternehmen danach ausrichten.</font></p>
      <p><font color="#000000">Offenheit, Vertrauen, Verantwortung - &quot;das
        steckt in den meisten Betriebsinhabern drin,&quot; wissen Lohe und Rumi
        aus langj&auml;hriger Erfahrung als Trainer f&uuml;r F&uuml;hrungskr&auml;fte.
        Vielen sei dieses aber nicht bewusst, &quot;und deshalb entwicklen wir
        es.&quot; In Seminaren mit wenigen Teilnehmern geht es den beiden Inhabern
        der Firma Pluspunkt im m&uuml;nsterl&auml;ndischen Senden zun&auml;chst
        darum, das Selbstvertrauen und die Kommunikationsf&auml;higkeit ihrer
        Kunden zu st&auml;rken. Erst sp&auml;ter - bei der Begleitung im betrieblichen
        Alltag - werden Fragen der klassischen Unternehmensberatung beantwortet.
        Dabei stehen je nach Bedarf weitere Partner zur Verf&uuml;gung.</font></p>
      <p><font color="#000000">Die Kunden - das sind &uuml;berwiegend Handwerksunternehmer
        mit Betrieben in einer Gr&ouml;ssenordnung von zum Teil weit &uuml;ber
        100 Besch&auml;ftigten. Dazu geh&ouml;rt Matthias Essmann (&quot;Essmann's
        Backstube&quot;) aus Altenberge. Der B&auml;ckerei- und Konditormeister
        mit 60 Filialien in M&uuml;nster und im M&uuml;nsterland will vor allem
        die Eigenverantwortung seiner 520 Mitarbeiterinnnen und Mitarbeiter f&ouml;rdern.
        Seine Pluspunkt-Seminare haben ihm dazu wichtige Anst&ouml;&szlig;e gegeben.
        Und zwar zun&auml;chst f&uuml;r sich selbst, denn, so erl&auml;utert der
        Unternehmener: &quot;Ich habe gelernt, zun&auml;chst einmal die eigene
        Baustelle zu analysieren und Schwachstellen zu erkennen.&quot;</font></p>
      <p><font color="#000000">Aus dieser Analyse entwickelte Matthias Essmann,
        der sich bei der Handwerkskammer M&uuml;nster auch zum Betriebswirt des
        Handwerks qualifizierte, neue Gedanken &uuml;ber sein Unternehmen und
        neues Handeln. Er hat einen Teil seiner Aufgaben und damit Verantwortung
        delegiert. Motivation sei nur m&ouml;glich, wenn die St&auml;rken jedes
        Einzelnen gest&auml;rkt w&uuml;rden. Das f&ouml;rdere ergebnisorientiertes
        Handeln ebenso wie positives Denken - ein Prozess, den Essmann erfolgreich
        in Gang gesetzt hat, denn: &quot;Die Mitarbeiterinnen und Mitarbeiter
        sind das eigentliche Kapital des Unternehmens.&quot;</font></p>
      <p><font color="#000000">Ein solcher Prozess komme im betrieblichen Alltag
        aber erst in Gang, wenn Unternehmer und F&uuml;hrungskr&auml;fte selbst
        positiv d&auml;chten und - so wie Matthias Essmann - das auch mittelten,
        betont Ralf Lohe. Motivation und Kommunikation sind deshalb Schwerpunkte
        des Einstiegsseminars bei Pluspunkt, das die beiden Trainer auf Fuerteventura
        anbieten. Damit schaffen sie eine Atmosph&auml;re weg vom Alltag und zugleich
        etwas Urlaubsgef&uuml;hl. Das hat der Handwerksunternehmer aus Altenberge
        zwar nicht auf der Kanareninsel, sondern auf Mallorca erlebt, wo er an
        einem Pluspunkt-F&uuml;hrungskr&auml;fteseminar teilnahm.</font></p>
      <p><font color="#000000">Neben den Seminaren, die nicht nur auf Ferieninseln,
        sondern auch im M&uuml;nsterland stattfinden, bieten Lohe und Rumi individuelle
        Beratung an und vermitteln dabei sachkundige Partner zum Beispiel zu speziellen
        betriebswirtschaftlichen Problemen. So arbeitet Pluspunkt unter anderem
        mit der Zentralstelle f&uuml;r Weiterbildung im Handwerk und mit einigen
        Handwerkskammern zusammen.</font></p>
      <p><font color="#000000">Pluspunkt legt grossen Wert darauf, zu allen Kunden
        auch ein pers&ouml;nliches Verh&auml;ltnis aufzubauen. Daraus entwickeln
        sich Gespr&auml;che, die weit &uuml;ber betriebliche Themen hinausgehen.
        &quot;Auch im privaten Bereich k&ouml;nnen wir h&auml;ufig Hilfestellung
        leisten,&quot; betont Manfred Rumi. positives Denken im privaten Umfeld
        sehen die beiden Trainer deshalb auch als Erfolgsfaktor f&uuml;r unternehmerisches
        Handeln.</font></p>
      <p><font color="#000000">Matthias Essmann, der 1981 in den elterlichen Betrieb
        einstieg und st&auml;ndig expandierte, hat inzwischen auch F&uuml;hrungskr&auml;fte
        seines Unternehmens in die Seminare eingebunden. Was dort vermittelt werde,
        lasse sich gut multiplizieren.</font></p>
