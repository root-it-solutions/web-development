      <p><font color="#000000"><b>Neues Handeln f&uuml;r Unternehmen lernen<br>
        <br>
        </b></font><font color="#000000"><i>Karsten Kr&uuml;ger lie&szlig; sich 
        erfolgreich beraten<br>
        <br>
        </i></font><font color="#000000"><b>M&uuml;nster/L&ouml;hne (DHB)</b>. 
        Als Unternehmensberater, die zum Beispiel Marketing- oder Finanzierungsprobleme 
        l&ouml;sen, sehen sich Ralf Lohe und Manfred Rumi nicht. Ihre Beratung 
        hat einen anderen Ansatz: Der Unternehmer soll seine eigenen Werte erkennen 
        und sein Unternehmen danach ausrichten..</font></p>
      <p><font color="#000000">Offenheit, Vertrauen, Verantwortung - &quot;das 
        steckt in den meisten Betriebsinhabern drin,&quot; wissen Lohe und Rumi 
        aus langj&auml;hriger Erfahrung als Trainer f&uuml;r F&uuml;hrungskr&auml;fte. 
        Vielen sei dieses Potenzial aber nicht bewusst, &quot;und deshalb entwickeln 
        wir es&quot;. In Seminaren mit wenigen Teilnehmern geht es den beiden 
        Inhabern der Firma Pluspunkt im m&uuml;nsterl&auml;ndischen Senden zun&auml;chst 
        darum, das Selbstvertrauen und die Kommunikationsf&auml;higkeit ihrer 
        Kunden zu st&auml;rken. Erst sp&auml;ter - bei der Begleitung im betrieblichen 
        Alltag werden Fragen der klassischen Unternehmensberatung beantwortet. 
        Dabei stehen je nach Bedarf weitere Partner zur Verf&uuml;gung.</font></p>
      <p><font color="#000000">Die Kunden - das sind &uuml;berwiegend Handwerksunternehmer 
        mit Betrieben in einer Gr&ouml;&szlig;enordnung von zehn bis 200 Besch&auml;ftigten. 
        Dazu geh&ouml;rt Karsten Kr&uuml;ger aus L&ouml;hne. Der Junior-Chef der 
        B&auml;ckerei &quot;Karlchen's Backstube&quot; mit 28 eigenen Filialen 
        will vor allem die Eigenverantwortung seiner 160 Mitarbeiter f&ouml;rdern. 
        Sein erstes Pluspunkt-Seminar hat ihm dazu wichtige Anst&ouml;&szlig;e 
        gegeben. Und zwar zun&auml;chst f&uuml;r sich selbst, denn, so erl&auml;utert 
        der B&auml;cker und Betriebswirt: &quot;Ich habe gelernt, intensiv &uuml;ber 
        mich nachzudenken und auch das eigene Handeln infrage zu stellen.&quot;</font></p>
      <p><font color="#000000">Daraus entwickelte Karsten Kr&uuml;ger neue Gedanken 
        &uuml;ber sein Unternehmen und neues Handeln. Die Strukturver&auml;nderung 
        des handwerklichen Familienbetriebes vom aufgabenbezogenen zum ergebnisorientierten 
        F&uuml;hrungsstil ist daf&uuml;r ein Beispiel. Viele intensive Gespr&auml;che 
        hat Kr&uuml;ger auf diesem Weg gef&uuml;hrt, auch seine leitenden Mitarbeiter 
        haben inzwischen F&uuml;hrungsseminare bei Pluspunkt besucht. Seine Erfahrung: 
        Positives Denken ist ebenso erlernbar wie &uuml;bertragbar.</font></p>
      <p><font color="#000000">Ein solcher Prozess komme im betrieblichen Alltag 
        aber erst in Gang, wenn Unternehmer und F&uuml;hrungskr&auml;fte selbst 
        positiv d&auml;chten und - so wie Karsten Kr&uuml;ger - das auch mitteilten, 
        betont Ralf Lohe. Motivation und Kommunikation sind deshalb Schwerpunkte 
        des Einstiegsseminars, das die beiden Berater auf Fuerteventura anbieten. 
        Damit schaffen sie eine entspannte Atmosph&auml;re.</font></p>
      <p><font color="#000000">Pluspunkt legt gro&szlig;en Wert darauf, zu allen 
        Kunden auch ein pers&ouml;nliches Verh&auml;ltnis aufzubauen. Daraus entwickeln 
        sich Gespr&auml;che, die weit &uuml;ber betriebliche Themen hinausgehen. 
        &quot;Auch im privaten Bereich k&ouml;nnen wir h&auml;ufig Hilfestellung 
        leisten,&quot; betont Manfred Rumi.</font></p>
      <p><font color="#000000">Positives Denken im privatem Umfeld sehen die beiden 
        Trainer deshalb auch als Erfolgsfaktor f&uuml;r unternehmerisches Handeln. 
        Karsten Kr&uuml;ger formuliert das so: &quot;Ich kann &uuml;ber alles 
        klagen, oder ich mache mir meine eigene Konjunktur.&quot; Der 33-j&auml;hrige 
        hat sich erfolgreich f&uuml;r die zweite Variante entschieden: &quot;Karlchen's 
        Backstube&quot; verzeichnet f&uuml;r 2003 das &quot;wirtschaftlich beste 
        Jahr seit zehn Jahren&quot;. Daf&uuml;r hat sich Kr&uuml;ger, der das 
        Unternehmen nach dem geplanten Generationswechsel im Jahre 2005 gemeinsam 
        mit seiner Schwester Simone B&ouml;hne weiterf&uuml;hren wird, auch selbst 
        fit gemacht: Seit seinem ersten Pluspunkt-Seminar verringerte er sein 
        Gewicht um 30 Kilogramm.<br>
        </font> </p>
