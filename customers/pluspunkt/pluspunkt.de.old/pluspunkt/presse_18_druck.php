<P> 
      <p> <b><font color="#000000">Bremen. Warum streicht ein mittelst&auml;ndischer 
        Handwerksbetrieb eine Hierarchie-Ebene und st&auml;rkt trotzdem - oder 
        gerade deshalb - Motivation und Engagement des Mitarbeiters?</font></b></p>
      </b></font>
<p><b><font color="#000000">Tenter's Backhaus in Bremen hat diesen Weg beschritten 
        und die Verkaufs- bzw. Bezirksleitung abgeschafft. Der Schritt basiert 
        auf der Erkenntnis, dass reine Kontrollinstanzen nicht mehr zeitgem&auml;&szlig; 
        sind, weil sie die Motivation der &quot;Ebene darunter&quot; einschr&auml;nken 
        oder gar nicht erst aufkommen lassen.</font></b></p>
      <p><b><font color="#000000">Gerade in personalintensiven Betrieben w&uuml;rden 
        verantwortungsbewusste Mitarbeite immer mehr zum entscheidenden Erfolgsfaktor, 
        unterstreicht Ralf Lohe. Der Gesch&auml;ftsf&uuml;hrer der Pluspunkt-Unternehmensentwicklung 
        hat Tenter's Backhaus bei der Neuausrichtung der Personalstruktur unterst&uuml;tzt 
        und begleitet die B&auml;ckerei, die in 30 Filialen ca. 70 Mitarbeiter 
        besch&auml;ftigt, auch auf dem weiteren Weg. Die Abschaffung einer Hierarchie-Ebene 
        bleibe n&auml;mlich ohne Wirkung, so der erfahrene Trainer, wenn nicht 
        weitere Schritte hinzuk&auml;men.<br>
        <br>
        So m&uuml;ssten Mitarbeiter mit Potenzial gef&ouml;rdert und die Verantwortung 
        f&uuml;r Ergebnisse an die Basis verlagert werden, denn dort w&uuml;rden 
        sie erarbeitet. In tenter's Backhaus gibt es jetzt anstelle der Bezirksleiter 
        (Kontrolle) deshalb jetzt Gruppenleiter (F&uuml;hrung), die mit hohem 
        Engagement an ihre neue Aufgabe herangehen. Dabei zeigte sich schnell 
        ein gro&szlig;er Bedarf an Weiterbildung, der von vielen Mitarbeitern 
        geradezu eingefordert werde, stellte Lohe fest. Auch darin sieht er eine 
        positive Entwicklung: Weiterbildung werde nicht von der Gesch&auml;ftsleitung 
        &quot;verordnet&quot;, sondern von der Basis gewollt. Hier m&uuml;ssten 
        unternehmen reagieren, um ihre Mitarbeiter m&ouml;glichst durch Schulung 
        &quot;vor Ort &quot; zu entwickeln. Tenter's Backhaus sei auf einem guten 
        Weg und zugleich ein Beispiel f&uuml;r einen Prozess, der die Verantwortung 
        der Mitarbeiter st&auml;rke und dadurch neue Energien freisetze.</font></b></p>
