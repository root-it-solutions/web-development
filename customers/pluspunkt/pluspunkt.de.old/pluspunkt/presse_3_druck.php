      <p><font color="#000000"><b><font size="3">Proberating im Parkhotel<br>
        </font></b></font><font color="#333333"><b>Schloss Hohenfeld wurde bewertet 
        und analysiert/ Mitarbeiter kennen jetzt &quot;Knackpunkte&quot;</b></font></p>
      <p><b><font color="#000000">M&Uuml;NSTER - Was bestimmt den Wert eines Unternehmens? 
        Nur die harten Faktoren, also das, was auf der finanziellen Habenseite 
        zu verbuchen ist? Oder auch die weichen Faktoren, wie zum Beispiel die 
        Unternehmensf&uuml;hrung und die Qualifikation der Mitarbeiter?</font></b></p>
      <p><font color="#000000">Sp&auml;testens seit Basel 11 und den damit verbundenen 
        Ver&auml;nderungen bei der Kreditvergabe ist die Antwort auf diese Frage 
        wichtiger als je zuvor. K&uuml;nftig ermitteln die Banken die Bonit&auml;t 
        eines Unternehmens mit Hilfe eines so genannten Ratings und legen je nach 
        Ergebnis die H&ouml;he der Kreditzinsen fest. In Vorbereitung auf ein 
        solches Bankenrating hat im Parkhotel<br>
        Schloss Hohenfeld vor kurzem ein &quot;Proberating&quot; stattgefunden. 
        Das Besondere: Beteiligt waren neben der Dresdener Bank aus M&uuml;nster 
        auch zwei Unternehmensberatungen. Gemeinsam haben sie das Hotel nicht 
        nur bewertet, sondern zugleich auch eine Potenzanalyse durchgef&uuml;hrt. 
        Somit verf&uuml;gt das Parkhotel jetzt &uuml;ber eine ausf&uuml;hrliche 
        Bewertung des Ist-Standes sowie einer detaillierten Empfehlung f&uuml;r 
        den k&uuml;nftigen Soll-Zustand und den Weg dorthin.</font></p>
      <p><font color="#000000">&quot;Dieses Verfahren ist zumindest im M&uuml;nsterland 
        sicher einmalig&quot;, meint Bernhard Rothenberger, der das Vier-Sterne-Hotel 
        zu' Beginn des Jahres &uuml;bernommen hat.<br>
        Obwohl der M&uuml;nsteraner Hotelier im Moment keinen akuten Finanzbedarf 
        hat, wollte er wissen, wie die Bewertung im Fall der F&auml;lle aussehen 
        w&uuml;rde.</font></p>
      <p><font color="#000000">&quot;Ich sehe das Geldinstitut als Partner, mit 
        dem ich gemeinsam die Voraussetzung f&uuml;r ein gutes Rating schaffen 
        will&quot;, so Rothenberger. &quot;Mit dem jetzt vorliegenden Ergebnis 
        kann ich die k&uuml;nftige Entwicklung meines Hauses zielgerichtet steuern. 
        Und wenn es dann in einigen Jahren wirklich um einen Kredit geht, hoffentlich 
        gute Zinsbedingungen erwarten&quot;.</font></p>
      <p><font color="#000000">Um dieses Ziel zu erreichen, haben Bernhard Rothenberger 
        und seine 65 Mitarbeiter in den vergangenen zwei Monaten sehr viel Zeit 
        investiert. Gemeinsam mit Ralf Lohe von der Pluspunkt Unternehmensentwicklung 
        und Winfried B&uuml;th von der Firma intectum Unternehmens-Organisation 
        und Systemgestaltung galt es, umfangreiche Fragenkataloge zu bew&auml;ltigen.</font></p>
      <p><font color="#000000">Nach der Bestandsaufnahme folgte gemeinsam die 
        Zielsetzung f&uuml;r die Zukunft. Dabei konnten die Mitarbeiter auch Empfehlungen 
        f&uuml;r die Verbesserung der Arbeitsabl&auml;ufe und Strukturen aussprechen.<br>
        &quot;Wir wissen jetzt, was die Knackpunkte sind, wo die Ver&auml;nderungen 
        ansetzen m&uuml;ssen so Rothenberger abschlie&szlig;end.</font></p>
                  <p></p>
