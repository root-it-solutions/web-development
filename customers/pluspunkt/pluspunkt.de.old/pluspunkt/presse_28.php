							Inspirationstag zu familienfreundlicher Betriebsführung in Altenberge</strong>, von Alexander Sundermeier
						</p>
						<p>
Wollen sich Unternehmen etablieren und ebenfalls zukünftig am Markt halten, gilt es, dem viel zitierten Fachkräftemangel und dem demographischen Wandel entgegenzuwirken. Was läge da näher, als sich mit der Vereinbarkeit von Familie und Beruf gerade auch im Rahmen firmenpolitischer Agitation auseinanderzusetzen?
</p>
<p>
Altenberge. "Familienfreundliches Unternehmen" heißt das derzeitige Schlagwort, mit dem sich Firmeninhaber nun konfrontiert sehen. Doch wie definiert sich ein solcher Betrieb? Vielen schweben Bilder vor, wie sie beispielsweise bei der Oke-Gruppe in Hörstel praktiziert werden: Ein modernes Unternehmen mit Fitnessraum, Krabbelgruppe, Mitarbeiter, die sich dort wie zu Hause fühlen, und Krankenstände, von denen andere nur träumen.
</p>
<p>
Gestern trafen sich 45 Unternehmer gemeinsam mit ihren Familien in der Backstube Essmann in Altenberge, um im Rahmen eines „Inspirationstages“ der hoch gelobten familienfreundlichen Betriebsführung auf die Schliche zu kommen. Während 28 Kinder und Jugendliche gemeinsam mit einem Bäckermeister der Bäckerei Essmann österliche Leckereien backten, stimmte parallel dazu HWK-Präsident Hans Rath die Erwachsenen auf das Thema ein.
</p>
<p>
Viele mittelständische Betriebe würden zum größten Teil von Familien geführt. Hier stellt sich beim Inhaber schon die Frage nach der eigenen Vereinbarkeit von Familie und Beruf. Und bei den Mitarbeitern, wie sieht es da aus? Unter der Leitung der Unternehmensentwickler Manfred Rumi und Ralf Lohe von der Firma Pluspunkt stellten sich die Unternehmer in Workshops zum Thema "Wertvolle Unternehmer für wertvolle Mitarbeiter" die Frage, welche Aspekte für sie persönlich familienfreundlich sind und wie diese im Betrieb umsetzbar sind.
</p>
<p>
Familienbewusst sind Unternehmen nämlich auch dann, wenn sie ihren Mitarbeitern Gutes tun, aber ihren eigenen Vorteil nicht aus den Augen verlieren: weniger Krankheitsstände, kaum Fluktuation, niedrige Kosten für Wiederbesetzung und Erhalt des Firmen-Know-hows sind nur einige betriebswirtschaftliche Größen, die Familienfreundlichkeit für Unternehmen rentabel machen.

						</p>
