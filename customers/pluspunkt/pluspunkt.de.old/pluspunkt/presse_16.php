                        den Bäckermeister Alexander Puls, allerdings nichts mit Urlaub zu tun. </b> 
                        </p><p>
In einem sechstägigen Führungsseminar gewann das Bäcker-Ehepaar aus Salzbergen neue Erkenntnisse über Motivation und Delegation, 
über eigenverantwortliches Handeln der Mitarbeiter und über die Entwicklung und Umsetzung von Visionen. Der Unternehmer soll seine 
eigenen Werte erkennen und sein Unternehmen danach ausrichten: Das ist der Kern des Seminars, an dem Simone und Alexander Puls 
gemeinsam mit fünf weiteren Interessenten teilnahmen.
<br>
<br>
Ralf Lohe und Manfred Rumi, den beiden Geschäftsführern der Pluspunkt Unternehmensentwicklung aus Senden, die solche Seminare 
veranstalten, geht es zunächst darum, das Selbstvertrauen und die Kommunikationsfähigkeit ihrer Kunden zu stärken. Erst in einem 
weiteren Schritt - bei der Begleitung im betrieblichen Alltag - werden zum Beispiel Marketing- oder Finanzierungsthemen erörtert.
<br>
<br>
Die Kunden der Firma Pluspunkt sind überwiegend Handwerksunternehmen, wie zum Beispiel die Bäckerei Puls, die in ihrem Hauptgeschäft 
und den vier Filialen 35 Mitarbeiter beschäftigt und seit 2001 in der vierten Generation von Alexander Puls geleitet wird.
Als Unternehmer müsse man sich ständig weiterentwickeln, sagt der 36-jährige Bäckermeister. Und wenn, wie in seinem Betrieb, die 
Frau mit in der Verantwortung stehe, dann sei es auch sinnvoll, eine solche Weiterbildung gemeinsam zu machen. "Verantwortung, 
Offenheit, Vertrauen, das steckt in den meisten Betriebsinhabern drin", wissen Lohe und Rumi aus langjähriger Erfahrung als Trainer 
für Führungskräfte. Vielen sei dieses Potenzial aber nicht bewusst, "und deshalb entwickeln wir es und bringen damit einen Prozess 
in Gang".
<br>
<br>
Positives Denken, davon sind beide überzeugt, sei ebenso erlernbar wie übertragbar. Auch bei Simone und Alexander Puls haben sie 
diesen Prozess in Gang gebracht. Dazu trug die entspannte Atmosphäre weg vom Alltag (und zugleich etwas Urlaubsgefühl) bestimmt bei. 
