      <p><font color="#000000"><b><font size="3">Konstruktives und produktives 
        Betriebsklima f&ouml;rdern</font><font size="2"><br>
        </font> <font color="#333333">Unternehmenscoaching lohnt sich, wie das 
        Beispiel der B&auml;ckerei &quot;Lorenz B&auml;cker Victor Bur GmbH&quot; 
        in Aurich zeigt</font><br>
        <br>
        <br>
        Aurich (zi).</b> F&uuml;nf Mal hatte die Filialleiterin nun schon dem 
        Disponenten gesagt, dass sie die Ware p&uuml;nktlich um sechs Uhr morgens 
        brauche. F&uuml;nf Mal hatte der Fahrer sie bereits versetzt. F&uuml;nf 
        Tage, an denen sie ihre Stammkunden vertr&ouml;sten musste. Die holten 
        ihre Br&ouml;tchen nun woanders. Und der letzte Kunde war am f&uuml;nften 
        Tag sogar richtig laut geworden. Als dann noch der neue Lehrling das Tablett 
        mit den frischen Backlingen fallen l&auml;sst, reichte es der Filialleiterin. 
        Den folgenden R&uuml;ffel w&uuml;rde der Lehrling nicht so schnell vergessen. 
        Und auch beim Fahrer lie&szlig; sie geh&ouml;rig Dampf ab. Ein konstruierter 
        Fall, der so schon in jedem Backbetrieb vorgefallen ist und nicht mal 
        ein reines Branchen-Problem. &quot;Der Fehler hier w&auml;re nicht mal 
        die Unp&uuml;nktlichkeit des Fahrers oder das Missgeschick des Lehrlings. 
        Der Fehler liegt in der Kommunikation zwischen den Kollegen&quot;, sagt 
        Manfred Rumi, Unternehmensberater der Firma &quot;Pluspunkt&quot;. Der 
        Kursleiter aus Senden schult Mitarbeiter und unterst&uuml;tzt Firmen in 
        allen Fragen zum Thema Mitarbeiterschulung. Sein letztes Projekt: Er betreut 
        die Gro&szlig;b&auml;ckerei &quot;Lorenz Victorbur&quot; in Aurich und 
        coachte dort das Personal.</font></p>
      <p><font color="#000000"><b>Frust: Gift f&uuml;rs Betriebsklima</b><br>
        Falsche Kommunikation ist Gift im Unternehmen. Die Filialleiterin aus 
        dem konstruierten Fall wird den ganzen Tag sauer sein und kaum einen Kunden 
        mit wahrer Freundlichkeit betreuen k&ouml;nnen. Ganz zu schweigen von 
        der Motivation des Lehrlings. Der Frust wird weitergegeben und verbreitet 
        sich, staut sich an zu einem schlechten Betriebsklima, vielleicht sogar 
        zur Unproduktivit&auml;t, mit fatalen Folgen in einem Unternehmen. Dabei 
        kann alles so einfach sein, wie der nicht konstruierte, sondern reale 
        Fall aus dem Unternehmen &quot;Lorenz Victorbur&quot; zeigt: Konditormeister 
        Uwe de Vries hat es von seinem Arbeitsplatz nicht weit bis zur Backstube. 
        Quasi ein Katzensprung von Feinb&auml;ckerei bis zur Teigmisch-Maschine 
        und den Back&ouml;fen nebenan. Doch der Gang r&uuml;ber war f&uuml;r ihn 
        sonst kein Thema: &#132;Fr&uuml;her habe ich meinen Job gemacht&quot;, 
        sagt der B&auml;cker-Fachmann heute. &#132;Wenn ich fertig war, dann war 
        es das eben. Feierabend. Heute geh ich im Betrieb herum und schau mir 
        die Arbeit der anderen an. Wenn irgendwo Rat gebraucht wird oder nur eine 
        helfende Hand, dann bin ich dabei.&quot; So konnte der Konditor seinen 
        B&auml;ckerkollegen schon ein paar Anregungen geben und ihnen bei der 
        Produktion helfen.</font></p>
      <p><font color="#000000"><b>Die Kunst des &quot;Kritisierens&quot;</b><br>
        &quot;Die Hilfsbereitschaft in einem Betrieb ist grunds&auml;tzlich immer 
        da&quot;, beobachtete Manfred Rumi. &quot;Der Helfer hat nur zwei Probleme: 
        Er will nicht als Besserwisser dastehen und hat Angst, abgewiesen zu werden.&quot; 
        Oft sei es im Berufsalltag so, dass Kritik oder Anregungen pers&ouml;nlich 
        aufgefasst werden. &#132;Und da liegt der Fehler&quot;, sagt Manfred Rumi. 
        &#132;Die Mitarbeiter m&uuml;ssen erkennen, dass die Kritik sich nicht 
        gegen den Menschen und die Person richtet, sondern gegen eine spezielle 
        Sache. Kein pers&ouml;nlicher Angriff!&quot; Durch das Training, das abteilungs&uuml;bergreifend 
        durchgef&uuml;hrt wurde, lernten die Mitarbeiter die Kunst des Kritisierens.</font></p>
      <p><font color="#000000"><b>Konstruktives Betriebsklima</b><br>
        Und das geht so weit, dass selbst die Gesch&auml;ftsf&uuml;hrerin Melitta 
        Lorenz heute erstaunt dar&uuml;ber ist, was sie f&uuml;r konstruktive 
        Vorschl&auml;ge von ihren Angestellten bekommt: &quot;Die Menschen kommen 
        einfach zu mir und sagen, was ihnen einf&auml;llt. Das Betriebsklima ist 
        dadurch offener geworden, ohne dass die Grenze zwischen Leitung und Angestelltem 
        verschwindet. Auch wenn eine Idee verworfen wird, dann macht das nichts. 
        Die Motivation, am Unternehmen mitzuarbeiten, bleibt hoch.&quot;<br>
        Der Kontakt mit dem Unternehmen &quot;Lorenz Victorbur&quot; war f&uuml;r 
        Manfred Rumi kein Zufall: &quot;Wichtig war mir, eine Firma zu w&auml;hlen 
        und zu betreuen, in der das Klima schon gut ist. Ich bin kein Friedensstifter 
        f&uuml;r zerstrittene Angestellte oder zerr&uuml;ttete Umgangsformen.&quot; 
        Das w&uuml;rde sich auch mit der Arbeitsweise von Pluspunkt nicht decken. 
        Denn der Ansatz geht hier vom Menschen aus. &quot;Die Hilfestellung ist 
        meine Hilfe&quot;, sagt Manfred Rumi. Durch Rollenspiele und teilweiser 
        extremer Provokation l&auml;sst Coach Rumi die Angestellten selber ihren 
        Weg finden. Dabei ver&auml;ndert sich auch schnell einiges im Menschen: 
        ,Ich hatte eine wahre Schaffenskrise&quot;, gesteht B&auml;ckermeister 
        Holger Sigl. Seine Arbeit sei halt ein Job gewesen, und richtig Spa&szlig; 
        habe er als Teigmacher nicht mehr gehabt. &quot;W&auml;hrend des Seminars 
        hab ich mir Gedanken &uuml;ber den Betrieb und meine Rolle im Team gemacht. 
        Die Diskussionen haben mir neues Selbstvertrauen gegeben, im wahrsten 
        Sinne des Wortes, ich traue mir nun selber viel mehr zu und weiss, dass 
        dies auch von den Kollegen gefordert wird.&quot; In diesem Fall setzte 
        sich der B&auml;ckermeister gest&auml;rkt durch den Zuspruch seiner Kollegen 
        ein neues Ziel: Stellvertretender Produktionsleiter zu werden. Ein Effekt, 
        den er selber am wenigsten erwartete. &quot;,Was soll das denn?' war ganz 
        ehrlich meine erste Reaktion, als ich zum ersten Mal am Coaching teilgenommen 
        hatte&quot;, gesteht Holger Sigi. Heute, ein paar Wochen nach dem Training 
        wirbt der Teigmacher im Betrieb f&uuml;r die Schulung von Manfred Rumi 
        und &uuml;berzeugt die letzten Skeptiker.<br>
        &quot;Ein w&uuml;nschenswerter Effekt ist auch, dass das Training im Betrieb 
        weiter l&auml;uft&quot;, sagt Manfred Rumi. ein effektiver Produktivit&auml;tsfaktor. 
        Wenn der Stein einmal ins Rollen gebracht worden sei, w&uuml;rden sich 
        viele Mitarbeiter selber coachen. Durch die Schulung wird der Blickwinkel 
        auf die Kollegen ver&auml;ndert. Nicht die Frage &quot;Was machen die 
        anderen falsch&quot;, sondern &quot;Wo k&ouml;nnenwir was besser machen&quot; 
        pr&auml;ge das Bild. Ziel von Rumis Programm ist nicht eine ewige Besserwisserei, 
        sondern der konstruktiv-kritische Umgang miteinander.</font></p>
      <p><font color="#000000"><b>Motiviert und produktiv</b><br>
        Die Frage nach dem messbaren Erfolg beantwortet der Juniorchef im Betrieb 
        der Wikinger-Familie, Martin Lorenz: &quot;In Heller und Pfennig ist der 
        Nutzen so eienr Schulung nat&uuml;rlich nicht zu &uuml;berpr&uuml;fen. 
        Aber wir als Haus mit einer Schulungstradition wissen, dass wir den Marktvorteil 
        der Mitarbeitermotivation nutzen k&ouml;nnen. Und ein sehr gutes Betriebsklima 
        zahlt sich immer aus.&quot; Der Streit aus dem konstruierten Fall zwischen 
        Filialleitung und Disponent jedenfalls h&auml;tte durch trainierte Kommunikation 
        vermieden werden k&ouml;nnen.</font></p>
