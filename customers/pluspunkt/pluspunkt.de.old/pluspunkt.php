<h1>Pluspunkt Unternehmensentwicklung GMBH</h1>
<br><br>
Unsere Mission - Unser Selbstverständnis<br>
Pluspunkt berät Führungskräfte und deren Mitarbeiter in allen Fragen der Unternehmensleitung und -entwicklung.
<br>
Pluspunkt schafft die Rahmenbedingungen für Unternehmensentwicklungsleistungen und koordiniert sie. Wir arbeiten ausschließlich mit ausgewählten Partnern zusammen.
<br>
Unser Anspruch: wir unterstützen unsere Kunden, indem wir alle Prozesse ganzheitlich betrachten, Veränderungsprozesse zusammen mit den Betroffenen entwickeln und die Projektleitung in der Umsetzungsphase koordinieren.
<br><br>
Gründe für Pluspunkt<br>
• Die Inhaber der Pluspunkt GmbH verfügen über 15 Jahre Trainingserfahrung. Dies garantiert Ihnen reibungslose Seminare und Trainings.
<br>
• Die Ausbildung zum Kursleiter erfolgte durch Dale Carnegie Training®. Dadurch werden internationale Standards realisiert.
<br>
• Die Trainer sind als Selbstständige tätig und denken folglich unternehmerisch. Sie können auf das Verständnis für unternehmerische Entscheidungen setzen.
<br>
• Jeder assoziierte Partner von Pluspunkt genügt definierten Qualitätsstandards. Das bedeutet Qualitätssicherheit für unsere Kunden.
<br>
• Pluspunkt ist werteorientiert. Unsere Kunden sind sich durch Fairneß aller Beteiligten untereinander bewußt.
<br>
• Pluspunkt verfolgt einen ganzheitlichen Ansatz. Wir lassen unsere Kunden nicht mit Problemen allein. Wir haben Partner, die Probleme lösen.
<br>
• Es existiert ein Partnernetzwerk mit weiteren Dienstleistern. Hierdurch haben Sie Zugriff auf eine breit Dienstleistungsbandbreite.
<br>
• Pluspunkt ist Herausgeber eines Buches zum Thema Kommunikation. Hierdurch erhalten Sie Inspirationen zu den unterschiedlichsten Facetten der Kommunikation.
<br>
• Pluspunkt vrfügt über ein selbst entwickeltes Persönlichkeits training (SEAL-Training). Dies gibt Ihnen die Möglichkeit, sich mit ihrem Lebensziel, Ihren Neigungen und Vorlieben zu verwirklichen.
<br>
• Die Inhaber von Pluspunkt stammen aus dem Handwerk und arbeiten für das Handwerk. In dem wir die Sprache und die Bedürfnisse der Handwerker verstehen, entwickeln wir geeignete Hilfestellungen.
