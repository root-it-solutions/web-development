            <h1><b>Presse</b></h1>
            <p><b><font size="2">Pluspunkte sammeln f&uuml;r die Unternehmensf&uuml;hrung</font></b> 
              - 25.08.03<br>
              Neues Managementbuch einmal erfrischend anders - erfrischend praktisch<br>
              <br>
              Angesichts einer &uuml;berbordenden F&uuml;lle von Managementb&uuml;chern 
              kann man die Entr&uuml;stung beinahe h&ouml;ren. Nein, nicht schon 
              wieder. Eine Handreichung von diesen selbst ernannten Spezialisten 
              f&uuml;r Kommunikation. Halt! Immer mit der Ruhe, und erst mal reinschauen 
              in &quot;Typisch!&quot; Oder in der beigef&uuml;gten DVD rumst&ouml;bern, 
              wo kurz und knapp, dazu noch pointiert die Intention auf den Punkt 
              gebracht wird. Da kann man eine Menge erfahren &uuml;ber die &quot;Potenziale 
              erlebter Kommunikation&quot;. Ein Titel, der sich quasi an alle 
              richtet. An die Entscheider in der Wirtschaft, an die Medienschaffenden 
              und den Rest, der mit diesem Wissen, wenn er es denn umsetzt, alltagstauglicher 
              und damit erfolgreicher wird. Zu empfehlen ist die Lekt&uuml;re 
              auch allen, die mit dem Gedanken spielen, sich selbstst&auml;ndig 
              zu machen. </p>
            <p>Und so kann der Leser sich einzelne Kapitel, der gut ein Dutzend 
              Autoren, rauspicken. &quot;Der Mensch steht im Mittelpunkt&quot; 
              zeigt die vielf&auml;ltigen M&ouml;glichkeiten der verbalen Kommunikation 
              auf innerbetrieblicher Ebene auf. Als gutes Beispiel nonverbaler 
              Kommunikation sei das Kapitel &quot;Kleider machen Leute&quot; empfohlen. 
              Klar werden die meisten sagen, alles schon mal geh&ouml;rt. Wer 
              sich dann aber die M&uuml;he macht und das ist das schwierigste, 
              selbstkritisch die gar nicht so neuen Erkenntnisse an sich &uuml;berpr&uuml;ft, 
              wird so manches feststellen, das dringend einer Verbesserung bedarf.</p>
            <p>Die Autoren sind keine reinen Theoretiker, schon gar keine Ideologen, 
              denn fast alle k&ouml;nnen auf eine fundierte handwerkliche und 
              akademische Ausbildung zur&uuml;ckblicken. Die Summe von beidem 
              spiegelt sich in den Texten wider. Gemeinsam ist ihnen, so unterschiedlich 
              ihre Arbeitsfelder auch sein m&ouml;gen, das Menschenbild, das ihren 
              Erkenntnissen zugrunde liegt. Der Mensch, in diesem Fall der Mitarbeiter 
              und der Kunde, steht im Mittelpunkt. Das mag f&uuml;r die Herren 
              aus der Chefetage zun&auml;chst eine bittere Pille sein. Da kommt 
              denen der Unternehmensberater erst einmal wie der Pfarrer vor. H&ouml;ren 
              sie genau zu, merken sie schnell, was dahinter steckt. </p>
            <p>F&uuml;r beide Seiten ist diese Vorgehensweise ein Gewinn. Bezieht 
              man die Besch&auml;ftigten in Planungsprozesse mit ein, so f&uuml;hlen 
              sie sich ernstgenommen und werden in Zukunft motivierter sein. Ein 
              weiterer Pluspunkt: Die Praktiker aus den Abteilungen kommen mit 
              vielen sehr sinnvollen Vorschl&auml;gen und Ideen, die ein Abteilungsleiter 
              oder eine externe Unternehmensberatung gar nicht mitbringen kann. 
              Besonders wichtig ist dieses Verfahren, wenn Umstrukturierungsma&szlig;nahmen 
              im Unternehmen anstehen. Nichts kann die Motivation von Mitarbeitern 
              st&auml;rker l&auml;hmen, als dass sie davon aus den Medien erfahren. 
              Angst geht um, berechtigt oder nicht. Mit einer funktionierenden 
              innerbetrieblichen Kommunikation lassen sich solche Konfliktsituationen 
              abmildern oder sogar vermeiden. </p>
            <p>Zu dem Menschenbild der Autoren geh&ouml;rt aber zun&auml;chst 
              auch das Finden der eigenen Identit&auml;t. So sind sie der &Uuml;berzeugung, 
              dass die Au&szlig;enwirkung schon entscheidenden Einfluss auf den 
              positiven oder negativen Verlauf eines Gespr&auml;chs hat. Es ist 
              ja offensichtlich und deutet f&uuml;r den Betrachter auf eine gewisse 
              Z&uuml;gellosigkeit hin, wenn man mit der Figur etwas aus den Fugen 
              geraten und die Kleidung so eng ist, dass man aussieht wie die Wurst 
              in der Pelle. Oder bei der Farbwahl voll daneben liegt. Als Beispiel 
              wird hier eine Rothaarige in einem pinkfarbenen Kost&uuml;m genannt. 
              Und die richtige Au&szlig;enwirkung erreicht man nur, wenn das Outfit 
              vom Scheitel bis zur Sohle dem Typ entspricht. Wem dies nicht gelingt, 
              dem bleibt nichts anderes &uuml;brig als in eine sach- und fachgerechte 
              Beratung zu gehen. Im Normalfall gelingt es dann, die positiven 
              Eigenschaften zu verst&auml;rken und die Schw&auml;chen zu kaschieren. 
              Ein Pluspunkt mehr also auf dem Weg zum individuellen Gesamtkunstwerk. 
              Wirkungsvoll ist das allemal. Und der Erfolg stellt sich beinahe 
              von alleine ein.</p>
            <p>Das bei W. Bertelsmann erschienene Managementbuch &quot;Typ[isch]! 
              Potenziale erlebter Kommunikation&quot; (Preis 24,90 &euro;), bietet 
              dabei eine Vielzahl ganz unterschiedlicher Aspekte und Perspektiven 
              an. Das, was auf den ersten Blick wie ein bunt best&uuml;ckter Bauchladen 
              erscheint, entpuppt sich bei n&auml;herem Hinsehen als eine wohldosierte 
              Mischung hochwirksamer Strategien, die neugierig macht auf mehr 
              und die auf vielf&auml;ltige Weise praktikable Anregungen vermittelt. 
              Was kann man besseres von einem Fachbuch &uuml;ber neue Unternehmensstrategien 
              noch sagen?!</p>
            <p>Die unterschiedlichen Ans&auml;tze verbinden sich im &Uuml;brigen 
              zu einem feinen Netzwerk, das die Autoren untereinander bilden und 
              miteinander verbindet. Dreh- und Angelpunkt dieses Netzwerkes ist 
              das M&uuml;nsteraner Unternehmen &quot;Pluspunkt Unternehmensentwicklung&quot;, 
              das sich vor allem auf das Thema Personalentwicklung spezialisiert 
              hat. Der Zusammenhalt der Autoren zeugt von diesem Konzept und gibt 
              damit auch f&uuml;r Pluspunkt und deren Netzwerk 
              eine lebendige Visitenkarte ab. Ins Netz gehen bekommt damit eine 
              befreiende Bedeutung.<br>
            </p>
