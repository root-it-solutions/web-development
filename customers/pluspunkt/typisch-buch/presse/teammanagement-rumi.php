            <h1><b>Presse</b></h1>

            <p><b><font size="2">Ein starkes Team</font></b> - Chemie Technik 
              Ausgabe 6 / 2004<br>

              Geschlechtsspezifische Unterschiede verstehen und richtig einsetzen<br>

              <br>

              <b>Ob zuh&ouml;ren oder Ratschl&auml;ge geben - in der Kommunikation 

              k&ouml;nnten die Unterschiede zwischen Frauen und M&auml;nnern kaum 

              gr&ouml;&szlig;er sein. Nur wer gelernt hat, mit diesen Unterschieden 

              umzugehen, kann sie produktiv einsetzen. Das gilt vor allem im Gesch&auml;ftsleben, 

              wenn die beiden Geschlechter in unterschiedlichen Positionen aufeinander 

              sto&szlig;en - als Mitarbeiter und als F&uuml;hrungskraft.</b></p>

            <p>Die Unterschiede zwischen Frauen und M&auml;nnern scheinen im heutigen 

              Berufsleben so gut wie weggewischt zu sein. Ein Blick in den Spitzensport 

              st&uuml;tzt diese Ansicht. Die Williams-Schwestern dreschen den 

              Ball mit ihren Muskelpaketen so hart &uuml;ber das Tennisnetz, dass 

              sie problemlos bei den Herren mithalten k&ouml;nnten. Annika S&ouml;renstam 

              schwingt ihren Schl&auml;ger so perfekt, dass gestandene Golfprofis 

              sich vor laufender Kamera einen &auml;hnlich perfekten Schwung f&uuml;r 

              ihr Spiel w&uuml;nschen. Und Jutta Kleinschmidt donnert mit ihrem 

              Auto durch die Sahara und l&auml;sst auf der Strecke Paris-Dakar 

              s&auml;mtliche m&auml;nnlichen Konkurrenten im W&uuml;stenstaub 

              hinter sich.</p>

            <p><b>Unterst&uuml;tzung richtig anbieten und auch annehmen</b></p>

            <p>Frauen und M&auml;nner haben in unserer Gesellschaft die gleichen 

              Rechte und Pflichten. Damit h&ouml;ren die Gemeinsamkeiten aber 

              auch fast schon auf. Von Frauen kann man zum Beispiel lernen wie 

              wichtig es ist, anderen zuzuh&ouml;ren.<br>

              <br>

              M&auml;nner dagegen nehmen Hilfe in der Regel nur dann gerne an, 

              wenn sie selbst darum bitten. Doch auch das geh&ouml;rt nicht gerade 

              zu ihrem Alltag. Freiwillig fragen die meisten nur dann um Rat, 

              wenn sie wirklich nicht weiterwissen. Eine gute M&ouml;glichkeit 

              f&uuml;r F&uuml;hrungsfrauen besteht darin, den Mann um einen Rat 

              zu bitten, anstatt eigene Hilfe anzubieten. Sonst wird sie bei ihm 

              m&ouml;glicherweise das Gef&uuml;hl ausl&ouml;sen, dass er in ihren 

              Augen seinen Job nicht richtig macht.<br>

              <br>

              Wenn eine 'starke' Frau es schafft, einen Mann sein Ziel selbst 

              erreichen zu lassen und ihm dann noch die geb&uuml;hrende Anerkennung 

              geben kann, dann ist sie auf dem richtigen Weg. Eine Frau dagegen 

              f&uuml;hlt sich angenommen, wenn man ihr Hilfe zur Erreichung ihrer 

              Ziele anbietet. In der gleichen Situation f&uuml;hlt sich ein Mann 

              eher angegriffen oder verletzt. Chefinnen sollten deshalb versuchen, 

              keine ungebetenen Ratschl&auml;ge zu geben, sondern nur Informationen. 

              Im Unterschied zu M&auml;nnern sind die meisten Frauen im Umgang 

              mit Problemen v&ouml;llig unproblematisch. Sie sprechen gerne &uuml;ber 

              Probleme - allerdings nicht nur, um eine L&ouml;sung zu finden. 

              Oft suchen sie N&auml;he. Der Mann wittert in einer solchen Situation 

              seine Chance. Er hat eine L&ouml;sung parat und will sie der Frau 

              pr&auml;sentieren. In diesem Fall sollte er allerdings darauf verzichten, 

              einen ungebetenen Tipp zu geben, da die Frau sich unverstanden f&uuml;hlen 

              wird. Er sollte einfach zuh&ouml;ren und ihre Gef&uuml;hle sp&uuml;ren.<br>

              <br>

              Wenn Frauen mit M&auml;nnern &uuml;ber ihre Probleme sprechen, dann 

              ist das auch ein Zeichen von Vertrauen. Frauen m&uuml;ssen nicht 

              st&auml;ndig die eigene Kompetenz unter Beweis stellen, was bei 

              M&auml;nnern oft fast zwanghaft der Fall ist. Deshalb f&auml;llt 

              es Frauen auch leicht, Hilfe anzubieten und anzunehmen. Je mehr 

              Einzelheiten eine Chefin in einem Gespr&auml;ch, in einer Sitzung 

              oder bei einer Pr&auml;sentation schildert, desto angespannter wird 

              der Mann. Schlie&szlig;lich haben die Herren der Sch&ouml;pfung 

              ihre L&ouml;sung f&uuml;r das Problem schon l&auml;ngst im K&ouml;cher. 

              Erwartungsvoll harren sie der Chance, die Idee endlich abfeuern 

              zu d&uuml;rfen. <br>

              <br>

              Deshalb folgender Tipp: <br>

              Wenn die Chefin wei&szlig;, was sie erreichen will, dann sollte 

              sie ihm den Vortritt lassen. Die Einzelheiten k&ouml;nnen anschlie&szlig;end 

              folgen. So hilft sie dem Mann, ihr entspannt zuh&ouml;ren zu k&ouml;nnen.</p>

            <p><b>Entspannte Atmosph&auml;re erleichtert vieles</b></p>

            <p>Die Unterschiede zwischen M&auml;nnern und Frauen k&ouml;nnen Probleme 
              mit sich bringen, wenn man sie nicht versteht oder nicht mit ihnen 
              umgehen kann. Das gilt nicht nur f&uuml;rs Gesch&auml;ftsleben. 
              Gerade im Privatleben kann es auf Grund der Differenzen zu Spannungen 
              kommen. Das wiederum hat Auswirkungen auf die Arbeit. Um eine beruflich 
              und privat zufrieden stellende L&ouml;sung zu finden, bietet es 
              sich oft an, dass Teilnehmer von F&uuml;hrungsseminaren ihre Lebenspartner 
              mitbringen.<br>
              <br>
              Sicher ist: Wenn man die unterschiedlichen St&auml;rken und Qualit&auml;ten 
              von Mann und Frau versteht und richtig einsetzt, sind sie im Verbund 
              unschlagbar. Entsprechend dem Motto: Die Mischung macht's. <br>
              <br>
              <br>
              Manfred Rumi, Unternehmensberatung Pluspunkt</p>
            <p><b>Interview mit Manfred Rumi</b></p>
            <p><b><font size="2">&quot;Pers&ouml;nliche und soziale Kompetenz 
              entwickeln&quot;</font></b></p>
            <p><b>CT: </b>Welche typischen Probleme in der Kommunikation unter 
              m&auml;nnlichen und weiblichen Kollegen in naturwissenschaftlich- 
              technischen Berufen f&uuml;hren nach Ihrer Erfahrung besonders h&auml;ufig 
              zu Problemen? <br>
              <b><br>
              Rumi: </b>Wenn Partner ihr Gegen&uuml;ber nicht verstehen, nichts 
              &uuml;ber ihre Unterschiedlichkeit wissen, kostet es Energie. Die 
              Folge sind in vielen F&auml;llen Auseinandersetzungen, K&auml;mpfe, 
              &Auml;ngste, private oder berufliche Trennung, Frust oder auch Mobbing. 
              In einem solchen Umfeld Ver&auml;nderung zu bewirken, Neues zu kreieren, 
              ist schwer wenn nicht gar unm&ouml;glich. <br>
              <br>
              Extrovertierte Entscheider &#150; ob M&auml;nner oder Frauen &#150; 
              dominieren oft. Introvertierte, gef&uuml;hlsm&auml;&szlig;ig angelegte 
              Frauen und M&auml;nner haben oftmals Schwierigkeiten, den anderen 
              zu verstehen, weil sie seine Sprache nicht &#132;&uuml;bersetzen&#147; 
              k&ouml;nnen. M&auml;nnern und Frauen sowie Teams, die in einem solchen 
              vermeintlichen Spannungsfeld stehen und von denen erwartet wird, 
              dass sie erfolgreich zusammenarbeiten, empfehle ich in einem ersten 
              Schritt ihre Werte und Dominanzen &uuml;ber einen Pers&ouml;nlichkeitstest 
              herauszufinden. Sie erfahren so schnell, wer sie in ihrem Verhalten 
              sind. Mit dieser Erkenntnis verringern sie z.B. Fehlbesetzungen 
              von Positionen und Teams. </p>
            <p><b>CT: </b>Gleichen sich m&auml;nnliche und weibliche Denk- und 
              Kommunikationsweisen durch die zunehmende Gleichstellung von Mann 
              und Frau mit der Zeit an? <br>
              <b><br>
              Rumi:</b> Ich glaube nicht, dass sich durch die gesellschaftlichen 
              Ver&auml;nder-ungen in den Grundanlagen von M&auml;nnern und Frauen 
              etwas ver&auml;ndern wird. Frauen und M&auml;nner adaptierten ihre 
              Werte in einem ver&auml;nderten Umfeld. Das hei&szlig;t, sie verbiegen 
              sich, um so zu sein, wie es die Position oder Gesellschaft von ihnen 
              erwartet. Das birgt gro&szlig;e Gefahren, die zu innerer K&uuml;ndigung 
              oder Krankheit f&uuml;hren k&ouml;nnen. Genau wie im Berufsleben 
              sind auch privat die zwischenmenschlichen Beziehungen entscheidend 
              f&uuml;r Zufriedenheit und Erfolg. F&uuml;hrungskr&auml;ften gerade 
              in naturwissenschaft-lichen technischen Bereichen empfehle ich, 
              sich und ihre Mitarbeiter auf den Gebieten der pers&ouml;nlichen 
              und sozialen Kompetenz zu entwickeln. </p>
