            <h1><b>Presse</b></h1>
            <p><b><font size="2">Veranstaltung macht Appetit auf mehr</font></b> 
              - 18.08.03<br>
              Bernhard Rothenberger &uuml;ber das richtige Catering und perfekte 
              Events<br>
              <br>
              Nach Knigge klingt das nicht gerade: Schraubenzieher, Holzs&auml;ge 
              und Spachtel als Besteck, Reagenzgl&auml;ser und &Ouml;lk&auml;nnchen 
              als Trinkgef&auml;&szlig;e. Dreck soll ja gut f&uuml;r den Magen 
              sein. Aber das? </p>
            <p>Liest man ein wenig weiter, was Bernhard Rothenberger in seinem 
              Aufsatz &quot;Die Kommunikation Ihrer Veranstaltung&quot; schreibt, 
              beruhigt sich der Magen wieder wie nach einem Grappa beim Italiener. 
              Der Chef der Firma Gastronomie Projektbegleitung in M&uuml;nster 
              ist &uuml;ber jeden Verdacht erhaben, keine Tischmanieren zu besitzen. 
              Seine Vorschl&auml;ge zeugen eher von Kreativit&auml;t als von verrohten 
              Sitten. Man darf sie nur nicht aus dem Zusammenhang rei&szlig;en. 
            </p>
            <p>Das ungew&ouml;hnliche Essbesteck kann sich der gelernte Hotelier 
              nur auf einer Party vorstellen, dessen Gastgeber ein Werkzeughersteller 
              ist. Nagelneu und steril muss es nat&uuml;rlich auch sein.</p>
            <p>Bernhard Rothenberger macht sich eher mehr Gedanken als die meisten, 
              wenn es um den richtigen Service zu Tische geht. Das ist sein Beruf. 
              Er hat einfach gute Ideen, und verr&uuml;ckte Einf&auml;lle sind 
              allemal besser als keine. Als Ideengeber f&uuml;r Betriebsfeste 
              und &auml;hnlich gro&szlig;e Veranstaltungen wei&szlig; er, dass 
              man als Gastgeber auch dann kommuniziert, wenn man nichts sagt beziehungsweise 
              nichts macht. Denn der Service spiegelt die Qualit&auml;t der Dienstleistung 
              wider.</p>
            <p>Mit den Vorurteilen und Fehleinsch&auml;tzungen, Betriebsfeste 
              k&ouml;nne jeder planen, r&auml;umt Rothenberger fachm&auml;nnisch 
              und sehr gr&uuml;ndlich auf. Wichtige Einzelheiten wie die Einbeziehung 
              selbst des Corporate Designs einer Firma w&uuml;rden zu oft vernachl&auml;ssigt, 
              wenn die Veranstaltung nicht in professionelle H&auml;nde gegeben 
              w&uuml;rde.</p>
            <p>Er selber scheint eher Perfektionist zu sein. Detailgetreu beschreibt 
              Rothenberger, welche Punkte er ber&uuml;cksichtigte, als er eine 
              Party f&uuml;r 250 G&auml;ste eines renommierten Softwareherstellers 
              plante. Allein ein Auszug aus dem Men&uuml; l&auml;sst einem das 
              Wasser im Mund zusammen laufen: Herzmuscheln mit Tomaten, Rosmarin 
              und Balsamico oder Lammkeule im Heu gegart mit Sellerie-Kartoffelragout. 
              Genie&szlig;ergaumen, was willst du mehr?! Das ganze lie&szlig; 
              er in einem Planetarium mit angrenzendem Naturkundemuseum reichen, 
              das zwecks dieser Veranstaltung noch extra aufgepeppt worden war. 
              Die &uuml;berwiegend jungen G&auml;ste waren begeistert.</p>
            <p>Selbst eine gute Schlachtplatte kann da nicht mithalten. Und der 
              Aufwand sei kein Verlustgesch&auml;ft, kalkuliert Rothenberger. Durch den besonderen 
              Touch f&uuml;hle sich der Kunde ernst genommen und kriege das Gef&uuml;hl, 
              dass es wirklich um ihn ginge und er dem Gastgeber etwas wert sei. 
              Kommunikation auf einer nonverbalen Ebene, kann man das wohl nennen. 
              Liebe geht halt &uuml;ber den Magen.</p>
            <p>Auch die anderen Beitr&auml;ge in dem Buch &quot;Typ[isch]! Potenziale 
              erlebter Kommunikation&quot;, in dem Rothenbergers Beitrag erschienen 
              ist, drehen sich, wie der Titel bereits sagt, um Kommunikation. 
              Derart schmackhaft sind aber nur wenige Formen der behandelten zwischenmenschlicher 
              Austauschm&ouml;glichkeiten.</p>
            <p>Bernhard Rothenberger ist einer von 12 Autoren und Fachleuten, 
              die sich mit den meist ungenutzten Potenzialen der Kommunikation 
              besch&auml;ftigen. Das bei W. Bertelsmann erschienene Managementbuch 
              &quot;Typ[isch]! Potenziale erlebter Kommunikation&quot; (Preis 
              24,90 &euro;), bietet dabei eine Vielzahl ganz unterschiedlicher 
              Aspekte und Perspektiven an. Das, was auf den ersten Blick wie ein 
              bunt best&uuml;ckter Bauchladen erscheint, entpuppt sich bei n&auml;herem 
              Hinsehen als eine wohldosierte Mischung hochwirksamer Strategien, 
              die neugierig macht auf mehr und die auf vielf&auml;ltige Weise 
              praktikable Anregungen vermittelt. Was kann man besseres von einem 
              Fachbuch &uuml;ber neue Unternehmensstrategien noch sagen?!</p>
            <p>Die unterschiedlichen Ans&auml;tze verbinden sich im &Uuml;brigen 
              zu einem feinen Netzwerk, das die Autoren untereinander bilden und 
              miteinander verbindet. Dreh- und Angelpunkt dieses Netzwerkes ist 
              das M&uuml;nsteraner Unternehmen &quot;Pluspunkt Unternehmensentwicklung&quot;, 
              das sich vor allem auf das Thema Personalentwicklung spezialisiert 
              hat. Der Zusammenhalt der Autoren zeugt von diesem Konzept und gibt 
              damit auch f&uuml;r Pluspunkt und deren Netzwerk 
              eine lebendige Visitenkarte ab.</p>
