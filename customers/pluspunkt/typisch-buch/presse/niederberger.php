            <h1><b>Presse</b></h1>
            <p><b><font size="2">So wird die Glotze zum Lehrmeister</font></b> 
              - 09.09.03<br>
              Neue Managementmethoden: Christoph Niederberger begeistert sich 
              &uuml;ber neue Lernmedien<br>
              <br>
              Unz&auml;hlige Stunden flatterte das Moorhuhn &uuml;ber die Monitore 
              arbeitsunwilliger deutscher Arbeitnehmer und mit ihm das Geld aus 
              dem Fenster. Nicht wenige Unternehmen mussten aufgrund der verballerten 
              Arbeitszeit Federn lassen. Bei den Arbeitgebern stand hingegen nicht 
              der Vogel, sondern das Spiel ganz oben auf der Abschussliste. </p>
            <p>Dieser Spieltrieb, der f&uuml;r Millionenverluste sorgte, kann 
              allerdings auch wirtschaftlichen Nutzen haben, meint Christoph Niederberger. 
              Der selbstst&auml;ndige Trainer, Berater und Coach arbeitet selbst 
              mit interaktiven Medien, wenn es um Aus- und Weiterbildung geht. 
              In dem Buch &quot;Typ[isch]! Potenziale erlebter Kommunikation&quot; 
              erl&auml;utert er in seinem Beitrag &quot;Interaktive Lernmedien 
              - mehr Effektivit&auml;t in der Personalentwicklung!&quot; diese 
              neuartige Form der Wissensvermittlung auf anschauliche Weise. </p>
            <p>Das Konzept, mit dem er die Motivation von Arbeitnehmern f&uuml;r 
              Schulungen steigern m&ouml;chte, ist einfach, aber effektiv, da 
              es auf den Spieltrieb der Mitarbeiter setzt: Professionelle Schauspieler 
              stellen verschiedene M&ouml;glichkeiten beispielsweise eines Verkaufsgespr&auml;chs 
              nach. Das &quot;Realityspiel&quot; wird auf einer DVD gespeichert, 
              und der Arbeitnehmer kann dann interaktiv die F&uuml;hrung eines 
              optimalen Kundengespr&auml;chs spielend erlernen. Wenn er taktisch 
              falsch vorgeht, kann er einen neuen Versuch starten. So ist das 
              halt in der Computerwelt. Wenn die Moorh&uuml;hner abgeschossen 
              sind, erweckt man sie mit einem Mausblick wieder zum Leben. Das 
              gleiche gilt f&uuml;r den virtuellen Kunden. In der Realit&auml;t 
              s&auml;he das anders aus. Der Kunde w&auml;re zwar nicht klinisch 
              tot, f&uuml;r das Unternehmen aber eventuell dennoch gestorben.</p>
            <p>Zudem werden die Kosten f&uuml;r die Schulungen gesenkt, da das 
              Anmieten von Seminarr&auml;umen, Arbeitszeitausfall durch lange 
              Anreisen, Verk&ouml;stigung und viele andere Umkosten ganz wegfallen. 
              Selbst weltweite Unternehmen k&ouml;nnen mit einem mehrsprachigen 
              Programm alle Mitarbeiter erreichen. Und da bei einer DVD nicht 
              einmal ein Computer zur Verf&uuml;gung stehen muss, kann man auch 
              problemlos Zuhause auf &quot;Kundenjagd&quot; gehen.</p>
            <p>Ein weiterer Vorteil einer derart spielend leichten Schulung ist 
              der, dass der Verk&auml;ufer auch in die Rolle des Kunden schl&uuml;pfen 
              kann. Durch den Rollentausch sieht und sp&uuml;rt er, wie sein eigenes 
              Verhalten auf andere wirkt. Niederberger geht davon aus, dass der 
              kommunizierende Mensch zwei Drittel der gesamten Informationen dem 
              Tonfall und der K&ouml;rpersprache seines Gegen&uuml;bers entnimmt. 
              Das richtige Auftreten will also (virtuell) gelernt sein.</p>
            <p>Zukunftsmusik hingegen sind rein computersimulierte Verkaufsgespr&auml;che, 
              Vortragssituationen oder Konferenzen. Noch! &quot;Das alles ist 
              heute schon m&ouml;glich&quot;, schreibt Niederberger, &quot;mit 
              den jetzigen Rechnerkapazit&auml;ten aber noch nicht kosteng&uuml;nstig 
              realisierbar. Wir sind aber nicht mehr weit davon entfernt.&quot; 
              Sp&auml;testens dann d&uuml;rfte eine neue Manie &uuml;ber Deutschland 
              und die ganze Welt schwappen. Selbst nach Feierabend werden dann 
              virtuelle Vortr&auml;ge gehalten, bis auch der unerwartetste Zwischenruf 
              fachm&auml;nnisch pariert wurde. Auf der Strecke bleiben werden 
              dann weder Moorhuhn noch der Chef. Ganz im Gegenteil. Letzterer 
              zieht damit den Jackpot.</p>
            <p>Das bei W. Bertelsmann erschienene Managementbuch &quot;Typ[isch]! 
              Potenziale erlebter Kommunikation&quot; (Preis 24,90 &euro;), bietet 
              dabei eine Vielzahl ganz unterschiedlicher Aspekte und Perspektiven 
              an. Das, was auf den ersten Blick wie ein bunt best&uuml;ckter Bauchladen 
              erscheint, entpuppt sich bei n&auml;herem Hinsehen als eine wohldosierte 
              Mischung hochwirksamer Strategien, die neugierig macht auf mehr 
              und die auf vielf&auml;ltige Weise praktikable Anregungen vermittelt. 
              Was kann man besseres von einem Fachbuch &uuml;ber neue Unternehmensstrategien 
              noch sagen?!</p>
            <p>Die unterschiedlichen Ans&auml;tze verbinden sich im &Uuml;brigen 
              zu einem feinen Netzwerk, das die Autoren untereinander bilden und 
              miteinander verbindet. Dreh- und Angelpunkt dieses Netzwerkes ist 
              das M&uuml;nsteraner Unternehmen &quot;Pluspunkt Unternehmensentwicklung&quot;, 
              das sich vor allem auf das Thema Personalentwicklung spezialisiert 
              hat. Der Zusammenhalt der Autoren zeugt von diesem Konzept und gibt 
              damit auch f&uuml;r Pluspunkt und deren Netzwerk 
              eine lebendige Visitenkarte ab. Ins Netz gehen bekommt damit eine 
              befreiende Bedeutung.<br>
            </p>
