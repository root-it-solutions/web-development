                  <p><b>Interview</b><br>
                    <br>
                    <b><i>Sie schreiben &uuml;ber eine Pers&ouml;nlichkeitstypologie. 
                    Welcher Typ sind Sie?</i><br>
                    <br>
                    </b>Wenn man von einem flexiblen &quot;lieber als ...&quot; 
                    ausgeht, bin ich extravertiert. Also jemand, der gern auf 
                    andere zugeht, leicht kennen zu lernen ist und auch schon 
                    mal bereit ist, etwas Vertrauliches von sich selbst zu erz&auml;hlen.<br>
                    <br>
                    Ich bin jemand, der kreative Prozesse bevorzugt und gern dabei 
                    ist, wenn es um eine neue Idee oder eine Konzeptentwicklung 
                    geht. Mir ist es wichtig, einen &Uuml;berblick &uuml;ber Dinge 
                    zu gewinnen und mich zu fragen: Tue ich &uuml;berhaupt das 
                    Richtige? Ich bin kein &quot;Macher-Typ&quot; und komme mit 
                    Dauer-Routine nicht so gut klar. Details sind eher eine Fehlerquelle.</p>
                  <p> Und dann bin ich - wie die Amerikaner sagen - ein &quot;Feeler-Typ&quot;. 
                    Das sind Menschen mit einer gef&uuml;hlsm&auml;&szlig;igen 
                    Beurteilung. Wenn es um die Frage geht: &quot;Wie w&uuml;rdest 
                    Du Dich am liebsten entscheiden?&quot;, dann bestimmt bei 
                    mir das Herz &uuml;ber den Kopf.<br>
                    <br>
                    Was meine grunds&auml;tzliche Lebenseinstellung angeht, bin 
                    ich jemand, der gern Entscheidungen trifft. Ich mag Strukturen 
                    und plane das, was zu tun ist.</p>
                  <p><b><i>Warum ist es wichtig, sich selber gut zu kennen?</i><br>
                    <br>
                    </b>Nur wer wei&szlig;, wohin er will, kommt auch dort an. 
                    Darum m&uuml;ssen wir uns im Leben Ziele stecken. Um Ziele 
                    zu finden, muss ich wissen, von wo ich starte. Wenn die Frage 
                    hei&szlig;t: &quot;Wie kann ich mich entwickeln, um ein Ziel 
                    zu erreichen?&quot;, dann muss ich wissen, was mir dabei hilft 
                    und was ich noch brauche. So kann ich herausfinden, was genau 
                    meine pers&ouml;nliche Lern-Herausforderung ist. Jemand, der 
                    sich pers&ouml;nlich gut einsch&auml;tzen kann, seine St&auml;rken 
                    und Schw&auml;chen kennt, kann sich zielorientiert entwickeln 
                    und bei dem, was er tut, sehr erfolgreich sein.<br>
                    <br>
                    <b><i>Was kann ich mit dem Wissen &uuml;ber verschiedene Pr&auml;ferenzen 
                    anfangen?<br>
                    </i><br>
                    </b>Im Alltag hilft mir dieses Wissen, den anderen besser 
                    anzunehmen - so wie er ist. Obwohl ich wei&szlig; - und das 
                    ist trivial - die Menschen sind verschieden und der andere 
                    ist anders als ich, kommt es im Alltag h&auml;ufig vor, dass 
                    wir uns dar&uuml;ber streiten, wer von uns Recht hat. Ich 
                    mit meiner tollen Idee oder der Andere mit seinem Satz: &quot;Das 
                    ist nicht machbar, was Du Dir da vorstellst.&quot; Anstatt 
                    zu bewerten, was gut oder machbar ist, versuche ich heraus 
                    zu arbeiten: Wie hilft mir das Andere? Wie kommen wir zusammen, 
                    um von beiden Ans&auml;tzen zu profitieren? Nicht nur eine 
                    tolle Idee, sondern auch deren Umsetzung ist sehr wichtig. 
                    Nur so kommt ein Projekt oder Vorhaben wirklich zum Erfolg. 
                    Dieses Wissen &uuml;ber unterschiedliche Pr&auml;ferenzen 
