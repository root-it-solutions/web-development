                <td><b>Interview</b><br>
                  &nbsp;<br>
                  <i><b>Worauf sollte bei Veranstaltungen besonders geachtet werden? 
                  </b></i>
<p>Es sind einmal die fünf Sinne, die dringend bedient werden 
                    müssen. <br>
                    Es sind die Ohren, die eine Musik aufnehmen wollen, die beruhigend 
                    wirkt, die nicht zu laut sein darf, die genau stimmen muss, 
                    die austariert, überlegt sein muss. Die Zunge muss etwas zum 
                    Schmecken haben. Die Nase muss mit Wohlgerüchen versorgt werden, 
                    die stimulierend wirken. Aber ich muss auch was fühlen können. 
                    Ich muss die richtige Raumtemperatur haben. Alle diese Dingen 
                    müssen vernünftig berücksichtigt werden. Das sind schon mal 
                    die Basis-, die Grundvoraussetzungen. </p>
                  <p><i><b>Was hat meine Veranstaltung mit Kommunikation zu tun? 
                    </b></i></p>
                  <p>Wenn ich also möchte, dass mein Unternehmen langfristig im 
                    Gespräch bleibt, dann muss ich dieses Ziel nicht nur definieren 
                    sondern es auch dementsprechend verfolgen. Es gibt einen Grundsatz, 
                    der dabei berücksichtigt werden muss. Das ist: "Der erste 
                    Eindruck ist entscheidend, und der letzte Eindruck bleibt." 
                    Man geht nicht einfach nur nach Hause und sagt: "Prima, das 
                    war eine gute Feier, da habe ich hervorragend gegessen und 
                    ich bin satt und zufrieden, lass uns mal zum Alltag übergehen." 
                    Sondern man geht vielmehr hin und reflektiert das Ganze. Man 
                    spricht noch mal mit demjenigen, der eingeladen hat, und man 
                    trägt es weiter. Der Name der Firma wird jedes mal am Markt 
                    neu projiziert. Genau das, was ich erreichen will.<font face="Arial, Helvetica, sans-serif"></font><font face="Arial, Helvetica, sans-serif"></font></p>
