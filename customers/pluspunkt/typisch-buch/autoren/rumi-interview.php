                <td><b>Interview</b><br>
                  &nbsp;<br>
                  <b><i>Wie sind Sie zu Ihrem Thema gekommen? </i></b>
<p>Als Führungskraft im Unternehmen spürte ich, dass ich bei 
                    Männern, wenn ich klare Anweisungen gab, gut das Ziel erreichte. 
                    Bei Frauen war das völlig anders. Ich bin nicht so stark auf 
                    ihre Eigenarten eingegangen. Erst als ich ganz einfache Hilfsmittel 
                    in der Ansprache entdeckte, z.B. die Bereitschaft auch meine 
                    eigenen Gefühle auszudrücken, gerade Frauen Liebe und Nähe 
                    zu geben, sie zu verstehen, Mitgefühl zu haben und sie auch 
                    ausreden zu lassen, konnte ich meine Ziele im Unternehmen 
                    viel eher erreichen. Und so bin ich letztendlich auch zu diesem 
                    Thema gekommen. </p>
                  <p><b><i>Geben Sie doch einmal ein Beispiel für die Unterschiede 
                    <br>
                    zwischen Männern und Frauen in der betrieblichen Praxis. </i></b></p>
                  <p>Wenn ich z.B. an eine kreative oder problemlösende Sitzung 
                    in einem Unternehmen denke, dann ist mir doch immer wieder 
                    aufgefallen, dass Frauen, wenn sie dabei sind kreative Ideen 
                    zu entwickeln oder wenn Sie dabei sind Probleme zu lösen, 
                    sehr viel reden. Und oftmals gerade wenn ein Mann solch eine 
                    Sitzung leitet, nervt ihn das. <br>
                    Ich habe festgestellt, dass gerade Frauen wenn sie reden ihre 
                    Gedanken klären und so letztendlich beim Sprechen zur Lösung 
                    kommen. <br>
                    Bei Männern ist das völlig anders. Sie diskutieren auch am 
                    Anfang. <br>
                    Wenn sie eine Lösung in der Sitzung gefunden haben, schweigen 
                    sie in der Regel und prüfen nochmals ab: "Ist die Situation 
                    jetzt klar für mich?". <br>
                    Und wenn sie sich sicher sind, dann kommen sie mit ihrer Lösung. 
                    <br>
                    Das muss der Team- oder Besprechungsleiter erkennen. </p>
                  <p><b><i>Wie lautet Ihre wichtigste Erkenntnis? </i></b></p>
                  <p>Was ich gelernt habe im Alltag: Männer müssen Frauen anders 
                    führen als Frauen Männer führen. Frauen führen Frauen anders 
                    als sie Männer führen. Es sind ganz ganz wenige Dinge, die 
                    sowohl in der Beziehung ihre Bedeutung haben und die auch 
                    ganz ganz stark im Umgang miteinander im beruflichen Alltag 
                    eine Rolle spielen. Dieses gilt es zu beachten. <font face="Arial, Helvetica, sans-serif"></font></p>
