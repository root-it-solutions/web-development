                        <p><span class="titel">Interview mit Frau Lena Strothmann</span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Modedesignerin und Damenschneidermeisterin, G&uuml;tersloh. 
                          15 Mitarbeiter.</p>
                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Ich habe an drei F&uuml;hrungsseminaren teilgenommen. 
                          Zwei waren Intervalltrainings, die hier in der Umgebung 
                          stattgefunden haben und an dem ersten F&uuml;hrungsseminar 
                          auf Fuerteventura habe ich teilgenommen.</p>
                        <p><b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Vor dem ersten F&uuml;hrungsseminar war ich in der Situation, 
                          dass ich an der Obergrenze meiner Kapazit&auml;ten angelangt 
                          war und vor der Entscheidung stand, den Betrieb zu vergr&ouml;&szlig;ern. 
                          Von daher waren f&uuml;r mich die Themen Planung, Zielsetzung 
                          und Mitarbeiterf&uuml;hrung sehr wichtig. Das zweite 
                          Seminar habe ich schon mit meinem Mann zusammen besucht, 
                          der auch Modedesigner ist. Wir standen damals vor der 
                          &Uuml;berlegung, unsere Firmen zu verschmelzen und erhofften 
                          uns Anregungen zu diesem Thema. Grunds&auml;tzlich gehe 
                          ich aber mit niedrigen Erwartungshaltungen an Fortbildungsveranstaltungen. 
                          <br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Grunds&auml;tzlich habe ich in den F&uuml;hrungsseminaren 
                          immer viel Selbstvertrauen bekommen und bin selbstbewusster 
                          zur&uuml;ckgekommen. Das liegt daran, dass ich mich 
                          viel mit mir selbst auseinander gesetzt und mich mit 
                          meinen St&auml;rken und Schw&auml;chen besser kennen 
                          gelernt habe. Als Unternehmer macht man viel aus dem 
                          Bauch heraus. In den F&uuml;hrungsseminaren, habe ich 
                          gelernt Ziele zu formulieren und sie mit Systematik 
                          und Disziplin zu verfolgen. <br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Besser zu organisieren, zu delegieren, eben besser zu 
                          f&uuml;hren. Ein sicherer Umgang mit Mitarbeitern und 
                          die Erkenntnis, wenn man Ziele formuliert und sich setzt, 
                          dann erreicht man sie auch. <br>
                          <br>
                          <b>Wie fanden Sie die Trainer?</b><br>
                          Motivierend und mitrei&szlig;end - kompetent.<br>
                          <br>
                          <b>Was halten Sie von dem Austragungsort?</b><br>
                          Ich konnte meine Tochter mit nach Fuerteventura nehmen, 
                          was ich sehr sch&ouml;n fand. Der Nachteil daran war, 
                          dass sie mich in der begrenzten Freizeit eben beanspruchte, 
                          was ja auch logisch ist, mir aber die Zeit nahm, den 
                          Seminarstoff entsprechend nachzubereiten. Die tolle 
                          Umgebung und die Urlaubsatmosph&auml;re haben mich zudem 
                          ziemlich abgelenkt. Von daher fand ich die Intervalltrainings 
                          &#130;Seminar - Betrieb - berichten' f&uuml;r mich systematischer. 
                          <br>
                          <br>
                          <b>H&auml;tten Sie das F&uuml;hrungsseminar gebucht, 
                          wenn es hier in Deutschland durchgef&uuml;hrt worden 
                          w&auml;re?</b><br>
                          Ja, Fuerteventura war aber auch schon reizvoll.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja, auf jeden Fall<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          An Schw&auml;chen kann ich mich nicht erinnern, es ist 
                          ja auch schon lange her. Das Einzige was ich vielleicht 
                          als Schw&auml;che bezeichnen kann ist, dass ich durch 
                          den Freizeitbereich sehr abgelenkt war.<br>
                          <br>
                          <b>Wie fanden Sie die Zusammenarbeit der Trainer?</b><br>
                          Sinnvolle Erg&auml;nzung.<br>
                          <br>
                          <b>K&ouml;nnten Sie eine Weiterempfehlung geben?</b><br>
                          Ja, das habe ich auch schon getan.<br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Bunt gemischt, sie kamen aus unterschiedlichen Branchen, 
                          es gab dadurch die verschiedensten Problematiken, fand 
                          ich sehr interessant. Zu vielen bestehen auch heute 
                          noch gute Kontakte. <br>
                          <br>
                          <b>Sollten Partner (Ehepartner) daran teilnehmen?</b><br>
                          Kommt auf die Situation eines jeden einzelnen an. Bei 
                          meinem ersten F&uuml;hrungsseminar fand ich es gut, 
                          dass ich alleine war, so konnte ich mich sehr auf mich 
                          konzentrieren. Beim zweiten Mal war mein Mann dabei, 
                          und in unserer Situation der Firmenverschmelzung war 
                          das auch sinnvoll.<br>
                          <br>
                          <b>Was ist Ihrer Meinung nach die optimale Teilnehmerzahl?</b><br>
                          Nicht zu wenige. Man kann durch die Coachings sehr viel 
                          von den Problemen der anderen Teilnehmer lernen. 10 
                          - 15 Leute sind eine gute Anzahl.</p>
