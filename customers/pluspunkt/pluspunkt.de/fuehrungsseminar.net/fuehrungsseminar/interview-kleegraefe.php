                        <p><span class="titel">Interview mit Herrn Heinrich Kleegr&auml;fe<font color="#FFD9A2"></font></span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Ma&szlig;schneider und Herrenausstatter, G&uuml;tersloh, 
                          12 Mitarbeiter</p>
                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Das ist schon lange her, das muss 1998 gewesen sein.<br>
                          <br>
                          <b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Ich bin von Hause aus Handwerker und hatte ein Defizit 
                          in <br>
                          Sachen F&uuml;hrung. Meine Erwartung war, das R&uuml;stzeug 
                          zum <br>
                          F&uuml;hren eines Unternehmens zu bekommen.<br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Die anderen Teilnehmer hatten ebensolche Defizite. Wir 
                          haben viel mit- und voneinander gelernt. Ich konnte 
                          mich in der angenehmen Umgebung gut fallen lassen und 
                          mich ganz dem F&uuml;hrungsseminar widmen, au&szlig;erhalb 
                          des Alltagstrotts. Ein Gro&szlig;teil der Teilnehmer 
                          waren Carnegie Leute und wir wurden in dieser Seminarwoche 
                          ein Team. Der zwischenmenschliche Bereich war sehr aufbauend.<br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Die Erkenntnis, dass es Mittel und Wege gibt, sein Unternehmen 
                          besser zu f&uuml;hren. Wir haben firmeninterne Fortbildungen 
                          und Mitarbeiterwochenenden durchgef&uuml;hrt, damit 
                          die Mitarbeiter mit ins Boot kamen. Ich habe auch weitere 
                          Seminare wie z. B. Pr&auml;sentation und Sealltraining 
                          besucht. Das Denken und Handeln des Erlernten habe ich 
                          im Laufe der Jahre verinnerlicht und wende es erfolgreich 
                          im Gesch&auml;ftsleben an. <br>
                          <br>
                          <b>Wie fanden Sie die Trainer ?</b><br>
                          Ralf Lohe ist gef&uuml;hlsbetont und einf&uuml;hlsam 
                          als Seminarleiter. Manfred Rumi ist schn&ouml;rkellos, 
                          rational, oft herausfordernd. Als Team erg&auml;nzen 
                          sie sich hervorragend.<br>
                          <br>
                          <b>Kritik?</b><br>
                          Die Nachbereitung des F&uuml;hrungsseminars fand z&ouml;gerlich 
                          bis gar nicht statt. Ich h&auml;tte mir eine Nachbetreuung 
                          gew&uuml;nscht.<br>
                          <br>
                          <b>Was halten Sie von dem Austragungsort?</b><br>
                          Der Veranstaltungsort ist ideal, es muss aber nicht 
                          Fuerteventura sein, Ich k&ouml;nnte mir auch einen netten 
                          Ort in Deutschland vorstellen. Der Feriencharakter muss 
                          aber vorhanden sein, Duisburg liegt da nicht an.<br>
                          <br>
                          <b>H&auml;tten Sie das F&uuml;hrungsseminar gebucht, 
                          wenn es hier in Deutschland durchgef&uuml;hrt worden 
                          w&auml;re?</b><br>
                          Damals nein, heute ja.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja, uneingeschr&auml;nkt.<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          Kann keine entdecken.<br>
                          <br>
                          <b>Wie fanden Sie die Zusammenarbeit der Trainer?</b><br>
                          Super.<br>
                          <br>
                          <b>K&ouml;nnten Sie eine Weiterempfehlung geben?</b><br>
                          Ja, auf jeden Fall<br>
                          <br>
                          <b>Gibt es etwas zu verbessern?</b><br>
                          Wei&szlig; nicht.<br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Genau so offen wie ich, Das war der Punkt, den das F&uuml;hrungsseminar 
                          auszeichnete, dass die Teilnehmer mit jedem Tag offener 
                          wurden.<br>
                          Sollten Partner (Ehepartner) daran teilnehmen?<br>
                          Das macht Sinn. <br>
                          <br>
                          <b>Was ist Ihrer Meinung nach die optimale Teilnehmerzahl?</b><br>
                          12 Leute.</p>
