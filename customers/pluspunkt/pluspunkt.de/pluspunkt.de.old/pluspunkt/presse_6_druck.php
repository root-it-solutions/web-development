      <p><font size="2"><b><font color="#000000">Motivation f&uuml;r Unternehmer</font></b></font><font color="#000000"><br>
        <br>
        <b>Sendener helfen Betriebsinhabern, die Werte ihrer Firmen zu erkennen</b><br>
        </font></p>
      <p><font color="#000000">-hk- Senden. Als Unternehmensberater, die zum Beispiel 
        Marketing- oder Finanzierungsprobleme l&ouml;sen, sehen sich Ralf Lohe 
        und Manfred Rumi nicht. Ihre Beratung hat einen anderen Ansatz. Der Unternehmer 
        soll seine eigenen Werte erkennen und sein Unternehmen danach ausrichten. 
        <br>
        Offenheit, Vertrauen, Verantwortung - &quot;das steckt in den meisten 
        Betriebsinhabern drin&quot; wissen Lohe und Rumi als langj&auml;hrige 
        Trainer f&uuml;r F&uuml;hrungskr&auml;fte. Vielen sei dieses Potenzial 
        aber nicht bewusst, &quot;und deshalb entwickeln wir es&quot; In Seminaren 
        mit wenigen Teilnehmern geht es den beiden Inhabern der Firma Pluspunkt 
        in Senden zun&auml;chst darum, das Selbstvertrauen und die Kommunikationsf&auml;higkeit 
        ihrer Kunden zu st&auml;rken. Erst sp&auml;ter - bei der Begleitung im 
        betrieblichen Alltag - werden Fragen der klassischen Unternehmensberatung 
        beantwortet. Dabei stehen je nach Bedarf weitere Partner zur Verf&uuml;gung.<br>
        <br>
        Die Kunden - das sind &uuml;berwiegend Unternehmer aus Handwerk und Handel 
        mit Betrieben in einer Gr&ouml;&szlig;enordnung von 10 bis 200 Besch&auml;ftigten. 
        Dazu geh&ouml;rt Karsten Kr&uuml;ger aus L&ouml;hne. Der Junior Chef der 
        B&auml;ckerei &quot;Karlchens Backstube&quot; mit 28 Filialen will vor 
        allem die Eigenverantwortung seiner 160 Mitarbeiter f&ouml;rdern. Sein 
        erstes Pluspunkt-Seminar hat ihm dazu wichtige Anst&ouml;&szlig;e gegeben. 
        Und zwar zun&auml;chst f&uuml;r sich selbst, denn so erl&auml;utert der 
        B&auml;cker und Betriebswirt: &quot;Ich habe gelernt, intensiv &uuml;ber 
        mich nachzudenken und auch das eigene Handeln in Frage zu stellen.&quot;<br>
        <br>
        Daraus entwickelte Karsten Kr&uuml;ger neue Gedanken &uuml;ber sein Unternehmen 
        und neues Handeln. Die Strukturver&auml;nderung des handwerklichen Familienbetriebes 
        von aufgabenbezogenem zu ergebnisorientierten F&uuml;hrungsstil ist daf&uuml;r 
        ein Beispiel. Viele intensive Gespr&auml;che hat Karsten Kr&uuml;ger auf 
        diesem Weg gef&uuml;hrt, auch seine leitenden Mitarbeiter haben inzwischen 
        F&uuml;hrungsseminare bei Pluspunkt besucht. Seine Erfahrung: Positives 
        Denken ist ebenso erlernbar wie &uuml;bertragbar. <br>
        <br>
        Ein solcher Prozess komme im betrieblichen Alltag aber erst in Gang, wenn 
        Unternehmer und F&uuml;hrungskr&auml;fte selbst positiv d&auml;chten und 
        das auch mitteilten, betont Ralf Lohe. Motivation und Kommunikation sind 
        deshalb Schwerpunkte des Einstiegsseminars bei Pluspunkt, das die beiden 
        Trainer auf Fuerteventura anbieten. Damit schaffen sie eine entspannte 
        Atmosph&auml;re weg vom Alltag.<br>
        <br>
        Davon f&uuml;hlte sich auch Burkhard M&uuml;ller angesprochen, der &quot;im 
        Urlaub etwas sinnvolles machen&quot; wollte. Vor zwei Jahren nahm der 
        Fleischermeister aus Erndtebr&uuml;ck bei Siegen an seinem ersten Seminar 
        auf der Kanareninsel teil. Dabei habe er gelernt, sich in seinem Betrieb 
        aus der Funktion des produktiv t&auml;tigen Handwerksmeister zu l&ouml;sen 
        und mehr F&uuml;hrungsverantwortung zu &uuml;bernehmen. Bei 65 Mitarbeitern 
        und sieben Verkaufsstellen nehme diese Aufgabe zu, betont M&uuml;ller. 
        <br>
        <br>
        &Uuml;ber die Seminare hinaus bieten Lohe und Rumi auch individuelle Beratung 
        an und vermitteln dabei sachkundige Partner zum Beispiel zu speziellen 
        betriebswirtschaftlichen Fragestellungen. So arbeitet Pluspunkt unter 
        anderem mit der Zentrale f&uuml;r Weiterbildung im Handwerk und mit Handwerkskammern 
        zusammen. </font></p>
