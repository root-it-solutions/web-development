      <p><b><font size="3" color="#000000">Parkhotel Schloss Hohenfeld lie&szlig; 
        sich &quot;proberaten&quot;</font></b></p>
      <p><font color="#000000"><b>Beteiligt waren neben der Dresdener Bank zwei 
        Unternehmensberatungen / Vorhandenes Potenzial soll k&uuml;nftig besser 
        ausgesch&ouml;pft werden</b></font></p>
      <p><font color="#000000">M&Uuml;NSTER. Was bestimmt den Wert eines Unternehmens? 
        Nur die harten Faktoren, also das, was auf der finanziellen Habenseite 
        zu verbuchen ist? Oder auch die weichen Faktoren, wie zum Beispiel die 
        Unternehmensf&uuml;hrung und die Qualifikation der Mitarbeiter? Sp&auml;testens 
        seit Basel II und den damit verbundenen Ver&auml;nderungen bei der Kreditvergabe 
        ist die Antwort auf diese Frage wichtiger als je zuvor. K&uuml;nftig ermitteln 
        die Banken die Bonit&auml;t eines Unternehmens mit Hilfe eines sogenannten 
        Ratings und legen je nach Ergebnis die H&ouml;he der Kreditzinsen fest. 
        In Vorbereitung auf ein solches Bankenrating hat im Parkhotel Schloss 
        Hohenfeld vor kurzem ein &quot;Proberating&quot; stattgefunden.</font></p>
      <p><font color="#000000">Das Besondere: Beteiligt waren neben der Dresdner 
        Bank aus M&uuml;nster auch zwei Unternehmensberatungen. Gemeinsam haben 
        sie das Hotel nicht nur bewertet, sondern zugleich auch eine Potenzialanalyse 
        durchgef&uuml;hrt. Somit verf&uuml;gt das Parkhotel jetzt &uuml;ber eine 
        ausf&uuml;hrliche Bewertung des Ist-Standes sowie einer detaillierten 
        Empfehlung f&uuml;r den k&uuml;nftigen Soll-Zustand und den Weg dorthin. 
        &quot;Dieses Verfahren ist zumindest im M&uuml;nsterland sicher einmalig&quot;, 
        meint Bernhard Rothenberger, der das Vier-Sterne-Hotel zu Beginn des Jahres 
        &uuml;bernommen hat.<br>
        Obwohl der M&uuml;nsteraner Hotelier im Moment keinen akuten Finanzbedarf 
        hat, wollte er wissen, wie die Bewertung im Fall der F&auml;lle aussehen 
        w&uuml;rde. &quot;Ich sehe das Geldinstitut als Partner mit dem ich gemeinsam 
        die Voraussetzung f&uuml;r ein gutes Rating schaffen will&quot;, so Rothenberger. 
        &quot;Mit dem jetzt vorliegenden Ergebnis kann ich die k&uuml;nftige Entwicklung 
        meines Hauses zielgerichtet steuern. Und wenn es dann in einigen Jahren 
        wirklich um einen Kredit geht, hoffentlich gute Zinsbedingungen erwarten.&quot;</font></p>
      <p><font color="#000000">Um dieses Ziel zu erreichen, haben Bernhard Rothenberger 
        und seine 65 Mitarbeiter in den vergangenen zwei Monaten sehr viel Zeit 
        investiert. Gemeinsam mit Ralf Lohe von der Pluspunkt Unternehmensentwicklung 
        und Winfried B&uuml;th von der Firma Intectum Unternehmens Organisation 
        und Systemgestaltung galt es, umfangreiche Fragenkataloge zu bew&auml;ltigen. 
        Dabei ging es ebenso um die Art der Unternehmensf&uuml;hrung, wie um den 
        Fortbildungsstand der Mitarbeiter und personelle Nachfolgeregelungen. 
        &quot;Die Einbeziehung der Mitarbeiter in den Ratingprozess ist unverzichtbar&quot;, 
        erkl&auml;rt Ralf Lohe. &quot;Denn sie sind das wichtigste Kapital des 
        Unternehmens.&quot; Am Anfang des Ratings stand zun&auml;chst die Bestimmung 
        des Ist-Zustandes in den einzelnen Abteilungen. &quot;Schon in dieser 
        Phase begannen die Mitarbeiter damit, ihr t&auml;gliches Tun zu reflektieren&quot;, 
        berichtet Winfried B&uuml;th. Nach der Bestandsaufnahme folgte dann gemeinsam 
        'die Zielsetzung f&uuml;r die Zukunft, dabei konnten die Mitarbeiter auch 
        Empfehlungen f&uuml;r die Verbesserung der Arbeitsabl&auml;ufe und Strukturen 
        aussprechen.</font></p>
      <p><font color="#000000">&quot;Obwohl der ganze Prozess ungeheuer arbeitsintensiv 
        war und parallel zum laufenden Hotelbetrieb stattgefunden hat, war die 
        Motivation der Mitarbeiter riesengro&szlig;&quot;, freut sich Bernhard 
        Rothenberger. So wundert es auch nicht, dass das Fazit des Hotelchefs 
        durchweg positiv ausf&auml;llt: &quot;Mit dem jetzt vorliegenden Ergebnis 
        k&ouml;nnen wir arbeiten und unser vorhandenes Potenzial in den kommenden 
        Jahren verbessern. Zudem haben wir durch das Einbeziehen der Bank ein 
        Vertrauensverh&auml;ltnis aufgebaut. Wenn es in ein paar Jahren dann zu 
        einem echten` Bankenrating kommt, ist bereits eine Basis vorhanden, auf 
        die man aufbauen kann.&quot; In den n&auml;chsten Wochen und Monaten stehen 
        im Parkhotel Schloss Hohenfeld auf jeden Fall einige Ver&auml;nderungen 
        an. &quot;Wir wissen jetzt, wo die Knackpunkte sind, wo die Ver&auml;nderungen 
        ansetzen m&uuml;ssen`, so Rothenberger.</font></p>
      <p><font color="#000000">Das Parkhotel Schloss Hohenfeld liegt ruhig und 
        dennoch zentral in der Westfalen-Metropole M&uuml;nster. Es bietet 163 
        Betten, die sich auf 84 Hotelzimmer und 13 Landhaus-Appartements verteilen. 
        Im vergangenen Jahr kamen &uuml;ber 25000 G&auml;ste, was einer Auslastung 
        von mehr als 57 Prozent entspricht. Die 65 Mitarbeiter f&uuml;hlen sich 
        ebenso wie die Inhaber ganz dem Wohlbefinden der G&auml;ste verntwortlich. 
        Mit neun Tagungsr&auml;umen sowie moderner Technik und einer Kapazit&auml;t 
        von bis zu 180 Personen bietet das Hotel auch ein breites Veranstaltungsspektrum. 
        Im vergangenen Jahr erhielt das Hotel die Auszeichnung &quot;Ausgew&auml;hltes 
        Tagungshotel zum Wohlf&uuml;hlen&quot;; Zudem z&auml;hlt das Haus zu den 
        vier &quot;M&uuml;nster Top Hotels&quot;.</font></p>
