                         <p><b>Unter der Sonne des Südens</b></p>
                        <p><em>Sendener Unternehmensberater bietet Seminare auf den Kanaren und Mallorca an</em></p>
                        <p style="font-weight: bold;">Senden &middot; Arbeit auf den Kanaren – weit gefehlt, wer denkt, dies sei nur für Animateure und Mitarbeiter von Reiseunternehmen möglich. Der Sendener Ralf Lohe ist als Unternehmensberater für deutsche Unternehmen tätig. Er arbeitet regelmäßig auf Fuerteventura.</p>
                        <p>Lohe und sein Kompagnon, der Drensteinfurter Manfred Rumi, veranstalten Motivations- und Rhetorikseminare für Führungskräfte und Mitarbeiter von mittelständischen Unternehmen, meist aus dem Handwerk.
20 Mal haben Lohe und Rumi seit 1997 Seminare in Ferienclubs auf Fuerteventura, manchmal aber auch auf Mallorca oder in der Türkei veranstaltet. "Hochgradig vorteilhaft" nennt Ralf Lohe diese Veranstaltungen, deren Dauer von einem Wochenende bis zu sieben Tagen reichen kann.</p><p>
Schon der Hinflug sorge für Entspannung, so Lohe. "Die Gedanken ans Tagesgeschäft verfliegen, wenn die Unternehmer das Meer unter sich sehen." Mit Urlaub hätten die Seminare jedoch nicht viel zu tun, betont der 55-jährige. "Das Programm ist so dicht, dass tagsüber keine Zeit für Freizeitmöglichkeiten bleibt." Lediglich abends biete der Ferienclub, in dem die Seminare stattfinden, eine Show.</p><p>
"Was ist das für eine Schnapsidee?" habe er gedacht, als Rumi ihm vor zehn Jahren vorschlug, doch einmal Seminare auf Fuerteventura zu veranstalten. "Das hatte noch nie jemand gemacht."</p><p>
Doch die Idee bewährte sich. Die Entspannung unter der Sonne Spaniens sorge für mehr Kreativität und Teamgeist unter den Teilnehmern, so Lohes Erfahrung.</p><p>
Der nächste Flug ist schon gebucht. Im März fliegt er mit seinem Kompagnon und 18 Mitarbeitern einer IT Firma aus Düsseldorf nach Mallorca.
