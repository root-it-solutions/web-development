      <p><font color="#000000">Mitarbeiterschulungen geh&ouml;ren in jedem Unternehmen 
        mittlerweile zur Tagesordnung. Ob der Manager einem Trainingsprogramm 
        unterzogen wird, entscheidet nicht selten &uuml;ber Erfolg oder Misserfolg 
        im Gesch&auml;ftsleben. Aber ist so ein Coaching f&uuml;r einen handwerklichen 
        Betrieb, speziell f&uuml;r einen mittelst&auml;ndischen BackBetrieb sinnvoll?<br>
        <br>
        Unsere Zeitung sprach mit Melitta Lorenz, Gesch&auml;ftsf&uuml;hrerin 
        von &quot;Lorenz Victorbur&quot; in Aurich, deren Wahrzeichen die stolzen 
        Wikinger sind, und ihrem Sohn Martin, sowie Trainer Manfred Rumi &uuml;ber 
        die Chancen und M&ouml;glichkeiten wirklich professioneller Schulung.</font></p>
