    <p> <font color="#000000"><b><font size="3">Vier Stunden Starthilfe</font></b></p>
                        <p>
                        	<strong>
								Dülmen. Wie kann ein Gründungsvorhaben gefördert werden? Welche Anforderungen muss ein Geschäftsplan erfüllen? Und wie teuer werden 
								Steuer und Sozialversicherung? Auf diese und auf viele weitere Fragen erhielten die Teilnehmer der "Messe für Existenzgründer und 
								Jungunternehmer – Chancenforum 2008" fundierte Antworten: Die 14 Institutionen und Initiativen des "gwc.netzwerks für Gründung 
								und Wachstum im Kreis Coesfeld" hatten in den Räumen der Schornsteinfegerschule in Dülmen ihre Infostände aufgebaut. Vier Stunden 
								lang standen die Experten den Gründern als Ansprechpartner für eine erste Kontaktaufnahme zur Verfügung. "Das Angebot wurde von den 
								Teilnehmern gerne angenommen", resümierte Thomas Brühmann, Prokurist der wfc, die Veranstaltung.
                        	</strong>
						</p>
						<p>
							Abgerundet wurde der Netzwerk-Nachmittag durch den Vortrag eines externen Referenten: Wie ein junges Unternehmen auf Erfolgkurs zu 
							führen und zu halten sei, verriet Ralf Lohe (Pluspunkt GmbH) den Forumsteilnehmern. In seinem Vortrag "Richtig führen – richtig Geld 
							verdienen – vom Existenzgründer zur effektiven Führungspersönlichkeit" öffnete der Sendener Unternehmensberater die Schatzkiste seiner 
							Erfahrungen und gab Tipps für alle Gründungsphasen.
						</p>
