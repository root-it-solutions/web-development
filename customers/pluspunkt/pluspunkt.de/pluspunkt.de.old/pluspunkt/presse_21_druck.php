        </font> <br>
        </b></font><em>Weiterbildung auf Fuerteventura? &quot;Viele Bekannte haben 
        gedacht, dass wir dort Urlaub machen,&quot; so B&auml;cker-Ehepaar Simone 
        und Andreas Puls aus Salzbergen. Ferien waren aber nicht angesagt, sondern 
        lernen.</em>
      <p>In einem sechst&auml;gigen F&uuml;hrungsseminar gewann das Paar neue 
        Erkenntnisse &uuml;ber Motivation und Delegation, &uuml;ber eigenverantwortliches 
        Handeln der Mitarbeiter und &uuml;ber die Entwicklung - und Umsetzung 
        - von Visionen.</p>
      <p>Zun&auml;chst gin es darum, das Selbstvertrauen und die Kommunikationsf&auml;higkeit 
        zu st&auml;rken. &quot;Man ist auf einer Insel einfach entspannter und 
        offener f&uuml;r neue Ideen und kann das Erlernte au&szlig;erhalb des 
        Betriebsalltages viel besser verinnerlichen&quot;, so Simone Puls. Als 
        Unternehmer m&uuml;sse man sich st&auml;ndig weiterentwicklen - davon 
        ist der 36j&auml;hrige B&auml;ckermeister Andreas Puls ebenso &uuml;berzeugt 
        wie die anderen Teilnehmer des Seminars. Und wenn, wie in seinem Betrieb, 
        die Ehefrau mit in der Verantwortung stehe, dann sei es auch sinnvoll, 
        eine solche Weiterbildung gemeinsam zu machen. Zudem k&ouml;nnen die gewonnenen 
        Erkenntnisse in entspannter Atmosph&auml;re und in aller Ruhe auf die 
        spezifischen Bedingungen &uuml;bertragen werden. &quot;Wir kamen mit produktiven 
        Ideen und Konzepten aus dem Urlaub zur&uuml;ck, die wir sofort im Betrieb 
        umgesetzt haben - mit Erfolg&quot;, bilanziert Andreas Puls.</p>
      <p>Weiterbildung fern ab vom betrieblichen Alltag - das schafft eine entspannte 
        Atmosph&auml;re und zugleich nat&uuml;rlich etwas Urlaubsgef&uuml;hl. 
        Und das stellt aich auf Fuerteventura ein, wenn die Seminarstunden am 
        sp&auml;ten Nachmittag beendet sind und dann der Swimmingpool in der Clubanlage 
        oder das Sonnenbad am Strand lockt.</p>
      <p>von Andreas Lehr</p>
