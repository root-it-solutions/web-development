            <p><b>DVD-Trainer</b></p>
            <div class=Section1> 
              <p><b>Interaktives Lehren und Lernen</b></p>
              <p>Das gr&ouml;&szlig;te Problem von Trainingsveranstaltungen ist 
                ein ver&auml;ndertes Verhalten nachhaltig zu erzeugen. Pr&auml;senztrainings 
                haben hier nur einen begrenzten Erfolg. Viel wirkungsvoller sind 
                Ma&szlig;nahmen, die die Umsetzung des Gelernten in der Praxis 
                sicherstellen. Um diesem Anspruch gerecht zu werden, haben wir 
                zusammen mit <a href="http://www.christoph-niederberger.de" target="_blank">Christoph 
                Niederberger</a> und <a href="http://www.otv-media.de" target="_blank">Harald 
                Ortlieb</a> ein interaktives Trainingsprogramm entwickelt, das 
                vom <a href="http://www.dvdtrainer.de" target="_blank">W. Bertelsmann 
                Verlag</a> vertrieben wird. Wir haben hierin unsere guten Erfahrungen 
                mit unserer ersten Schulungs-DVD, die wir f&uuml;r die Edeka Minden-Hannover 
                bereits vor vier Jahren produziert haben, einflie&szlig;en lassen.</p>
              <p><b>Die Themen</b>:</p>
            </div>
            Motivierende Gespr&auml;che:<br>
            <ul>
              <li>&nbsp;Das Analyse-Meeting</li>
              <li>&nbsp;Das Kritik-Gespr&auml;ch</li>
              <li>&nbsp;Das Kreativ-Meeting</li>
              <li>&nbsp;Das Delegations-Gespr&auml;ch </li>
            </ul>
            <div class=Section1> 
              <p>Wie funktioniert das Programm?</p>
              <p>Auf einer DVD finden Sie zu den o.g. Themen jeweils eine eingespielte 
                Szene. Hierzu finden Sie dann eine Menge Informationen, &uuml;ber 
                die ein Coaching erfolgen kann. Mehr Informationen zu den Inhalten 
                erhalten Sie hier [link: Seite 2].</p>
              <p>Eine Gruppe von F&uuml;hrungskr&auml;ften Ihres Unternehmens 
                wird als Multiplikatoren innerhalb eines eint&auml;gigen Workshops 
                auf ihre Arbeit vorbereitet. Danach sind sie in der Lage mit der 
                DVD und selber vorbereitet und unterst&uuml;tzt durch den ausf&uuml;hrlichen 
                <a href="#inhalt">Trainerleitfaden</a> alle Ihre Mitarbeiter individuell 
                zu allen <a href="#themen">Themen auf dieser DVD</a> zu schulen. 
                Hierf&uuml;r entstehen Ihnen keine Kosten f&uuml;r Trainer, Schulungsr&auml;ume 
                und Organisation. Ihre Multiplikatoren sorgen dann f&uuml;r die 
                Nachhaltigkeit des Erlernten, wodurch Sie eine tats&auml;chlich 
                wirksame Verhaltens&auml;nderung erzielen.</p>
              <p>Was sind die Vorteile des DVD-Trainers</p>
            </div>
            <ul>
              <li>&nbsp;Sie erhalten f&uuml;r alle vier Themen eine sogenannte 
                &quot;K&ouml;nigsl&ouml;sung&quot;, die ein optimales Verhalten 
                beschreibt und somit zum Leitbild f&uuml;r Ihr Unternehmen wird.<br>
                <br>
              </li>
              <li>&nbsp;Die professionelle Herstellung (nach Drehbuch mit professionellen 
                Schauspielern; modernste Programmiertechnik) wirken die Szenen 
                wie im Kino und erreichen den Betrachter auch emotional. Dadurch 
                werden im Lernvorgang beide Gehirnh&auml;lften angesprochen, was 
                nachweislich zu besseren Lernergebnissen f&uuml;hr.<br>
                <br>
              </li>
              <li>&nbsp;Sie erhalten mindestens 50 DVD, die sowohl &auml;u&szlig;erlich 
                als auch durch die Gestaltung der internen Screen an Ihr CD angepasst 
                sind. Dies garantiert Ihnen eine hohe Akzeptanz durch Ihre Mitarbeiter.<br>
                <br>
              </li>
              <li>&nbsp;Das Programm kann &uuml;berall dort eingesetzt werden, 
                wo ein DVD-Player oder ein DVD-Laufwerk in einem PC zur Verf&uuml;gung 
                steht. Die Einsatzm&ouml;glichkeiten sind somit nahezu unbegrenzt.<br>
                <br>
              </li>
              <li>&nbsp;Sie erreichen durch das individuelle Coaching auch alle 
                Mitarbeiter. Selbst Mitarbeiter, die erst in der Zukunft eingestellt 
                werden, k&ouml;nnen dann schnell unabh&auml;ngig von Trainern 
                oder Trainingszeiten geschult werden.<br>
                <br>
              </li>
              <li>&nbsp;Die sich selbst erkl&auml;rende einfache Menuef&uuml;hrung 
                erm&ouml;glicht auch das autodidaktische Lernen.</li>
            </ul>
