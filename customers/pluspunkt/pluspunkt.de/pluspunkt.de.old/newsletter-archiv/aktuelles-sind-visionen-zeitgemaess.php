                    f&uuml;r die Formulierungen. Dadurch verst&auml;rkt sich die 
                    Glaubw&uuml;rdigkeit einer Vision, was der &Uuml;berzeugung 
                    der &uuml;brigen Menschen im Unternehmen dienlich ist.</p>
                  <p>Kritisiert wird zuweilen auch, dass eine Vision zu unbestimmt 
