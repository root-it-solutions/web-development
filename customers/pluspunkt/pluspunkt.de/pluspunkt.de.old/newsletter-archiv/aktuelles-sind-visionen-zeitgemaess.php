                  <p>Es lohnt sich schon eingehend mit den Artikel auseinander 
                    zu setzen. Dann wird deutlich, dass jeder Manager, der erfolgreich 
                    sein will, ohne eine klare Ausrichtung nicht auskommt. Ob 
                    dies dann Unternehmensvision, Leitbild, Strategieentwurf, 
