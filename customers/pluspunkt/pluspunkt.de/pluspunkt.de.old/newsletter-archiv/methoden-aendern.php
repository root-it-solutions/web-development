<p>Nun, wenn sich das gew&uuml;nschte Ergebnis nicht wie gew&uuml;nscht einstellt, h&auml;ngt das damit zusammen, dass die gew&auml;hlten Methoden f&uuml;r das Ziel falsch oder nicht gut genug sind. Wenn die Verst&auml;rkung meiner Werbung nicht zu mehr Umsatz f&uuml;hrt, so werden meine potenziellen Kunden von dieser Werbung nicht richtig angesprochen oder ich spreche gar die falschen Kunden an.</p>
<p>Schauen wir uns als Beispiel mal das Ziel eines Einzelh&auml;ndlers an, mehr Umsatz zu generieren. Er analysiert, dass wenn sich das Verkaufsverhalten an der Bedientheke &auml;ndert, wenn mehr beratende Verkaufsgespr&auml;che gef&uuml;hrt, Empfehlungen ausgesprochen und Zusatzverk&auml;ufe erfragt werden, wird sich der Umsatz steigern lassen. Die Anweisung an die Mitarbeiter ihr Verkaufsverhalten zu &auml;ndern, mit Sanktionen zu drohen, wenn das nicht erfolgt oder gar letztendlich das Personal auszutauschen, wird nicht zum erhofften Ziel f&uuml;hren. W&uuml;rde der Einzelh&auml;ndler analysieren, was die Ursache ist, weshalb seine Verk&auml;ufer sich weigern, ihr Verhalten zu &auml;ndern, w&uuml;rde er erkennen, dass es seinen Mitarbeitern an dem n&ouml;tigen Selbstvertrauen fehlt, das gew&uuml;nschte Verhalten dauerhaft zu zeigen. Jetzt k&ouml;nnte er die Methoden &auml;ndern und alles daf&uuml;r unternehmen, die Voraussetzungen bei seinen Mitarbeitern f&uuml;r das gew&uuml;nschte Verhalten zu schaffen. So k&ouml;nnte er z.B. durch Rhetoriktrainings und herausfordernde Aufgaben das Selbstvertrauen der Mitarbeiter st&auml;rken und sie dann per Schulung auf ihr neues Verhalten vorbereiten.</p>
