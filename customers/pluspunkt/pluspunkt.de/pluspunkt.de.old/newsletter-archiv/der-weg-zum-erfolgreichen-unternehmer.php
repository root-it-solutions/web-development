                    <b>Der Weg zum erfolgreichen Unternehmer</b><br />Von: Ralf Lohe</p>
                  <p>
					Dieser Weg scheint steinig zu sein, denn zunächst lachen mich 14 (!) CD an, die der Reihe nach gehört werden wollen. Das heißt, die nächsten 800 Minuten (13 Stunden und 20 Minuten) Autofahrt gehören einem Hörbuch. Alleine dies mag schon abschrecken, ganz zu schweigen davon, dass man ein Hörbuch mehrfach hören sollte, um es besser zu verstehen. Doch angeregt von einer Empfehlung von Peter Wehrmann (vielen Dank dafür) habe ich mich dieser Herausforderung gestellt.
				  </p>
				  <p>
					  Und es war gar nicht so problematisch, da der Autor das Thema in eine Geschichte mit Dialogen zwischen einem Unternehmer und seinem Berater verpackt hat. So ist die Stofffülle leichter zu verdauen und der Hörer ist auf den Ausgang des Ganzen gespannt.
				  </p>
				  <p>
					Die entscheidenden Punkte, die dieses Hörbuch von anderen unterscheidet, und die ich als wertvolle Inspirationen aufgenommen habe, sind:
				  </p>
				  <blockquote>
					Die Unterscheidung der anstehenden Tätigkeiten in Aufgaben für Fachkräfte, Manager oder Unternehmer. So kann sich der Hörer selber reflektieren, in welchem Aufgabenfeld er gerade tätig ist und ob das auch seiner Position entspricht. Häufig wird nämlich während des Wachstums eines Unternehmens versäumt, Fachaufgaben loszulassen und der Verantwortung der Mitarbeiter zu überlassen.
				</blockquote>
				  <blockquote>
					Bekommt ein Unternehmen Format, ist es für potenzielle Kunden interessant. Die Wahrnehmung erfolgt über die Werte des Unternehmens, die dem Unternehmen Ausrichtung geben. Um dieses Phänomen optimal für sich nutzen zu können, muss der Unternehmer erst einmal für sich herausfinden, was seine Werte sind, diese dann definieren und schließlich kommunizieren, damit sich alle daran orientieren können. Hiervon werden dann Strategien und Visionen abgeleitet. Gibt es diese klare Ausrichtung nicht, wird in einer Krise jeder in seine und damit möglicherweise in eine andere Richtung entscheiden. Zudem fehlen den guten Mitarbeitern die Perspektiven, so dass sie das Unternehmen verlassen, statt ihm in einer Krise zu helfen.
				  </blockquote>
				  <p>
					Die Handlung zeigt sowohl die Entwicklung des Unternehmers in seinem Management also auch die Führung seiner Mitarbeiter in einer echten Krise mithilfe geeigneter betriebswirtschaftlicher Gestaltung. Dies macht deutlich, wie sinnvoll es auch in der Praxis ist, beide Aspekte gleichzeitig zu behandeln. Dies ist umso sinnvoller, als es in einer Krise auch keine echte Alternative gibt. Hier müssen betriebswirtschaftliche Ergebnisse schnell verbessert werden. Doch dies ohne die Verbesserung des Managements zu tun, bedeutet neue Gefahren zuzulassen und wird zum Scheitern führen. 
				  </p>
				  <p>
					Neben den wertvollen Hinweisen zum richtigen Führen macht gerade das Aufzeigen aller Problempunkte in einem angeschlagenen Unternehmen verbunden mit den geeigneten Lösungsstrategien, die der komplexen Situation gerecht werden, den besonderen Wert dieses Buches aus.
				  </p>
