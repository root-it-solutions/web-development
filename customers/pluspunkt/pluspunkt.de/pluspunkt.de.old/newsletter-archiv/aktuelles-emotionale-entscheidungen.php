                  <p>
Wenn wir eine Entscheidung treffen, sind beide Gehirnhälften gefordert. Die populärwissenschaftliche Erklärung lautet, dass die linke Gehirnhälfte mehr sachlich orientiert ist und die Frage beantwortet, was in diesem Zusammenhang sinnvoll ist. Die rechte Gehirnhälfte hingegen ist mehr gefühlsmäßig orientiert und fragt sich, was uns denn bei dieser Entscheidung gefallen würde.<br />
Dies ist für uns Menschen sehr hilfreich. Doch es existiert ein Ungleichgewicht was die Wirkung der Gehirnhälften auf unsere Entscheidung betrifft. Dies lässt sich sehr anschaulich am Eisbergmodell deutlich macht. Dieser schwimmt bekanntlich überwiegend (ca. 9/10) unter Wasser. Der Teil des Eisbergs, der aus dem Wasser schaut, entspricht der Ratio. Alles was man z.B. bei einem Kauf sachlich abwägt, verarbeitet man in der linken Gehirnhälfte. Über diese Fakten tauscht man sich auch mit dem/r Verkäufer/in aus. <br />
Wichtiger für die Entscheidung ist beim Menschen jedoch die Emotion. Sie entspricht dem Teil des Eisbergs, der sich unter Wasser befindet. Entsprechend der Größe dieses Teils ist auch die Emotion zu bewerten. Obwohl er sich bewusst gegen die Gefühle entscheiden kann, tut der Mensch dies überwiegend nicht und gehorcht seinen Gefühlen. Wie wäre es sonst zu erklären, dass die meisten Menschen zu viel essen, zu viel rauchen oder zu viel trinken? Im Verkauf wird auch überwiegend emotional entschieden aber darüber meist nicht gesprochen, so dass diese Beweggründe dem Verkäufer verborgen bleiben.<br />
Nehmen wir das Beispiel eines Autokaufs. Das Budget für den Kauf, die Farbe, die Motorleistung oder die Ladekapazität sind Teil des Verkaufsgesprächs. Aber der Mann, der mit seinem neuen Auto gegenüber seinen Kollegen angeben möchte oder die Frau, die ihre Lieblingsfarbe wählt, würden normalerweise hierüber nicht sprechen.<br />
Für den Verkäufer bedeutet dies, dass er, obwohl ihm die Gefühlsentscheidungen seiner Kunden verborgen bleiben, am besten die Gefühle der Kunden anspricht um die Kaufentscheidung positiv zu beeinflussen. Und dies sind nicht die Details unseres Produktes oder der Dienstleistung, sondern der Nutzen, der sich nach dem Kauf einstellen wird.<br />
Der gefühlsmäßige Aspekt im Verkauf bedeutet aber auch, dass die Sympathie, die zwischen dem Verkäufer und dem Kunden entsteht, nicht zu unterschätzen ist. Nach einer Untersuchung des Max-Planck-Institutes entscheidet der Kunde zu über 80% gegen den Kauf, weil die Sympathie zum Verkäufer fehlt.<br />
Dies bedeutet für uns, dass wir die Gefühle, die die Kommunikation begleiten, angemessen berücksichtigen müssen, wenn wir hier erfolgreich sein wollen. Zudem gilt, dass wir andere nur dann richtig verstehen können, wenn wir ihre Entscheidungen und damit ihre Gefühle verstehen können. Den Weg hierhin finden wir, wenn wir vertrauensvoll mit den Menschen reden. <br />
Fazit: Nur die Berücksichtigung der Gefühle Anderer führt zu echtem Verständnis oder außergewöhnlichen Verkaufsabschlüssen.
