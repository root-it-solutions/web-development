                  <b>Von den Chinesen stammt der Satz:<br>
                  "Ein Bild sagt mehr als tausend Worte". </b> 
                  <p>Dieses physikalisch nachgewiesene Phänomen beschäftigt mich 
                    als Designerin und es stellt sich die Frage, was durch das 
                    visuelle Erscheinungsbild, Corporate Design z.B. auf Briefbögen, 
                    Visitenkarten, Prospekten usw. visuell emotional oder sachlich 
                    theoretisch ausgesagt wird und auf welche Weise es mit seinen 
                    jeweiligen Betrachtern kommuniziert. <br>
                    <br>
                    Sowohl unbewusst, als auch bewusst wird ein Mensch, den wir 
                    neu kennen lernen - privat oder geschäftlich - innerhalb kürzester 
                    Zeit seinem ersten Eindruck gemäß beurteilt und einsortiert 
                    in die eigenen Kategorien wie zum Beispiel sympathisch, neutral 
                    oder unsympathisch. Dieses ist hinlänglich bekannt und bezieht 
                    sich sowohl auf die äußere aber auch noch wichtiger auf die 
                    erste innere Erscheinung, auf das Gefühl dieser Person gegenüber. 
                    <br>
                    <br>
