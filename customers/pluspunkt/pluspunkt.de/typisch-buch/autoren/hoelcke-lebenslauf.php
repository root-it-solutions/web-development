                <td><b>Lebenslauf</b><br>
                  &nbsp;<br>
                  Ich wurde am 15.12.1957 in Hemer (Sauerland) geboren, bin verheiratet 
                  und habe zwei Kinder. <br>
                  <br>
                  Über das Tiefbauunternehmen meines Vaters kam ich schon früh 
                  mit baulichen Tätigkeiten in Berührung. Da für mich aber bereits 
                  seit der Schulzeit Natur und Umwelt zum zentralen Gegenstand 
                  meines Interesses wurden, absolvierte ich nach Praktikum, Realschule 
                  und Fachoberschule (in Letmathe) ab 1978 eine Ausbildung als 
                  Gärtner.<br>
                  <br>
                  In mehreren heimischen Betrieben erwarb ich Kenntnisse in verschiedenen 
                  Fachrichtungen des „grünen“ Berufs (Blumen- und Zierpflanzenbau, 
                  Landschafspflege, Garten- und Landschaftsbau, Baumschule, Floristik) 
                  und legte 1983 nach dem erfolgreichen Besuch der Fachoberschule 
                  für Gartenbau in Wolbeck die Meisterprüfung ab. <br>
                  <br>
                  Daraufhin mchte ich mich selbstständig, wobei für mich eine 
                  Tätigkeit im Sinne der Umwelterhaltung und -verschönerung positiv 
                  und anregend wirken zu können, von vorrangiger Bedeutung war.<br>
                  <br>
                  Wichtige Arbeitsfelder neben dem Pflanzenverkauf im Gartencenter 
                  mit eigener Baumschule waren die Anwendung alternativer Energien 
                  wie z.B. Windkraftanlage, Biodiesel, Blockheizkraftwerk, Wärmepumpe, 
                  Regenwassernutzung und eigenhändige Pflanzung eines Mischwaldes. 
                  Dachbegrünungen, Teiche und Schwimmteiche und die Planung und 
                  Ausführung von kompletten Außenanlagen gehören zum Programm. 
                  <br>
                  <br>
                  Schulungen und Vorträge über verschiedene Themen meiner Tätigkeit 
                  führe ich seit Jahren nicht nur in meinem Betrieb, sondern auch 
                  auf Veranstaltungen durch. <br>
                  <br>
                  Ein Ansporn, in der begonnenen Richtung weiterzumachen sind 
                  die Umweltpreise des Märkischen Kreises, die Auszeichnungen 
                  der IHK und der Stadt Hemer. <br>
                  <br>
                  Ich freue mich, heute feststellen zu können, dass es mir (nicht 
                  zuletzt dank motivierter Mitarbeiter) gelungen ist, mein - immer 
                  noch mit Idealismus und Visionen geführtes - Unternehmen zu 
                  einem führenden Fachbetrieb der Region ausbauen zu können, der 
                  sich jeder Herausforderung im Umweltbereich stellt.</td>
