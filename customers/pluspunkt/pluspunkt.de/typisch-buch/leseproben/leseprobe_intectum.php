            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>Der Mensch steht im Mittelpunkt<br>
              Die Bereitschaft zur Ver&auml;nderung entscheidet &uuml;ber den 
              Erfolg</b></p>
            <p>Unternehmensberater sind immer noch &uuml;berwiegend M&auml;nner 
              in schwarzen Anz&uuml;gen, die danach bezahlt werden, in k&uuml;rzester 
              Zeit die Kosten, das hei&szlig;t in der Regel den Personalstamm 
              zu senken. Wenn dieses Bild zunehmend nicht mehr der Realit&auml;t 
              entspricht, so besteht es dennoch in den K&ouml;pfen der Mitarbeiter, 
              die wir in Betrieben antreffen. Folglich herrscht Angst vor, den 
              Arbeitsplatz zu verlieren.</p>
            <p>In Unternehmen, in denen Kommunikation und Information ein Privileg 
              weniger Menschen ist und Entscheidungen einsam an der Spitze getroffen 
              werden, ist es nicht verwunderlich, wenn es an Vertrauen, dem einzigen 
              Mittel gegen Angst, fehlt. Sehen Sie sich um! In wie vielen Unternehmen 
              steht der Mensch im Vordergrund? Wie oft werden Mitarbeiter ernsthaft 
              nach ihrer Meinung gefragt? Wie h&auml;ufig nehmen Mitarbeiter an 
              Weiterbildungsma&szlig;nahmen teil? Wie oft werden die Leistungen 
              der Mitarbeiter anerkannt? Wie oft beherrschen Hierarchie und Politik 
              im Unternehmen die Atmosph&auml;re und das Ergebnis von Besprechungen? 
              Und was glauben Sie, wie oft der Mitarbeiter das Gef&uuml;hl hat, 
              wirklich ernst genommen zu werden?</p>
            <p>&quot;Der Mensch steht im Mittelpunkt&quot;, so habe ich einmal 
              die Vorstellung unseres Unternehmens begonnen, als ich alle Mitarbeiter 
              auf einem Bild gezeigt habe. &quot;Genau, und da steht er immer 
              im Weg&quot; warf mir jemand aus dem Kreis der Zuh&ouml;renden zu. 
              Es wurde mir schlagartig deutlich, wo derjenige stand und wo viele 
              in den F&uuml;hrungsetagen der Unternehmen stehen. Doch als Unternehmer 
              und F&uuml;hrungskraft muss uns klar sein, worauf wir uns einlassen: 
              Es kommt nicht nur darauf an, mit hochwertigen Produkten und geschicktem 
              Marketing Markt zu machen, sondern mit gleicher Kraft die Menschen 
              im Unternehmen zu f&uuml;hren. Denn ist der Mitarbeiter unzufrieden, 
              wird dies fr&uuml;her oder sp&auml;ter auch eine Unzufriedenheit 
              der Kunden nach sich ziehen. Dies ist hinl&auml;nglich in zahlreichen 
              Untersuchungen best&ouml;tigt worden.</p>
