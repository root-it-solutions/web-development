            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>Geld und Kommunikation</b></p>
            <p>Kurz nachdem meine Frau und ich uns kennen gelernt hatten, fragte 
              ich sie, was sie sich unter meiner beruflichen T&auml;tigkeit tats&auml;chlich 
              vorstelle. Sie antwortete mir, dass sie seit ihrer Kindheit immer 
              die Vorstellung habe, dass Steuerberater den ganzen Tag am Schreibtisch 
              sitzen und Zahlenkolonnen zusammen rechnen. &quot;Stimmt das?&quot;, 
              war nat&uuml;rlich die sofortige Reaktion auf meine Frage. Also 
              ein ziemlich langweiliger Beruf. Man sitzt still vor der Rechenmaschine 
              und ist hilflos einem Zahlenfriedhof ausgeliefert.</p>
            <p>Wie so oft im Leben sieht die Wirklichkeit (Gott sei Dank) v&ouml;llig 
              anders aus. Eine Hauptaufgabe meines Berufes besteht darin, meine 
              Mandanten erfolgreicher zu machen. Ich sitze nur sehr selten am 
              Schreibtisch. Mein Lieblingsarbeitsplatz ist neben dem Schreibtisch. 
              Dort stehe ich die meiste Zeit und vergn&uuml;ge mich mit dem Telefon. 
              Tats&auml;chlich besteht meine T&auml;tigkeit &Uuml;berwiegend aus 
              Telefonieren und kreativen Gedanken. Dabei kann ich mich stehend 
              gut konzentrieren und mich gut in die Probleme meines Gespr&auml;chpartners 
              hineindenken. In sehr vielen F&auml;llen kann ich meinen Mandanten 
              direkte L&ouml;sungen anbieten oder wenigstens einen Weg zur L&ouml;sung 
              aufzeigen. Meine Mandanten sch&auml;tzen dabei sehr, dass sie sofort 
              Informationen zur Umsetzung erhalten und sich an die Beseitigung 
              ihrer Probleme oder Gestaltung der Sachverhalte machen k&ouml;nnen. 
              Wenn ich f&uuml;r den Erfolg meiner Mandanten verantwortlich bin, 
              so bezieht sich dies &uuml;berwiegend auf das Geld, das sie mit 
              ihrer T&auml;tigkeit f&uuml;r sich erwirtschaften. Und das tun sie, 
              um ausreichend Kapital zu besitzen. Viele Menschen glauben, es sei 
              Zufall, ob man eines Tages &uuml;ber &quot;ausreichend&quot; Geld 
              verf&uuml;gt oder nicht. Aber das ist in keiner Weise so. Lassen 
              Sie mich also im Folgenden beschreiben, wie Sie dem &quot;Zufall&quot; 
              auf die Spr&uuml;nge helfen k&ouml;nnen.</p>
            <p><i>(...) Ludger Schlotmann</i></p>
