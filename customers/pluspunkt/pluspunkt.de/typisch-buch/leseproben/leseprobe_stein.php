            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>Von Adam zu Escoffier oder wer in Deutschland gut kochen will</b></p>
            <p>&quot;Jahre dauert es, bis Fr&uuml;chte, Tiere, Gem&uuml;se und 
              Getreide heranwachsen, Tage, bis sie herangeholt und vorbereitet 
              sind, Stunden, bis die Lebensmittel gegart und angerichtet sind. 
              In Minuten werden sie dann gegessen.&quot; Zitat aus &quot;Die anst&auml;ndige 
              Lust&quot;.</p>
            <p>Diese Erkenntnis legt einem nahe, sich des Wertes guten Essens 
              bewusst zu werden. Als Gastgeber laden wir ja auch nicht nur zur 
              Lebensmittelaufnahme ein, sondern beschenken unseren Gast mit kulinarischen 
              Kostbarkeiten. Dies sagt einiges &uuml;ber uns als auch &uuml;ber 
              unsere Einstellung zu unserem Gast aus und hat so auch eine pers&ouml;nliche 
              Botschaft an unseren Gast. Wir alle kennen die peinliche Situation 
              zu gut, wenn wir zu einem guten Essen eingeladen haben und der Koch 
              etwas serviert, das allen missf&auml;llt.</p>
            <p>Schauen wir aber zun&auml;chst in die Geschichte und erkennen, 
              wie unsere Vorfahren sich gustatorisch beschenkten. Sehen wir, wie 
              sich im Bereiche des Kochens das entwickelt, was heute einen Global 
              Player erfolgreich macht: Entwicklung von Kernkompetenzen, Offenheit 
              f&uuml;r Neues, Nutzung der Synergien, Qualit&auml;t auf h&ouml;chsten 
              Niveau und die Einstellung &quot;think global, act local&quot;. 
              Vielleicht finden Sie hier die eine oder andere Anregung.</p>
            <p>Adam, unser Stammvater, wird in der Bibel als Rohk&ouml;stler geschildert. 
              Er war demnach zwar der erste Mensch, jedoch nicht der erste Koch. 
              Die von ihm vorr&auml;tig gehaltene Rippe wurde bekanntlich nicht 
              kulinarischen Gen&uuml;ssen zugef&uuml;hrt, was bis heute kein senkrechter 
              Mann bedauert hat. Auch sein &uuml;berst&uuml;rzter Biss in den 
              omin&ouml;sen Apfel l&auml;sst kaum darauf schlie&szlig;en, dass 
              er zum Gastgeber pr&auml;destiniert gewesen w&auml;re. <br>
              Die Vertreibung aus dem Paradies holt uns weg vom fertig gedeckten 
              Tisch und zwang die Menschheit dazu, sich selber um die Ern&auml;hrung 
              zu k&uuml;mmern. <br>
              Die sp&auml;tere Umstellung vom Jagen und Sammeln auf Ackerbau und 
              Viehzucht brachte eine grundlegende Ver&auml;nderung in der Ern&auml;hrung. 
              Jetzt standen nicht nur Fisch, Fleisch, Pilze und Beeren auf dem 
              Speisezettel, sondern auch Getreide und H&uuml;lsenfr&uuml;chte.</p>
