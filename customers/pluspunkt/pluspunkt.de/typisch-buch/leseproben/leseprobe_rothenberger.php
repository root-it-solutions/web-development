            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>Die Kommunikation Ihrer Veranstaltung</b></p>
            <p>Vielleicht sind Sie bereits &uuml;ber diesen Titel gestolpert. 
              Sollte es nicht besser hei&szlig;en: &quot;Die Kommunikation auf 
              Ihrer Veranstaltung?&quot; Nein, denn es geht nicht um Smalltalk. 
              Hier geht es darum, was Sie mit Ihrer Feier, Party, ihrem Event 
              beabsichtigen und welche Sprache die Veranstaltung (auch &uuml;ber 
              Sie) spricht.</p>
            <p>Der Witz oder das Problem (es h&auml;ngt von Ihrer Einstellung 
              ab) f&uuml;r den Auftraggeber, f&uuml;r den Veranstalter ist, dass 
              er immer kommuniziert, dass er immer Botschaften aussendet.</p>
            <p>Und was erwarten Ihre G&auml;ste? Und mit welchen Gef&uuml;hlen 
              m&ouml;chten Sie Ihre G&auml;ste nach Hause schicken? Zumindest 
              &uuml;ber diese beiden Punkte sollten Sie sich v&ouml;llig im Klaren 
              sein: <br>
              a) was ist mein Ziel, <br>
              b) wann sind meine G&auml;ste gl&uuml;cklich, begeistert, positiv 
              &uuml;berrascht...?</p>
            <p>Der springende Punkt ist, dass sich &uuml;ber einen professionellem 
              Veranstaltungsservice fast alle Marketingziele gleichzeitig bedienen 
              lassen: Kundenbetreuung, Kundenbindung, Gewinnung von Neukunden, 
              Transportieren der Unternehmensleitlinien und Unternehmensphilosophie, 
              der Charakter, die Eigenschaft Ihrer Produkte, die Verbesserung 
              des Bekanntheitsgrades, die Imagepflege und, und, und.</p>
            <p>Denken wir doch mal kundenorientiert. Eine Veranstaltung ist dann 
              als erfolgreich zu bewerten, wenn die G&auml;ste zufrieden oder 
              am Besten mehr als zufrieden sind. Schon dr&auml;ngt sich die Frage 
              auf, wie Sie dies erreichen k&ouml;nnen. Nun, ohne eine professionelle 
              Beratung eines Partners, der Sie gut kennt und sein Know-how mit 
              Ihren Vorstellungen in Verbindung bringen kann, werden Sie das Optimum 
              nicht erreichen.<br>
              Damit Sie aber schon mal einen Eindruck gewinnen, was so alles m&ouml;glich 
              ist und wie viele Stilelemente ber&uuml;cksichtigt werden k&ouml;nnen, 
              hier einige Inspirationen. </p>
            <p><i>(...) Bernhard Rothenberger</i><font color="#333333"><i></i></font></p>
