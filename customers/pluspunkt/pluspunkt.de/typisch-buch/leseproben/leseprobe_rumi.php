            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>Mitarbeiter binden und entwickeln</b></p>
            <p>Politik und Gesellschaft verbreiten es: M&auml;nner und Frauen 
              haben die gleichen F&auml;higkeiten, Potenziale, M&ouml;glichkeiten. 
              <br>
              Und Ihr konsequenter R&uuml;ckschluss als F&uuml;hrungskraft: Sie 
              m&uuml;ssen gleich behandelt, angefasst, gef&uuml;hrt werden.</p>
            <p>Auch wenn sich die Zeiten ge&auml;ndert haben und Frauen meist 
              nicht mehr wie ihre M&uuml;tter sind. Sie m&uuml;ssen sich in der 
