            <h1><b>Presse</b></h1>
            <p><b><font size="2">Probleme gibt's, wenn er mit L&ouml;sungen kommt</font></b> 
              - 09.08.03<br>
              Manfred Rumi &uuml;ber die Unterschiede von M&auml;nnern und Frauen 
              im Gesch&auml;ftsleben<br>
              <br>
              Kommunikation hei&szlig;t nicht unbedingt reden. Auch Zuh&ouml;ren 
              will gelernt sein. Und wer das tut, wenn Manfred Rumi spricht, der 
              kann noch was lernen. Jetzt hat der erfahrene Unternehemsnberater, 
              der selbst alle Hierarchiestufen in einem Unternehmen durchlaufen 
              hat, sich in seinem Aufsatz &quot;Mitarbeiter binden und entwickeln&quot; 
              mit den Eigenschaften und Eigenheiten von Frauen und M&auml;nnern 
              im Berufsleben besch&auml;ftigt. </p>
            <p>Ausgangspunkt seiner &Uuml;berlegungen ist die falsche Annahme, 
              es g&auml;be keine Unterschiede zwischen einer m&auml;nnlichen und 
              einer weiblichen F&uuml;hrungskraft. Zur Veranschaulichung arbeitet 
              Rumi mit Rollenbildern, die verdeutlichen, dass es sehr wohl einen 
              Unterschied macht, ob im Chefsessel eine Frau oder ein Mann sitzt. 
              Mit zahlreichen Belegen, dass M&auml;nner auf direkte Ratschl&auml;ge 
              und offene Kritik h&auml;ufig allergisch reagieren und Statistiken 
              wie die, dass M&auml;nner durchschnittlich nur 2.000 Worte am Tag 
              sprechen, Frauen dagegen 6.000, stellt Rumi die klassischen Differenzen 
              zwischen M&auml;nnlein und Weiblein dar. </p>
            <p>Dabei w&uuml;rdigt er aber keine der beiden Seiten herab, sondern 
              wirbt f&uuml;r Verst&auml;ndnis und angemessene Kommunikation. Zus&auml;tzlich 
              gibt er Ratschl&auml;ge, wie den Problemen beizukommen ist. So solle 
              beispielsweise die Chefin f&uuml;r eine bestimmte Probezeit ihrem 
              Angestellten keine ungebetenen Tipps mehr geben, sondern nur Informationen. 
              Der Erfolg sei ihr gewiss.</p>
            <p>Umgekehrt d&uuml;rfe der m&auml;nnliche Boss nicht den Fehler begehen, 
              immer eine L&ouml;sung anzubieten, wenn eine Frau aus seinem Unternehmen 
              &uuml;ber Probleme sprechen wolle. Vielmehr solle er einfach nur 
              zuh&ouml;ren. Frauen w&uuml;rden oft eher die N&auml;he eines Gespr&auml;chs 
              suchen anstatt L&ouml;sungen pr&auml;sentiert bekommen zu wollen. 
              Kommunikation sei f&uuml;r eine Frau h&auml;ufig die Reaktion auf 
              Stress. </p>
            <p>M&auml;nner h&auml;tten hingegen so ihre Probleme mit Problemen 
              und w&uuml;rden sich nicht selten schuldig f&uuml;hlen, wenn eine 
              Frau mit ihnen &uuml;ber Probleme spricht. Der Irrtum der M&auml;nner, 
              Frauen w&uuml;rden sie f&uuml;r das verantwortlich machen, was sie 
              ihnen an Problemen vortragen, l&auml;sst sich Rumi zufolge vermeiden, 
              wenn die M&auml;nner endlich verst&uuml;nden, dass Frauen so nur 
              ihren eigenen Stress abbauen. So lernt der Leser gleich noch etwas 
              f&uuml;r sein Privatleben hinzu. </p>
            <p>Manchmal erscheint es so, als w&uuml;rde Manfred Rumi in g&auml;ngige 
              Vorurteile abrutschen. Aber nat&uuml;rlich wei&szlig; auch er, dass 
              Frau nicht gleich Frau und Mann nicht gleich Mann ist. Es kann daher 
              sicherlich nicht schaden, als Mann und m&auml;nnlicher Chef auch 
              mal den Blickwinkel einzunehmen, den Rumi gegen&uuml;ber Frauen 
              im Berufsleben einnimmt - und umgekehrt. Denn von Kommunikation 
              versteht Rumi zweifelsohne eine Menge: Er coacht F&uuml;hrungskr&auml;fte 
              und Unternehmer, wobei er die Lebenspartner oft mit einbezieht. 
              Sein eigenes Unternehmen &quot;Pluspunkt&quot; ist ebenfalls spezialisiert 
              auf s&auml;mtliche Formen der Kommunikation.</p>
            <p>So ist Rumis Wissen auch in das Buch &quot;Typ[isch]! Potenziale 
              erlebter Kommunikation&quot; eingeflossen, in dem 12 Autoren sich 
              mit den verschiedenen Aspekten von Kommunikation befasst haben. 
              Nicht nur wegen Manfred Rumis Denkanst&ouml;&szlig;en ist dieses 
              Buch lesenswert.</p>
            <p>Das bei W. Bertelsmann erschienene Managementbuch &quot;Typ[isch]! 
              Potenziale erlebter Kommunikation&quot; (Preis 24,90 &euro;), bietet 
              dabei eine Vielzahl ganz unterschiedlicher Aspekte und Perspektiven 
              an. Das, was auf den ersten Blick wie ein bunt best&uuml;ckter Bauchladen 
              erscheint, entpuppt sich bei n&auml;herem Hinsehen als eine wohldosierte 
              Mischung hochwirksamer Strategien, die neugierig macht auf mehr 
              und die auf vielf&auml;ltige Weise praktikable Anregungen vermittelt. 
              Was kann man besseres von einem Fachbuch &uuml;ber neue Unternehmensstrategien 
              noch sagen?!</p>
            <p>Die unterschiedlichen Ans&auml;tze verbinden sich im &Uuml;brigen 
              zu einem fein-gesponnen Netzwerk, das die Autoren untereinander 
              bilden und miteinander verbindet. Dreh- und Angelpunkt dieses Netzwerkes 
              ist das M&uuml;nsteraner Unternehmen &quot;Pluspunkt Unternehmensentwicklung&quot;, 
              das sich vor allem auf das Thema Personalentwicklung spezialisiert 
              hat. Der Zusammenhalt der Autoren zeugt von diesem Konzept und gibt 
              damit auch f&uuml;r Pluspunkt und deren Netzwerk 
              eine lebendige Visitenkarte ab.<br>
