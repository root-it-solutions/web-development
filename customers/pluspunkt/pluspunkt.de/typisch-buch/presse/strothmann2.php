            <h1><b>Presse</b></h1>
            <p><b>Kleider machen Leute</b> - 12.11.03<b><font size="2"><br>
              </font></b> Die Kommunikation des Outfits</p>
            <p>Die eigene pers&ouml;nliche Darstellung ist gerade im Gesch&auml;ftsleben 
              von gro&szlig;er Bedeutung. Doch die meisten haben nicht gelernt, 
              Kleidung als ein Kommunikationsmittel zu verstehen und richtig einzusetzen. 
              Die richtige Wahl der Kleidung macht vieles leichter - weil das 
              Gesamtbild stimmt.<br>
              Wichtigste Voraussetzung: Wer seinen Typus erst einmal erkannt hat, 
              dem wird es ein Bed&uuml;rfnis sein, f&uuml;r den passenden Ausdruck 
              zu sorgen. Er hat begriffen, wof&uuml;r Authentizit&auml;t steht, 
              n&auml;mlich f&uuml;r Echtheit und damit Glaubw&uuml;rdigkeit. Und 
              die lassen sich auch durch die passenden Kleidung kommunizieren.</p>
            <p align="center"> Kleidung spricht ihre eigene Sprache</p>
            <p> Verbale und nonverbale Kommunikation sind uns allen gel&auml;ufige 
              Begriffe. F&uuml;r Mimik und Gestik gilt dasselbe. Diese Eigenschaften 
              lassen sich erlernen. Rhetorik und K&ouml;rpersprache &agrave; la 
              Samy Molcho sind wohl jedem ein Begriff. Ein Schattendasein f&uuml;hrt 
              dagegen die Kommunikation des Outfits. Denn erst mit diesem h&auml;ufig 
              untersch&auml;tzten &auml;u&szlig;erlichen Merkmal wird der Mensch 
              zum Gesamtkunstwerk, sprich zur Pers&ouml;nlichkeit.</p>
            <p>Zur Zeit ist jeder sein eigener Modemacher. Die private Anarchie 
              bl&uuml;ht regelrecht auf. Man sieht es in der Mixtur von Farben, 
              Material und Mustern. Auch der Stilmix treibt seine Bl&uuml;ten 
              in ungewohnten Kombinationen. Wie schwer den Zeitgenossen das Modemachen 
              f&auml;llt, sieht man in jeder x-beliebigen Fu&szlig;g&auml;ngerzone. 
              Mangels Ahnung sind die Verbraucher verunsichert und entscheiden 
              sich f&uuml;r die vermeintlich sichere Bank. Sie ziehen sich auf 
              Marken zur&uuml;ck. Nach dem Motto: Da kann man nichts falsch machen. 
              Am besten alles von einer, dazu noch teuren Marke. Alles die gleiche 
              Handschrift und vor allen Dingen trendy.</p>
            <p>Das oberste Gebot bei der Auswahl des richtigen Outfits - dies 
              gilt f&uuml;r M&auml;nnlein und Weiblein - lautet: typgerecht. Auf 
              Charakter, Temperament und, nicht zu vergessen, das Alter muss penibel 
              geachtet werden. Denn alles andere wirkt auf den Betrachter wie 
              verkleidet, unehrlich und eben auch nicht authentisch. Kommt eine 
              sportliche, burschikose, geradlinige Frau mittleren Alters in einem 
              jugendlichen Kleidchen mit R&uuml;schen und Bl&uuml;mchen daher, 
              wirkt sie mit Sicherheit &uuml;berdreht. L&auml;ssiger Hosenanzug 
              oder sportives Kost&uuml;m sind hier angesagt, kombiniert mit klassischen 
              Slippern und passender H&auml;ngetasche. Alles auf ihren Farbtyp 
              abgestimmt und fertig.</p>
            <p>Mit typgerecht oder authentisch hat das aber noch lange nichts 
              zu tun. Meistens geht diese Vorgehensweise wie der Schuss nach hinten 
              los. Selbst Laien merken, dass da was nicht stimmt. Eine wesentliche 
              Ursache liegt in der mangelnden Kompetenz des Fachhandels. Hier 
              fehlt es oftmals an der freundlichen, ehrlichen und vor allem fachkundigen 
              Beratung. Der Kunde st&uuml;lpt sich ein fertiges Outfit &uuml;ber, 
              oder er bekommt eins &uuml;bergest&uuml;lpt. Anschlie&szlig;end 
              l&auml;sst man ihn mit diesem Totallook auf die Menschheit los, 
              und dann rennt er rum wie jeder andere.</p>
            <p align="center">Die graue Maus ist auch nicht der wahre Jakob</p>
            <p>M&ouml;glichkeit zwei, die h&auml;ufig vorkommt: Der weniger Modeversierte 
              f&auml;ngt an, sich mit Einzelteilen einzudecken. Jedes f&uuml;r 
              sich genommen sicherlich modisch ansprechend. Aber dann beginnt 
              zu Hause das wilde Kombinieren mit bereits Vorhandenem. Ergebnis: 
              Das Bild im Spiegel ist v&ouml;llig identisch mit einem Foto aus 
              jeder Fu&szlig;g&auml;ngerzone dieser Republik. Mainstream eben. 
              Mit der Vielfalt der in den einschl&auml;gigen Gazetten angepriesenen 
              Tipps (Welcher Typ sind Sie?) umzugehen ist alles andere als einfach. 
              Da gibt es ganz wenige, die ein Gesp&uuml;r f&uuml;r ihren Typ haben. 
              Naturtalente sind eine sehr seltene Spezies. F&uuml;r alle anderen 
              gilt, sich ein wirklich gutes Fachgesch&auml;ft zu suchen, wo es 
              noch die entsprechende Beratung gibt, oder an einem Wochenendseminar 
              zur Typberatung teilzunehmen. Letzteres ist eine Investition f&uuml;rs 
              Leben.</p>
            <p>Kommen wir zu einer anderen Form des Outfits. Die Gegenbewegung 
              des Understatements. M&auml;nner wie Frauen k&auml;men in dieser 
              Kategorie niemals auf die Idee zu zeigen, was man hat oder was man 
              ist. Hier z&auml;hlt farblos statt farbenfroh, Klassik statt Moderne. 
              So weit, so gut. Nur zu h&auml;ufig ist das Resultat dieser Epoche 
              die unterentwickelte oder sogar fehlende kreative Inspiration, einhergehend 
              mit einem Verflachen der Kleiderkultur. Eine gewisse Lustlosigkeit 
              des Verbrauchers macht sich breit. Dabei liegt im Wettbewerb &quot;Wer 
              ist die Sch&ouml;nste im ganzen Land?&quot; ja auch ein gewisser 
              Reiz, der so manche unvergessliche Stilbl&uuml;te hervorgebracht 
              hat.<br>
              Von Mut zur Mode also keine Spur. Der Kleider-Knigge bleibt ebenfalls 
              unbeachtet. M&ouml;glicherweise ist das alles eine Folge von Orientierungs-losigkeit 
              im Zusammenhang mit schlechter Beratung. Zudem verunsichern schlechte 
              Absatzm&auml;rkte und die mangelnde Begeisterung der Verk&auml;ufer 
              und Endverbraucher zunehmend die Designer und Hersteller. Deutlich 
              wird dies erkennbar an dem einfallslosen Einheitsbrei der Kollektionen. 
              Kreiert wird auf Nummer Sicher. Die Wirtschaftlichkeit steht im 
              Vordergrund. Waren die 90er Jahre gepr&auml;gt von einer Schwarz-wei&szlig;-Suppe 
              mit Braun- und Beige-T&ouml;nen, so war es zum Schluss nur noch 
              Grau in Grau und noch mal Grau. Ein Ende dieser melancholisch machenden 
              Tristesse ist nicht in Sicht. Die &quot;Mode-Depression&quot; sitzt 
              tief in fast allen K&ouml;pfen.</p>
            <p>&Auml;hnliches gilt besonders f&uuml;r den Freizeitlook. Das neue 
              Freizeitbewusstsein der letzten zwei Jahrzehnte ver&auml;nderte 
              das Stra&szlig;enbild. Sportswear und Casuallook sind im wahrsten 
              Sinne des Wortes Gassenhauer geworden. Das derzeitige Modebild l&auml;sst 
              sich locker auf eine Kurzformel bringen: Sportlichkeit + Bequemlichkeit 
              = Formlosigkeit. L&auml;ssigkeit wird einfach mit Nachl&auml;ssigkeit 
              gleichgesetzt. Und davon bleibt auch der Arbeitsplatz nicht verschont. 
              Businesslook oder auch einfach nur korrekte Kleidung scheinen im 
              Bewusstsein der Menschen an Bedeutung verloren zu haben. Die breite 
              Masse tr&auml;gt Jeans und T-Shirts. Freizeitkleidungsst&uuml;cke 
              soweit das Auge reicht, bunt gemixt mit einem sportlichen Touch, 
              egal, ob in der Schule, der Beh&ouml;rde, in Gesch&auml;ften oder 
              Unternehmen. Und das treibt seine Stilbl&uuml;ten. Wir kennen diese 
              P&auml;rchen alle aus dem Sommerurlaub: Sie in Radlerhose, dazu 
              Top und Badelatschen. Er mit dem unvermeidlichen Hawaii-Hemd und 
              Boxer-Shorts, abgerundet mit Socken und Sandalen. Bevorzugt anzutreffen 
              ist dieses Erscheinungsbild an den St&auml;tten alter Kulturen.</p>
            <p align="center">Die Kleidung spricht B&auml;nde</p>
            <p>Ob Business, Holiday oder einfach nur schn&ouml;de Freizeit, es 
              dr&auml;ngt sich die Frage auf: &quot;Woher kommt dieser Trend?&quot; 
              Die zur&uuml;ckliegenden 30 Jahre waren unter anderem gepr&auml;gt 
              von Arbeitszeitverk&uuml;rzung, mehr Freizeit und mehr Geld. Die 
              J&uuml;ngeren mutierten zur Null-Bock-Generation. Und da ergibt 
              sich zwingend die n&auml;chste Frage: &quot;Dr&uuml;cken wir nicht 
              mit unserer Kleidung unsere Werte aus?&quot; Oder, etwas provokanter: 
              &quot;Kommunizieren wir nicht auch mit dem &uuml;bertriebenen Freizeitlook 
              unsere Einstellung zur Arbeit?&quot;<br>
              Keineswegs soll hier der Eindruck entstehen, dass der, der sich 
              nicht nach dem Kleider-Knigge kleidet, zwangsl&auml;ufig ein Versager 
              ist. Hier soll Bewusstsein geschaffen werden f&uuml;r ein viel zu 
              unbekanntes aber eminent wichtiges Medium: die eigene pers&ouml;nliche 
              Darstellung. Die meisten haben nicht gelernt, Kleidung als Kommunikationsmittel 
              zu verstehen und richtig einzusetzen. </p>
            <p>Denn wer seinen Typus, damit sind die inneren und &auml;u&szlig;eren 
              Werte gemeint, erst einmal identifiziert hat, dem wird es ein Bed&uuml;rfnis 
              sein, f&uuml;r das entsprechende Outfit zu sorgen. Es wird ihm leichter 
              fallen, als er je geahnt h&auml;tte. Er hat begriffen, wof&uuml;r 
              Authentizit&auml;t steht, n&auml;mlich f&uuml;r Echtheit und damit 
              Glaubw&uuml;rdigkeit.</p>
            <p>So oder so? Die richtige Kleidung ist gerade im Gesch&auml;ftsleben 
              eminent wichtig<br>
