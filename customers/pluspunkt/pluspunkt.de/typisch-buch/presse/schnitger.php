            <h1><b>Presse</b></h1>
            <p><b><font size="2">Dem Unternehmen ein Gesicht verleihen</font></b> 
              - 29.07.03<br>
              Kommunikationsspezialistin Angelika Schnitger &uuml;ber den ersten 
              Eindruck und die Corporate Identity eines Unternehmens<br>
              <br>
              Viele Gesch&auml;ftsleute sehen auch heute noch ihre Visitenkarte 
              als blo&szlig;en Vermittler der eigenen Anschrift an. Ein praktischer 
              Papierschnipsel halt, mehr nicht. Falsch gedacht. In ihrem Aufsatz 
              &quot;Ansehnlich, oder wie ist der erste &#130;Eindruck', der &uuml;berreicht 
              wird?&quot; r&auml;umt Angelika Schnitger mit diesem und anderen 
              Fehleinsch&auml;tzungen auf, die sich wie Spinnenweben in so mancher 
              Schublade festgesetzt haben. </p>
            <p>Kommunikation ist das A und O, wenn es um menschliche Beziehungen 
              geht, Kommunikation ist aber auch der Motor des Gesch&auml;ftsleben. 
              Diesen Aspekt hebt Schnitger auf ihre eigene, pointierte Art hervor. 
              F&uuml;r die Designerin sind Corporate Design und Corporate Identity 
              essentielle Bestandteile der Kommunikation. &quot;Ein Bild sagt 
              mehr als tausend Worte&quot; ist ihr Motto. Anhand von Beispielen 
              verdeutlicht Schnitger, dass in der Gesch&auml;ftswelt Pr&auml;sentation 
              und Verpackung mindestens so wichtig sind wie die Schokolade um 
              das Spielzeug beim &Uuml;-Ei. Die Kunden m&uuml;ssen buchst&auml;blich 
              anbei&szlig;en.</p>
            <p>Allerdings w&uuml;rde beispielsweise bei der Wahl des Designs f&uuml;r 
              die Visitenkarte oder das Firmenlogo die &quot;Seele des Unternehmens&quot; 
              zu oft vernachl&auml;ssig, beklagt die Designerin. Was sie meint, 
              wenn sie von der &quot;Seele des Unternehmens&quot; spricht, wird 
              an ihren Beispielen deutlich. So d&uuml;rfe nicht nur die Branche 
              selbst bei der Entwicklung der passenden Pr&auml;sentation eine 
              Rolle spielen, sondern auch der Charakter und die Position innerhalb 
              dieser m&uuml;ssten ber&uuml;cksichtigt werden. Ein zackiges Design 
              k&ouml;nne so selbst f&uuml;r ein Unternehmen in der Hebetechnik- 
              und Hubwagenbranche, die eigentlich mit langsamen Bewegungen von 
              G&uuml;tern verbunden werde, charakteristisch und wirkungsvoll sein, 
              wenn das Unternehmen sich durch aggressives und scharfes Kalkulieren 
              von der Konkurrenz abhebe. Die Verpackung d&uuml;rfe aber nichts 
              versprechen, was der Inhalt nicht halte. Das Design ist Gesicht 
              und Identit&auml;t eines Unternehmens in einem.</p>
            <p>Dass hinter der Verpackung auch etwas stecken muss, leuchtet ein. 
              W&auml;re hinter der Schale eines &Uuml;berraschungseis nichts als 
              blo&szlig;e Luft, w&uuml;rde sich wohl jedes Kind entt&auml;uscht 
              abwenden. Der Erfolg dieses Marketingproduktes h&auml;tte sich gar 
              niemals eingestellt. Im Produktnamen steht zwar das Wort &Uuml;berraschung, 
              trotzdem sind die Erwartungen relativ konkret. Etwas zum Spielen 
              oder Basteln muss schon im Innern sein. </p>
            <p>Der Fokus Schnitgers Interesses liegt auf eben jener Ebene zwischen 
              Verk&auml;ufer und K&auml;ufer. Und genau darum geht es in der Wirtschaftwelt 
              von heute: sich und sein Unternehmen gut zu verkaufen, ohne zuviel 
              zu versprechen. Das gelingt ihr und den anderen Autoren des Buches 
              &quot;Typ[isch]! Potenziale erlebter Kommunikation&quot;, in dem 
              Schnitgers Aufsatz erschienen ist, ebenfalls. Offensichtlich setzen 
              sie das in die Tat um, was sie selber predigen. Auch das Design 
              des Buches ist ansprechend. Angewandte Theorie nennt man das wohl. 
            </p>
            <p>Angelika Schnitker ist eine von 12 Autoren und Fachleuten, die 
              sich mit den meist ungenutzten Potenzialen der Kommunikation besch&auml;ftigen. 
              Das bei W. Bertelsmann erschienene Buch bietet dabei eine Vielzahl 
              ganz unterschiedlicher Aspekte und Perspektiven an. Das, was auf 
              den ersten Blick wie ein bunt best&uuml;ckter Bauchladen erscheint, 
              entpuppt sich bei n&auml;herem Hinsehen als eine wohldosierte Mischung 
              hochwirksamer Strategien, die neugierig macht auf mehr und die auf 
              vielf&auml;ltige Weise praktikable Anregungen vermittelt. Was kann 
              man besseres von einem Fachbuch &uuml;ber neue Unternehmensstrategien 
              noch sagen?!</p>
            <p>Die unterschiedlichen Ans&auml;tze verbinden sich im &Uuml;brigen 
              zu einem feinen Netzwerk, das die Autoren untereinander bilden und 
              miteinander verbindet. Dreh- und Angelpunkt dieses Netzwerkes ist 
              das M&uuml;nsteraner Unternehmen &quot;Pluspunkt Unternehmensentwicklung&quot;, 
              das sich vor allem auf das Thema Personalentwicklung spezialisiert 
              hat. Der Zusammenhalt der Autoren zeugt von diesem Konzept und gibt 
              damit auch f&uuml;r Pluspunkt und deren Netzwerk 
              eine lebendige Visitenkarte ab. Preis: 24,90 &euro;.</p>
