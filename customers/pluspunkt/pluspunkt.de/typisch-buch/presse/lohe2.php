            <h1><b>Presse</b></h1>
            <p><b>Das Potenzial nutzen</b> - 01/02.2004<b><font size="2"><br>
              </font></b> F&uuml;hrung will gelernt sein: Gute Motivation l&auml;sst 
              sich f&uuml;r den Prozess nutzen<br>
              <br>
              Arbeitsgruppen und Abteilungssitzungen stehen oft unter einem schlechten 
              Stern: Es kommt auf F&uuml;hrungsf&auml;higkeiten und auf die Regeln 
              der Moderation an, mit denen man alle Betroffenen zu Beteiligten 
              werden lassen kann. Das Ergebnis der Diskussion kann von allen geteilt 
              werden. Das bietet viele Vorz&uuml;ge. Mitarbeiter zu motivieren, 
              liegt nicht jedem Vorgesetzten im Blut. Das ist aber kein Problem. 
              Man kann es lernen. Und das sollte man auch, wenn man wirklich etwas 
              bewegen m&ouml;chte. Denn motivierte Mitarbeiter sind das Kapital 
              eines jeden Unternehmens. Wer sich schon einmal mit dieser Thematik 
              besch&auml;ftigt hat, wei&szlig;, was der Unterschied zwischen einer 
              motivierten und einer unmotivierten Belegschaft ist - und wie man 
              diesen beeinflussen kann.</p>
            <p align="center">Betroffene werden zu Beteiligten</p>
            <p>An erster Stelle steht das Prinzip, die Betroffenen zu Beteiligten 
              zu machen. Denn jeder, der eine Idee mit geboren hat, steht f&uuml;r 
              diese auch voll und ganz ein. Welche Mutter tut nicht alles f&uuml;r 
              ihr Kind?! Umgekehrt ist im Berufsleben wohl wenig so frustrierend 
              und demotivierend wie der Moment, in dem man die neueste Entscheidung 
              aus dem eigenen Unternehmen aus der Zeitung erf&auml;hrt. F&uuml;r 
              den Vorgesetzten bedeutet dies zwar auch weiterhin vorzu-denken, 
              aber zugleich die Analysen, Ideenfindungen und Entscheidungen gemeinsam 
              mit seinen Mitarbeitern zu erarbeiten.</p>
            <p> Man erlebt es immer wieder, dass F&uuml;hrungskr&auml;fte alles 
              selbst erledigen wollen - und das auch noch f&uuml;r klug halten. 
              Angeblich fehlt die Zeit f&uuml;r lange Diskussionen oder der Mitarbeiter 
              hat nicht gen&uuml;gend Fachkenntnisse oder ist sowieso komplett 
              inkompetent. Au&szlig;erdem m&uuml;sse es schnell gehen, zack, zack. 
              Die gleichen Herrschaften beklagen sich dann bitterlich &uuml;ber 
              unmotivierte Mitarbeiter, die nur ihren Aufgaben nachkommen und 
              wenig Bereitschaft zeigen, Verantwortung zu &uuml;bernehmen. Am 
              liebsten w&uuml;rden sie ihre Untergebenden alle in einen Sack stecken 
              und draufhauen. Oder teeren und federn wie im Wilden Westen. So 
              verliert man nicht nur wertvolle Lebens-, sondern auch Arbeitszeit. 
              Mitarbeiter, die nichts verantworten, leisten nicht nur weniger, 
              sondern schaden auch noch dem Image. S&auml;tze wie: &quot;Da m&uuml;ssen 
              Sie warten, bis der Chef wieder da ist!&quot; m&ouml;gen bei einer 
              millionenschweren Entscheidung auf Verst&auml;ndnis seitens des 
              Gesch&auml;fts-partners sto&szlig;en. Bei einer erw&uuml;nschten 
              Auskunft &uuml;ber einen Liefertermin der Geheimhaltungsstufe Null 
              aber eher weniger. Vertrauen ist der Anfang von allem. Und mit einer 
              gesunden Vertrauensbasis im R&uuml;cken lassen sich offene Diskussionen 
              problemlos und problemorientiert f&uuml;hren. Eine faire Streitkultur 
              ist essenziell f&uuml;r jedes Unternehmen. </p>
            <p>Damit man die Motivation der Mitarbeiter besser nutzen, Vertrauen 
              st&auml;rken und damit Angst abbauen, Mitarbeiter zum unternehmerischen 
              Denken anregen, Eigenverantwortung der Mitarbeiter weiterentwickeln, 
              die Wissens- und Erfahrungsressourcen der Mitarbeiter nutzen und 
              h&ouml;here Leistungen auf freiwilliger Basis entwickeln kann, sollte 
              man sich angew&ouml;hnen, alles mit den Betroffenen in einer offenen 
              Besprechungstechnik zu er&ouml;rtern. Dies ist Teil der Verantwortung 
              in der Personalentwicklung. Gleichzeitig ist jede Besprechung, deren 
              Ergebnis den Aufwand nicht rechtfertigt, verlorene Zeit. Weitblick 
              und Fingerspitzengef&uuml;hl sind hier n&ouml;tig. Und beides ist 
              erlernbar und stets ausbauf&auml;hig.</p>
            <p align="center"> Das Wissen der Mitarbeiter nutzen</p>
            <p>
              Man sollte vielmehr die Kreativit&auml;t und das Insiderwissen der 
              Mitarbeiter nutzen. Gerade auf Gebieten, die Mitarbeiter st&auml;rker 
              betreffen als den Chef, ist deren Ideenreichtum oft gr&ouml;&szlig;er 
              als das seine. Das ist gut so und stellt den Chef keineswegs in 
              Frage. Und nur weil er das Sagen hat, sollte er nicht immer sprechen. 
              Meetings und Besprechungen sind beispielsweise h&auml;ufig effektiver, 
              wenn der Gespr&auml;chsleiter sich so weit zur&uuml;cknimmt wie 
              m&ouml;glich.<br>
            </p>
            <p>Bei allen Problemen, die sachlicher Art sind, hat sich folgende 
              Besprechungslogik in f&uuml;nf Schritten bewehrt, die Pluspunkt 
              auch in Seminaren f&uuml;r F&uuml;hrungskr&auml;fte lehrt:<br>
            </p>
            <p>Punkt 1: <br>
              Zu Beginn ist eine kurze Einf&uuml;hrung in das zu er&ouml;rternde 
              Problem notwendig. Dabei sollte jedoch die L&ouml;sungsvorstellung 
              nicht schon durch die Problemformulierung deutlich werden. Wenn 
              ein Diskussionsleiter immer alles besser wei&szlig;, braucht er 
              keine Besprechungen abzuhalten.<br>
            </p>
            <p>Punkt 2: <br>
              Alle Ursachen f&uuml;r das Problem m&uuml;ssen gefunden werden. 
              Durch die Frage &quot;Warum ist das so?&quot; dringt man immer tiefer 
              zu den Ursachen der Problematik vor, bis man den entscheidenden 
              Punkt gefunden haben. Jetzt hat man den &quot;kritischen Faktor&quot; 
              gefunden, zu dem nun im folgenden Schritt L&ouml;sungen erarbeiten 
              werden.<br>
            </p>
            <p>Punkt 3: <br>
              Um die passenden L&ouml;sungen zu finden, m&uuml;ssen alle Antworten 
              unwidersprochen gesammelt werden. Nur so erh&auml;lt man den Effekt 
              des Brain-Stormings und verhindert, dass Teilnehmer bei der Besprechung 
              ihre Motivation verlieren.<br>
            </p>
            <p>Punkt 4: <br>
              Gemeinsam wird die beste L&ouml;sung bestimmt. Und wirklich gemeinsam. 
              Jeder Vorschlag sollte gepr&uuml;ft werden. Mehr als nur hilfreich 
              ist dabei, wenn die Entscheidungskriterien, die vom Unternehmen 
              vorgegeben sind, bekannt sind.<br>
            </p>
            <p>Punkt 5: <br>
              Im letzten Schritt vereinbart man die Umsetzung der beschlossenen 
              L&ouml;sung. Die Frage, die gekl&auml;rt sein muss, lautet: Wer 
              macht was bis wann? Es sollte auf keinen Fall &uuml;ber Personen 
              entschieden werden, die nicht zu der Besprechung eingeladen worden 
              sind oder die anderen Abteilungen angeh&ouml;ren. Die Devise lautet: 
              Mit der Mannschaft nur vor der eigenen T&uuml;r kehren!<br>
            </p>
            <p>Und als Chef sollte man die Besprechung m&ouml;glichst nicht selbst 
              leiten. Im Zweifelsfall erteilt man sich das Rederecht h&auml;ufiger 
              als anderen. Und das ist unproduktiv.<br>
            </p>
            <p>Nun, keine Sorge, der Chef ist weiterhin wichtig. Aber die Mitarbeiter 
              und die richtige Kommunikation sind es auch. Der Chef ist der Kapit&auml;n, 
              aber nicht auch der Matrose und Komb&uuml;senjunge. Ohne eine motivierte, 
              verantwortungsbewusste und verantwortungstragende Mannschaft kommt 
              man nicht von der Stelle. Diese Erfahrung haben schon viele gemacht. 
              Aber Tatsache ist auch, dass man nicht h&auml;ufig genug darauf 
              hinweisen kann.<br>
            </p>
