Nicht nur gesamtwirtschaftlich gesehen ist das eine Katastrophe. Mittelständische Unternehmen, die auf dem Markt eigentlich weiter bestehen könnten, müssen Insolvenz anmelden. Nicht selten gehen dadurch auch familiäre Beziehungen in die Brüche.

Dabei lassen sich in den meisten Fällen praktikable Lösungen finden, wenn die Unternehmensnachfolge rechtzeitig und richtig angegangen wird. Der Schlüssel zum Erfolg ist die offene Kommunikation zwischen den Generationen, zwischen Eltern und Kind, zwischen Vorgänger und potenziellem Nachfolger, zwischen Juniorchef und Seniorchef. Der Generationswechsel im Betrieb muss rechtzeitig und offen angegangen werden. Anders ist eine erfolgreiche Unternehmensnachfolge kaum zu realisieren.

Kommunikation und Organisation sind die Bereiche, auf die wir bei unserer Unternehmensberatung besonderen Wert legen. Welche Sorgen und Ängste haben die Gründer bezüglich der Unternehmensnachfolge? Wieso zögert die junge Generation, als Juniorchef die Unternehmensnachfolge anzutreten?

In vielen Fällen hilft die offene Kommunikation und zielgerichtete Aussprache, das Problem Generationswechsel im Betrieb in den Griff zu kriegen. Viele Konflikte schwelen unterschwellig weiter, anstatt sie offen auszutragen und zu einer Lösung zu bringen: Übernahme und Abgabe von Verantwortung, Pensionen und Renten um nur einige zu nennen. Neben betriebswirtschaftlichen Aspekten müssen dabei auch juristische, erbrechtliche und manch psychologische Hürden genommen und bewältigt werden.

