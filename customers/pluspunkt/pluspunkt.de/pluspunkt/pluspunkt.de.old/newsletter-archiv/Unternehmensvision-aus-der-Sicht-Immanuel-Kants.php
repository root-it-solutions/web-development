<h3>Unternehmensvision aus der Sicht Immanuel Kants</h3>

<p>Freiheit besteht nach Kant darin, &uuml;ber sich nachzudenken und dann im Wettbewerb etwas selbst&auml;ndig zu vollbringen (er nennt es: die Grenzen der Sach- und Systemzw&auml;nge wenigstens im Geiste zu &uuml;berschreiten). In einer Vision wird immer eine Herausforderung beschrieben, die erreicht werden soll und die im Wettbewerb mit anderen Kr&auml;ften des Marktes Motivation freisetzt (die Lust am Wetteifer und die Freiheit im Denken, um Fortschritt und Entfaltung zu mehren).</p>

<p>Freiheit verwirklichen hei&szlig;t nach Kant, lustvoll produktiv und innovativ zu sein und dabei das Unm&ouml;gliche zu denken. Es geht um eine "regulative Idee", an der die langfristigen Ziele ausgerichtet werden sollen und das Verm&ouml;gen des Einzelnen, an der Verwirklichung dieser Ideen mitzuwirken.<br />Kluges Management wird sich immer dadurch auszeichnen, m&ouml;gliche Freir&auml;ume zu suchen, auszuloten und notfalls neu zu erfinden. Erfolgreich ist dies besonders unter der Beteiligung der betroffenen Mitarbeiter.<br />Nach Kant erm&ouml;glicht die einzigartige Vernunft, mit der der Mensch ausgestattet ist, sich auf seine eigenen Urteile zu verlassen. Dies entspricht dem Verm&ouml;gen, das Bild einer Vision zu erschaffen und dem nachzustreben, auch wenn der Weg zum Ziel anfangs noch nicht klar ist oder man gro&szlig;em Unverst&auml;ndnis ausgesetzt ist.<br />Unternehmensziele m&uuml;ssen nach Kant grunds&auml;tzlich dem allgemeinen Fortschritt dienen. Dies wird bei der Beteiligung von Mitarbeitern an der Entwicklung einer Vision erzielt, da in der Gruppe positive Werte zum Ausdruck gebracht werden (denkend und wetteifernd Bedingungen schaffen, die f&uuml;r alle Menschen ein Leben in W&uuml;rde und Freiheit erm&ouml;glichen). Frei sein hei&szlig;t anzusteben, die Regeln zu &auml;ndern - allerdings nur in eine Richtung die allen zugutekommt und nicht nur Einzelnen.</p>

<p>F&uuml;r Kant ist der Motor des Fortschritts der Wetteifer (eine Anlage des Guten im Menschen). Er bringt die Ideen und Ver&auml;nderungen hervor, die Freiheit und Menschlichkeit f&uuml;r immer mehr Menschen Wirklichkeit werden lassen.<br />Nach Kant haben Feigheit und Faulheit einen Gro&szlig;teil der Menschen dazu gebracht, sich der Leitung durch andere zu unterwerfen. Statt sich in dieser Unm&uuml;ndigkeit zu belassen, gilt es den Ausgang aus der selbstverschuldeten Unm&uuml;ndigkeit zu beschreiten. Die Frage: "Wie k&ouml;nnen wir dies oder jenes &auml;ndern?" ist der erste Schritt sich aus den Zw&auml;ngen zu befreien.</p>

<p>Es gilt, immer wieder die Unm&uuml;ndigkeit in Form von lieb gewonnenen Erkl&auml;rungsmustern abzustreifen und die Ressource Denken gewinnbringend einzusetzen - auch und gerade, um den akuten Zw&auml;ngen gerecht zu werden, ohne deswegen die menschlichen Ziele aus den Augen zu verlieren.</p>

