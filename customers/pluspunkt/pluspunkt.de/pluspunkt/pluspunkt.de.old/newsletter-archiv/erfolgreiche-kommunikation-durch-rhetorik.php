                  <p>
Dieser Buchtitel ist in meinen Augen schon eine Provokation. Und so hätte ich beinahe dieses (Hör-)Buch nicht in die Finger bekommen. Doch letztendlich hat dann meine Neugier gesiegt, und das war gut so.
