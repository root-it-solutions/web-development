<p>
Da Kommunikation immer der Versuch ist, andere zu beeinflussen, kann ich dem Autor folgen, wenn er behauptet, dass dies bereits eine Form der Manipulation ist. Ob der Gesprächspartner unsere Worte als negative oder positive Manipulation auffasst, hängt entscheidend davon ab, mit welcher Absicht kommuniziert wird. Hier geht der Autor wie auch wir davon aus, dass sich jeder dieser Verantwortung bewusst ist.
</p>
<p>
Stefan Gössler ist NLP-Trainer und betrachte die Rhetorik aus der Sichtweise der NLP. Es kommt zwar keine andere Erkenntnis dabei heraus, als die die wir lehren. Doch der spezielle Blickwinkel regt zum Nachdenken an und ermöglicht weitere nützliche Inspirationen zum Thema Rhetorik bzw. Kommunikation aufzunehmen.
Grundsätzlich geht es in diesem Buch darum, wie wir uns in unserer Kommunikation auf unsere Gesprächspartner optimal einstellen. Dabei geht Gössler sehr systematisch vor. In insgesamt vier Kapiteln beschreibt er:
<ol>
<li>wie man sich darauf einstellen kann, dass Menschen in der Interaktion nach Sympathie und Gemeinsamkeiten trachten und sich von Autorität und Knappheit der Ressourcen beeinflussen lassen.</li>
<li>wie man sich auf die unterschiedlichen Charaktere und ihre Art zu Denken einstellen kann. Mit verständlichen Beispielen gibt er auch Hinweise, wie sich die Charaktere schnell an ihrer Sprache erkennen lassen. 
Dieses Thema korrespondiert mit der Verhaltenspräferenzanalyse, wie wir sie einsetzen. Dabei geht Gössler auf die Erkenntnisse von C.G. Jung ein und liefert damit Hinweise für alle vier von Jung definierten Präferenzpaaren.</li>
<li>wie man durch Berücksichtigung von a) Zahlen und Daten, b) Beispielen, c) Referenzen und d) Normen und Regeln seine Argumentationen unterstreichen kann, um eine (schnellere) Akzeptanz zu erhalten.</li>
<li>welche Worte unseren Äußerungen Kraft verleihen und welche sie eher schwächen. Diese Erkenntnis aus der NLP heraus helfen, eigene Texte zu optimieren bzw. den eigenen Sprachgebrauch zu verbessern.</li>
</ol>
</p>
<p>
Dieses Hörbuch empfehle ich all unseren Teilnehmern, um das Wissen aufzufrischen und sich selber in der Kommunikation kritisch zu überprüfen. Es empfiehlt sich zudem mehrfach zu hören, zumal der Autor Begrifflichkeiten benutzt, die den meisten Lesern/ Hörern nicht geläufig sein werden.
