<ol>

<li>Ausrichtung geben<br />Hinter allen Zielen, die tagt&auml;glich angegangen werden, stehen h&ouml;here Ziele. Und diese k&ouml;nnen sehr unterschiedlich sein. In einem Unternehmen geht es nur um das &Uuml;berleben, in einem anderen m&ouml;chte man Marktf&uuml;hrer werden. Andere versuchen sich im Markt zu positionieren und schaffen Wettbewerbsvorteile in dem sie z.B. die beste Frische von Lebensmitteln verkaufen, die gr&ouml;&szlig;te Kundenzufriedenheit schaffen oder ein Versprechen, das sie den Kunden gegeben haben, erf&uuml;llen. Egal welches Oberziel angestrebt wird, es muss allen Beteiligten klar sein, um eine Ausrichtung zu geben und Fehlentscheidungen zu vermeiden. Ist dieses Oberziel jedoch nur in den K&ouml;pfen der obersten F&uuml;hrungsebene und wird es nicht an alle Mitarbeiter kommuniziert, sind diese auf ihre pers&ouml;nlichen Interpretationen angewiesen. Nicht h&auml;ufig sind diese dann mehr von den eigenen Interessen als von denen des Unternehmens gepr&auml;gt.<br />Hier empfehlen wir, dem Unternehmen eine Vision zu geben, die von Zeit zu Zeit &uuml;berarbeitet wird und allen Beteiligten (auch Kunden und Lieferanten) zeigt, wo die Reise hingehen soll. Dann kann sich auch der folgende Satz realisieren: Die Menschen machen denen Platz, die genau wissen wohin sie wollen.</li>

<li>Beteiligung der Betroffenen<br />Unsere Zeit ist von vielen Ver&auml;nderungen gepr&auml;gt, die vom Menschen grunds&auml;tzlich nicht als angenehm empfunden werden. Nur Ver&auml;nderungen, die man selber auf den Weg bringt, findet der Mensch gut. Sie k&ouml;nnen das schnell nachvollziehen, wenn Sie an Ver&auml;nderungen denken, die Ihnen widerfahren sind. Erst wenn Sie diese angenommen haben, lie&szlig; der empfundene Druck nach.<br />Aus dieser Erkenntnis heraus ist es geradezu zwingend, daf&uuml;r zu sorgen, dass Ver&auml;nderungen von denen herbeigef&uuml;hrt werden, die anschlie&szlig;end damit umgehen m&uuml;ssen. Die Beteiligung der betroffenen Mitarbeiter kostet zwar Zeit und gelegentlich auch Geld, stellt aber eine gute Investition dar, aus der heraus Akzeptanz und Verantwortlichkeit hervorgeht.<br />Nehmen wir hier nur mal das Beispiel der Ausrichtung eines Unternehmens durch eine Vision. Die Mitarbeiter, die daran mitgearbeitet haben, die Vision zu erstellen, m&uuml;ssen zur Ableitung konkreter kurzfristiger Ziele nicht erst &uuml;berredet werden. Vielmehr weckt man durch die Beteiligung der Mitarbeiter das Gef&uuml;hl dazuzugeh&ouml;ren und aktiv an der Zukunft mitzuwirken. Das ist Motivation pur.</li>

