    <p> <font color="#000000"><b><font size="3">Vier Stunden Starthilfe</font><br />Chancenforum des gwc.netzwerks für Gründung und Wachstum<br /></b></p>
		<p>
			Wie kann ein Gründungsvorhaben gefördert werden, welche Anforderungen muss ein Geschäftsplan erfüllen, und wie teuer werden Steuer- und Sozialversicherung? Auf diese und 
			auf viele weitere Fragen erhielten die Teilnehmer der „Messe für Existenzgründer und Jungunternehmer - Chancenforum 2008“ fundierte Antworten: Die 14 Institutionen und 
			Initiativen des gwc.netzwerks für Gründung und Wachstum im Kreis Coesfeld hatten in den Räumen der Schornsteinfegerschule ihre Infostände aufgeschlagen. Vier Stunden 
			lang standen die Experten den Gründern als Ansprechpartner für eine erste Kontaktaufnahme zur Verfügung. „Das Angebot wurde von den Teilnehmern gerne angenommen‚ 
			resümiert Henner Rohde, Existenzgründungsberater der Kreishandwerkerschaft Coesfeld, die Veranstaltung. Abgerundet wurde der Netzwerk-Nachmittag durch den Vortrag 
			eines externen Referenten: Wie ein junges Unternehmen auf Erfolgskurs zu führen und zu halten sei, verriet Ralf Lohe (Pluspunkt GmbH) den Forumsteilnehmern im Hörsaal 
			der Schornsteinfegerschule. In seinem Vortrag „Richtig führen - richtig Geld verdienen - vom Existenzgründer zur effektiven Führungspersönlichkeit“ öffnete der Sendener 
			Unternehmensberater die Schatzkiste seiner Erfahrungen und gab Tipps für alle Gründungsphasen. Welchen Teil des Marktes will ich besetzen? Welchen Wettbewerbsvorteil will 
			ich platzieren und kommunizieren? Wie binde ich meine Mitarbeiter optimal ein und motiviere sie am besten? Zu diesen und zu vielen anderen gründungsrelevanten Themen bezog 
			Ralf Lohe in seinem Vortrag Stellung. Eröffnet wurde das Chancenforum vom Obermeister der Schornsteinfegerinnung Karl- Heinz Vogt. Er wies daraufhin, dass der 
			Landesinnungsverband der Schornsteinfeger als zentrale Schulungsstätte in Nordrhein-Westfalen mehr als 1000 Betriebe in die Selbständigkeit gebracht habe. 
		</p>
