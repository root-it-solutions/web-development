                        <p><b>Zum Schluss kommt Hummer auf den Tisch </b>
<br /><i>Ein kurzweiliger Nachmittag über Etikette im Geschäftsleben</i>  von Hubertus Kost </p>
<p>

Wer sich mit korrekten Umgangsformen gut auskennt, besonders mit den manchmal kniffligen Situationen am Arbeitsplatz oder ganz allgemein im Geschäftsleben, der muss bestimmt nicht an einem Etikette-Kurzseminar teilnehmen. Oder doch: Denn es gibt sehr vergnügliche Arten, sich Inspirieren zu lassen und dann — sozusagen als Belohnung für aktive Teilnahme — noch einen halben Hummer zu verköstigen (der Begriff „essen“ ist hier völlig unangebracht).
</p><p>
Die Pluspunkt-Unternehmensberatung aus Senden hat solche Ideen, nennt den Nachmittag, zu dem sie ihre Kunden (überwiegend Handwerksunternehmer) einlädt, ganz bescheiden Inspiration und lässt eine Fachfrau sehr unterhaltsam darüber sprechen, wie man (und frau) Etikette wahrt. Und das ist dann auch gleich die erste Frage: Woher kommt das Wort „Etikette“? Nach verschiedenen Interpretationen geht es auf den französischen Hof zurück, als die Könige Ludwig hießen. Dort wurde auf Zetteln (Etiketten) die Rangfolge von Personen notiert, die Zugang zum Hof hatten. Daraus ergaben sich so allerlei (Benimm-)Regeln. Und es macht sich auch in unserer Zeit gut, wenn man einige Verhaltensregeln, die sich im Laufe der Jahrhunderte verändert haben, kennt und auch anwendet. Und dort kennt Carolin Ruppert sich aus. Die Fachfrau für Unternehmenskommunikation aus Münster bietet auch Business-Knigge-Seminare an. Eine Kurzform erlebten die Inspirations-Teilnehmer. Zum Beispiel das Vorstellen von Gesprächspartnern. Im Geschäftsleben — das erläutert Caroline Ruppert amüsant und zugleich lehrreich — ist die Hierarchie entscheidend und nicht das Geschlecht. Das gilt auch für das „Du“-Angebot. Und wer wird privat wem zuerst bekannt gemacht? Der Herr der Dame, sagt Frau Ruppert. Aber privat ist zumindest die Sache mit dem „Du“ einfacher. Drei Antworten werden zu jeder Frage angeboten. Das klingt leicht, ist es aber keinesfalls immer. Wie ist das mit Blumen, wenn zu einer Geburtstagsfeier ins Restaurant eingeladen wird? Die Blumen bitte vorher zu Hause beim Gastgeber abgeben (lassen). Und was ist korrekt, wenn einem der Chef auf der Straße begegnet? Weder auf ihn zugehen noch die Straßenseite wechseln. Ein Gruß im Vorbeigehen ist angebracht. Und noch ein Beispiel für einen „Klassiker“: Sie sind im Gespräch mit einem Kunden, das Telefon klingelt. Klingeln lassen? Telefonieren? Kurz melden und Rückruf anbieten— sagt Frau Ruppert. Wo endet übrigens beim Herrn die gut gebundene Krawatte? An der Gürtelschnalle selbstverständlich! Es gibt noch viel mehr Fragen und Antworten. Aber es gibt ja auch noch den Hummer. Der wird von fachkundiger Hand mit sachgerechtem Gerät zubereitet und serviert. Sie mögen keinen Hummer? • Nach diesem Etikette-Kurzseminar auf jeden Fall. Und wenn Sie neugierig geworden sind und mehr erfahren möchten über die Firma Pluspunkt, die zum Beispiel Seminare ganz gezielt auch unter südlicher Sonne anbietet, und über Karoline Ruppert (,‚Gute Kommunikation entlastet Sie“): Im Internet gibt es viele Informationen: <a href="http://www.pluspunkt.de">www.pluspunkt.de</a>, <a href="http://www.caroline-ruppert.de">www.caroline-ruppert.de</a>
