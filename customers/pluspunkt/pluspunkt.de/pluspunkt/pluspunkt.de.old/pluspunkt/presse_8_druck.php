        machen&quot;</i></font><b><font color="#000000"><br>
        <font size="2"><br>
        </font> </font></b><font color="#000000">von Hubertus Kost</font></p>
      <p><font color="#000000"><b>Senden/Erndtebr&uuml;ck</b>. Als klassische 
        Unternehmensberater sehen sich Ralf Lohe und Manfred Rumi aus dem m&uuml;nsterl&auml;ndischen 
        Senden nicht. Ihre Beratung hat einen anderen Ansatz: Der Unternehmer 
        soll seine eigenen Werte erkennen und sein Unternehmen danach ausrichten. 
        Fleischermeister Burkhard M&uuml;ller aus Erndtebr&uuml;ck und B&auml;ckermeister 
        Karsten Kr&uuml;ger aus L&ouml;hne haben Ihre eigenen, positiven Erfahrungen 
        mit &quot;Pluspunkt&quot; gemacht.</font></p>
      <p><font color="#000000">Offenheit, Vertrauen, Verantwortung - &quot;das 
        steckt in den meisten Betriebsinhabern drin&quot;, wissen Lohe und Rumi 
        aus langj&auml;hriger Erfahrung als Trainer f&uuml;r F&uuml;hrungskr&auml;fte. 
        Vielen sei dieses Potenzial aber nicht bewusst, &quot;und deshalb entwickeln 
        wir es.&quot; In Seminaren mit wenigen Teilnehmern geht es den beiden 
        Inhabern der Firma Pluspunkt zun&auml;chst darum, das Selbstvertrauen 
        und die Kommunikationsf&auml;higkeit ihrer Kunden zu st&auml;rken. Erst 
        sp&auml;ter - bei der Begleitung im betrieblichen Alltag - werden Fragen 
        der klassischen Unternehmensberatung beantwortet. Dabei stehen je nach 
        Bedarf weitere Partner zur Verf&uuml;gung.<br>
        Die Kunden sind &uuml;berwiegend Handwerksunternehmer mit Betrieben in 
        einer Gr&ouml;&szlig;enordnung von 10 bis 200 Besch&auml;ftigten. Dazu 
        geh&ouml;rt Karsten Kr&uuml;ger aus L&ouml;hne. Der Junior-Chef der B&auml;ckerei 
        &quot;Karlchen's Backstube&quot; mit 28 Filialen will vor allem die Eigenverantwortung 
        seiner 160 Mitarbeiter f&ouml;rden. Sein erstes Pluspunkt-Seminar hat 
        ihm dazu wichtige Anst&ouml;&szlig;e gegeben. Und zwar zun&auml;chst f&uuml;r 
        sich selbst, denn, so erl&auml;utert der B&auml;cker und Betriebswirt: 
        &quot;Ich habe gelernt, intensiv &uuml;ber mich nachzudenken und auch 
        das eigene Handeln infrage zu stellen.&quot;<br>
        Daraus entwickelte Karsten Kr&uuml;ger neue Gedanken &uuml;ber sein Unternehmen 
        und neues Handeln. Die Strukturver&auml;nderung des handwerklichen Familienbetriebes 
        vom aufgabenbezogenen zum ergebnisorientierten F&uuml;hrungsstil ist daf&uuml;r 
        ein Beispiel. Viele intensive Gespr&auml;che hat Karsten Kr&uuml;ger auf 
        diesem Weg gef&uuml;hrt, auch seine leitenden Mitarbeiter haben inzwischen 
        F&uuml;hrungsseminare bei Pluspunkt besucht. Seine Erfahrung: Positives 
        Denken ist ebenso erlernbar wie &uuml;bertragbar.<br>
        Ein solcher Prozess komme im betrieblichen Alltag aber erst in Gang, wenn 
        Unternehmer und<br>
        F&uuml;hrungskr&auml;fte selbst positiv denken und - so wie Karsten Kr&uuml;ger 
        - das auch mitteilen, betont Ralf Lohe. Motivation und Kommunikation sind 
        deshalb Schwerpunkte des Einstiegsseminars bei Pluspunkt, das die beiden 
        Trainer auf Fuerteventura anbieten. Damit schaffen sie eine entspannte 
        Atmosph&auml;re weg vom Alltag und zugleich etwas Urlaubsgef&uuml;hl.<br>
        Davon f&uuml;hlte sich auch Burkhard M&uuml;ller angesprochen, der &quot;im 
        Urlaub etwas Sinnvolles machen&quot; wollte: Vor zwei Jahren nahm der 
        Fleischermeister aus Erndtebr&uuml;ck an seinem ersten Seminar auf der 
        Kanaren-Insel teil. Dabei habe er gelernt, sich in seinem Betrieb aus 
        der Funktion des produktiv t&auml;tigen Handwerksmeisters zu l&ouml;sen 
        und mehr F&uuml;hrungsverantwortung zu &uuml;bernehmen. Bei 65 Mitarbeitern 
        und sieben Verkaufsstellen nehme diese Aufgabe zu, betont M&uuml;ller.<br>
        Den Betrieb hat der 39-j&auml;hrige Handwerksunternehmer, der auch im 
        Vorstand der Fleischer-Innung Siegen-Wittgenstein mitarbeitet, von seinen 
        Eltern &uuml;bernommen. Die &Uuml;bergabe wurde von Pluspunkt begleitet, 
        denn &uuml;ber die Seminare hinaus bieten Lohe und Rumi auch individuelle 
        Beratung an und vermitteln dabei sachkundige Partner zum Beispiel zu speziellen 
        betriebswirtschaftlichen Problemen. So arbeitet Pluspunkt unter anderem 
        mit der Zentralstelle f&uuml;r Weiterbildung im Handwerk und mit einigen 
        Handwerkskammern zusammen.<br>
        Pluspunkt legt gro&szlig;en Wert darauf, zu allen Kunden auch ein pers&ouml;nliches 
        Verh&auml;ltnis aufzubauen. Daraus entwickeln sich Gespr&auml;che, die 
        weit &uuml;ber betriebliche Themen hinausgehen. &quot;Auch im privaten 
        Bereich k&ouml;nnen wir h&auml;ufig Hilfestellung leisten,&quot; betont 
        Rumi.<br>
        Positives Denken im privaten Umfeld sehen die beiden Trainer deshalb auch 
        als Erfolgsfaktor f&uuml;r unternehmerisches Handeln. Karsten Kr&uuml;ger 
        formuliert das so: &quot;Ich kann &uuml;ber alles klagen - oder ich mache 
        mir meine eigene Konjunktur.&quot; Der 33J&auml;hrige hat sich erfolgreich 
        f&uuml;r die zweite Variante entschieden: &quot;Karlchen's Backstube&quot; 
        verzeichnet f&uuml;r 2003 das &quot;wirtschaftlich beste Jahr seit zehn 
        Jahren&quot;. Daf&uuml;r hat sich Karsten Kr&uuml;ger, der das Unternehmen 
        nach dem geplanten Generationswechsel im Jahre 2005 gemeinsam mit seiner 
        Schwester Simone B&ouml;hne weiterf&uuml;hren wird, auch selbst fit gemacht: 
        Seit seinem ersten Pluspunkt-Seminar verringerte er sein Gewicht um 30 
        Kilogramm.</font></p>
