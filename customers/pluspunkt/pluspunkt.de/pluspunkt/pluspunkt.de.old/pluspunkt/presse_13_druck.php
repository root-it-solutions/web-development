      <p><b><font color="#000000">Kochen und zwar gemeinsam</font></b></p>
      <p><font color="#000000"><b>Erlebnisabend der Pluspunkt Unternehmensentwicklung 
        gegen mangelnde Kommunikation</b></font></p>
                        <p></p>
      <p><font color="#000000"><b>M&uuml;nster - Man kennt das. Auf jeder Party 
        gibt es die sogenannten Eckensteher, die ihre Kommunikation auf die G&auml;ste 
        beschr&auml;nken, die ihnen halbwegs bekannt sind. Beim &quot;Erlebnis- 
        und Kommunikationsabend&quot; der Sendener Firma Pluspunkt Unternehmensentwicklung 
        GmbH in Br&ouml;ker's Speicher sollte dies ganz anders sein.</b></font></p>
      <p><font color="#000000">&quot;Wir haben G&auml;ste eingeladen, die sich 
        unter normalen Umst&auml;nden nicht begegnen w&uuml;rden&quot;, erkl&auml;rt 
        Ralf Lohe, einer der beiden Pluspunkt-Gesch&auml;ftsf&uuml;hrer, das ungew&ouml;hnlich 
        Party-Konzept. Will sagen: Lohes Unternehmensentwicklung hat ihre Firmenphilosophie 
        walten lassen, welche die Mitarbeiter eines Unternehmens in den Vordergrund 
        stellt. &quot;Die Menschen sind das gr&ouml;&szlig;te Kapital eines Betriebes&quot;, 
        sagt er.<br>
        <br>
        Und gerade hier krankt es bisweilen. Gerade in mittelst&auml;ndischen 
        Handwerksbetrieben, die die Pluspunkt-Unternehmensentwicklung haupts&auml;chlich 
        betreue, mangele es oft an interner und externer Kommunikation, wei&szlig; 
        Lohe, der selbst aus einem Handwerksberuf kommt. &quot;Es geht darum, 
        das Selbstvertrauen der Mitarbeiter zu st&auml;rken und ihnen kommunikative 
        M&ouml;glichkeiten an die Hand zu geben, mit denen sie ihr Unternehmen 
        in ein positiveres Licht r&uuml;cken k&ouml;nnen&quot;, erkl&auml;rt er. 
        &quot;Nicht so sehr die Zahlen interessieren uns, sondern die so genannten 
        weichen Faktoren innerhalb eines Betriebes.&quot;<br>
        <br>
        Zusammen mit zehn Unternehmenspartnern aus ganz Nordrhein-Westfalen - 
        darunter sowohl Internetagenturen und Personaldienstleister als auch Landschaftsg&auml;rtner 
        und Personaldienstleister - habe die Pluspunkt-Unternehmensentwicklung 
        in den vergangenen sechs Jahren etwa 50 mittelst&auml;ndische Betriebe 
        in punkto Unternehmensleitung und -entwicklung betreut, so Lohe weiter. 
        &quot;Und die Kommunikationskompetenz steht dabei im Mittelpunkt&quot;, 
        sagt er.<br>
        <br>
        So auch am Samstag. Anders kann es auch nicht gewertet werden, wenn 50 
        sich unbekannte Menschen - allesamt Gesch&auml;ftspartner und Bekannte 
        der Pluspunkt-Unternehmensleitung - gemeinsam kochen sollen. Dies war 
        n&auml;mlich der Clou des &quot;Erlebnis- und Kommunikationsabends&quot;. 
        Miteinander den Tisch decken, Gem&uuml;se putzen und Fleisch tranchieren 
        - dies hatten Lohe und sein Kollege Rumi ihren G&auml;sten auferlegt.<br>
        <br>
        Unter kompetenter Aufsicht von erfahrenen K&uuml;chenchefs brachten die 
        G&auml;ste denn auch Exquisites wie Bouillabaisse oder Ziegenquark zu 
        Stande, und Michael Wrasmann, einer der G&auml;ste, war von der Idee begeistert. 
        &quot;Das Kochen wird zum ersten gemeinsamen Thema, und man wird zur Kommunikation 
        mit Fremden aufgefordert&quot;, freut sich der &quot;Back Europ Nordwest&quot; 
        aus Osnabr&uuml;ck.<br>
        <br>
        Allein das Kulinarische wird indes nicht die einzige Gemeinsamkeit der 
        G&auml;ste geblieben sein - warnen f&uuml;r sie doch papierne Kochm&uuml;tzen 
        vorbereitet, auf die sie ihre Namen schreiben konnten. Und bekannterma&szlig;en 
        kann schon diese Kenntnis f&uuml;r eine aufregende Kommunikation ausreichend 
        sein.</font></p>
