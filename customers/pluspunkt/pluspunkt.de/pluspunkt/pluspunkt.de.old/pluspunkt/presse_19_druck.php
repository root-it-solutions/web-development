      <p><font color="#000000"><b> Bremen</b>. Weniger Hierarchie f&uuml;hrt zu 
        mehr Motivation. Von dieser These sind Joachim und Christian Tenter, Gesch&auml;ftsf&uuml;hrer 
        der Bremer B&auml;ckerei Tenter's Backhaus, &uuml;berzeugt. Seit etwa 
        vier Wochen kommt ihr Betrieb ohne Bezirks- und Verkaufsleiter aus. Stattdessen 
        gibt es nun die neue Position der Filialgruppenleiter.<br>
        <br>
        Der Schritt basiert auf auf der Erkenntnis, dass reine Kontrollinstanzen 
        nicht mehr zeitgem&auml;&szlig; sind, weil sie die Motivation der &quot;Ebene 
        darunter&quot; einschr&auml;nken oder gar nicht erst aufkommen lassen. 
        &quot;Wir hatten bislang zwei Bezirksleiter&quot;, erkl&auml;rt Joachim 
        Tenter, &quot;die zwischen den Filialen hin- und hergefahren sind, um 
        den Verk&auml;uferinnen Tipps zu geben.&quot;<br>
        <br>
        In den Verkauf waren sie nicht fest eingeplant. Und genau dies habe sich 
        nicht bew&auml;hrt: Die B&auml;ckereiverk&auml;uferinnen in den einzelnen 
        Filialen waren zu wenig motiviert, sich eigenst&auml;ndig um die Verbesserung 
        des Umgangs mit den Kunden zu k&uuml;mmern - einer wichtigen Grundlage 
        im B&auml;ckereihandwerk. Ergebnis: Ein Umsatz, der die Tenters nicht 
        zufrieden stellte.<br>
        <br>
        &quot;Wenn Sie heute zum B&auml;cker gehen, finden Sie &uuml;berall &auml;hnliche 
        Produkte, also entscheidet die Qualit&auml;t des Services, ob Sie wiederkommen&quot;, 
        sagt Joachim Tenter, der das mittelst&auml;ndische Unternehmen Anfang 
        des Jahres gemeinsam mit seinem Bruder Christian vom Vater &uuml;bernommen 
        hat - ein Generationswechsel nach 20 Jahren. Die Umstrukturierung der 
        Personalhierarchie ist ein Teil des neuen F&uuml;hrungsstils, f&uuml;r 
        den sich die Br&uuml;der entschieden haben. Insgesamt soll der Mitarbeiter 
        mehr Verantwortung &uuml;bernehmen, was im Idealfall zwei positive Resultate 
        nach sich zieht: Einen mit seiner Arbeit zufriedenen, motivierten Angestellten, 
        und mehr Umsatz f&uuml;r das Unternehmen.<br>
        <br>
        Bei Tenter's Backhaus gibt es nun insgesamt sieben Gruppenleiterinnen, 
        die jeweils vier der insgesamt 30 Filialen betreuen. Gleichzeitig arbeiten 
        sie aber auch als Verk&auml;uferinnen in Ihren Zweigstellen - und m&uuml;ssen 
        so nicht nur die Mitarbeiterinnen, sondern auch sich selbst motivieren. 
        Unterst&uuml;tzt werden sie und die 170 Tenter's-Mitarbeiter dabei von 
        der Unternehmensentwicklung Pluspunkt, die das B&auml;ckerei-Unternehmen 
        bei der Umstrukturierung der Personalstruktur unterst&uuml;tzt und auch 
        in Zukunft begleiten wird.<br>
        <br>
        Die Abschaffung einer Hierarchie-Ebene bleibe ohne Wirkung, wenn nicht 
        weitere Schritte hinzuk&auml;men, sagt Ralf Lohe, Gesch&auml;ftsf&uuml;hrer 
        von Pluspunkt. Dazu z&auml;hlt er die gezielte F&ouml;rderung von talentierten 
        Mitarbeitern ebenso wie M&ouml;glichkeiten zur betrieblichen Weiterbildung. 
        Vor allem letztere werde oft nicht von &quot;oben&quot; verordnet, sondern 
        von den Angestellten gefordert.<b> </b></font><font color="#000000"><b></b></font></p>
