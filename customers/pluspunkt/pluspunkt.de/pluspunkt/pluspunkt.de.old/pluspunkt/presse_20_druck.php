      <p><em><font color="#000000">Potenzial bewusst machen. Das ist der Bildungsauftrag 
        von Pluspunkt, einer Unternehmensberatung, die Handwerkern auf die Spr&uuml;nge 
        helfen will. M&ouml;glichst weit weg vom Betriebsalltag sollen die Unternehmer 
        sich reflektieren. Was ein B&auml;ckermeister aus Salzbergen und seine 
        Frau auf Fuerteventura erlebten.</font></em></p>
      <p><font color="#000000">Zur Weiterbildung nach Fuerteventura? &quot;Nat&uuml;rlich 
        haben viele Bekannte gedacht, dass wir dort Urlaub machen,&quot; schmunzelt 
        Simone Puls. Der Aufenthalt auf der Kanaren-Insel hatte f&uuml;r die junge 
        Frau und ihren Mann, den B&auml;ckermeister Alexander Puls, allerdings 
        nichts mit Ferien zu tun. In einem sechst&auml;gigen F&uuml;hrungsseminar 
        gewann das B&auml;cker-Ehepaar aus Salzbergen - gemeinsam mit anderen 
        Interessenten - neue Erkenntnisse &uuml;ber Motivation und Delegation, 
        &uuml;ber eigenverantwortliches Handeln der Mitarbeiter sowie &uuml;ber 
        die Entwicklung und Umsetzung von Visionen.<br>
        <br>
        Und das kann man am besten auf Fuerteventura? Jetzt schmunzeln Ralf Lohe 
        und Manfred Rumi. Die beiden Gesch&auml;ftsf&uuml;hrer der Firma Pluspunkt-Unternehmens-entwicklung 
        aus dem m&uuml;nsterl&auml;ndischen Senden veranstalten ihre k&uuml;rzeren 
        Seminare &uuml;berwiegend in Deutschland, auf jeden Fall aber &quot;weit 
        weg vom betrieblichem Alltag&quot;. Das schaffe eine entspannte Atmosph&auml;re 
        und in einem Feriengebiet - wie auf der Kanareninsel im Robinson-Club 
        Esquinzo Playa - zugleich etwas Urlaubsgef&uuml;hl, was sich positiv auf 
        die Seminararbeit auswirke.<br>
        <br>
        Und die steht nat&uuml;rlich im Vordergrund und ist bei Pluspunkt darauf 
        ausgerichtet, dass die Teilnehmer - &uuml;berwiegend Handwerksunternehmer 
        und F&uuml;hrungskr&auml;fte mittelst&auml;ndischer Betriebe - ihre eigenen 
        Werte erkennen und ihr Unternehmen danach ausrichten sollen. Lohe und 
        Rumi geht es darum, das Selbstvertrauen und die Kommunikationsf&auml;higkeit 
        ihrer Kunden zu st&auml;rken.<br>
        <br>
        Verantwortung, Offenheit, Vertrauen - &quot;das steckt in den meisten 
        Betriebsinhabern und F&uuml;hrungskr&auml;ften drin&quot;, wissen die 
        beiden Trainer aus langj&auml;hriger Erfahrung. Vielen sei dieses Potenzial 
        aber nicht bewusst, &quot;und deshalb entwickeln wir es und bringen damit 
        einen Prozess in Gang, der positives Denken vermittelt.&quot; Das sei 
        nicht nur erlernbar, sondern auch auf Mitarbeiter &uuml;bertragbar. <br>
        <br>
        Ein Ansatz, der auch Alexander Puls &uuml;berzeugt. Gerade der kleine 
        und mittelst&auml;ndische Unternehmer sei h&auml;ufig zu sehr auf betriebliche 
        Abl&auml;ufe konzentriert, ohne die Mitarbeiter eigenverantwortlich einzubinden, 
        unterstreicht der Handwerksunternhemer, der die B&auml;ckerei mit vier 
        Filialen und 35 Mitarbeitern gemeinsam mit seiner Frau leitet. Und weil 
        sie beide - wie so h&auml;ufig in einem Familienbetrieb - in der Verantwortung 
        stehen, haben sie auch gemeinsam das Seminar besucht. Eine Erkenntnis: 
        Es sei ganz wichtig, gemeinsam Visionen zu entwicklen, besonders dann, 
        wenn es schon einmal unterschiedliche Ansichten &uuml;ber die betriebliche 
        Entwicklung gebe.<br>
        <br>
        Visionen zu haben und diese weiterzuentwickeln - dazu lie&szlig;en sich 
        auch Ina Kr&uuml;ger und Frank K&ouml;nemann auf Fuerteventura inspirieren. 
        Beide wollen ihr Potenzial als F&uuml;hrungskr&auml;fte in Karlchen's 
        Backstube, einer gro&szlig;en B&auml;ckerei in L&ouml;hne mit 28 Filialen, 
        ausbauen. Dazu m&uuml;sse &quot;der Kopf frei sein&quot;, betont der gelernte 
        Industriekaufmann K&ouml;nemann, und deshalb spiele die Umgebung eine 
        wichtige Rolle. Zu seinem &quot;pers&ouml;nlichem Seminarplan&quot; geh&ouml;rten 
        daher auch der fr&uuml;hmorgendliche Spaziergang am Meer und das abendliche 
        Training im Fitnessbereich des Clubs. Und bei diesen Aktivit&auml;ten 
        traf er meistens auch andere Seminarteilnehmer.<br>
        <br>
        Hubertus Kost</font></p>
