        </font> <font color="#333333">Führungstraining auf Fuerteventura - weit 
        weg vom Betriebsalltag</font><br>
      <p> <font color="#000000"><b>Zur Weiterbildung nach Fuerteventura? "Natürlich 
        haben viele Bekannte gedacht, dass wir dort Urlaub machen" schmunzelt 
        Simone Puls. Der Aufenthalt auf der Kanaren-Insel hatte für die junge 
        Frau und ihren Mann, den Bäckermeister Alexander Puls, allerdings nichts 
        mit Ferien zu tun. </b> <br>
        <br>
        Führungsseminar<br>
        <br>
        In einem sechstägigen Führungsseminar gewann das Bäcker-Ehepaar aus Salzbergen 
        neue Erkenntnisse über Motivation und Delegation, über eigenverantwortliches 
        Handeln der Mitarbeiter und über die Entwicklung - und Umsetzung - von 
        Visionen.<br>
        Der Unternehmer soll seine eigenen Wette erkennen und sein Unternehmen 
        danach ausrichten: Das ist der Kern des Führungs-Seminars, das Ralf Lohe 
        und Manfred Rumi anbieten.<br>
        Den beiden Geschäftsführern der Firma "Pluspunkt Unternehmensentwicklung" 
        aus Senden geht es zunächst darum, das Selbstvertrauen und die Kommunikationsfähigkeit 
        ihrer Kunden zu stärken.<br>
        Erst später - bei der Begleitung im betrieblichen Alltag - werden Fragen 
        der klassischen Unternehmensberatung wie zum Beispiel Marketing- oder 
        Finanzierungsfragen beantwortet. Dafür stehen je nach Bedarf weitere Partner 
        zur Verfügung. Die Kunden der Firma "Pluspunkt" sind zu einem großen Teil 
        Handwerksunternehmen wie zum Beispiel die Bäckerei Puls. <br>
        <br>
        Immer am Ball bleiben<br>
        <br>
        Als Unternehmer müsse man sich ständig weiterentwickeln - davon ist der 
        36jährige Bäckermeister Alexander Puls ebenso überzeugt wie die anderen 
        Teilnehmer des Seminars.<br>
        Und wenn, wie in seinem Betrieb, die Ehefrau mit in der Verantwortung 
        stehe, dann sei es auch sinnvoll, eine solche Weiterbildung gemeinsam 
        zu machen.<br>
        Verantwortung, Offenheit, Vertrauen "das steckt in den meistens Betriebsinhabern 
        drin", wissen Lohe und Rumi aus langjähriger Erfahrung als Trainer für 
        Führungskräfte. Vielen sei dieses Potenzial aber nicht bewusst, "und deshalb 
        entwickeln wir es und bringen damit einen Prozess in Gang, der positives 
        Denken vermittelt." Das sei nicht nur erlernbar, sondern im Betriebsablauf 
        auch übertragbar. <br>
        <br>
        Weit weg vom Alltag<br>
        <br>
        Die beiden Pluspunkt-Geschäftsführer bieten einige ihrer Seminare - wie 
        das Führungstraining - ganz bewusst "weit weg vom betrieblichen Alltag" 
        an. Das schaffe eine entspannte Atmosphäre und zugleich etwas Urlaubsgefühl. 
        Und das stellt sich Fuerteventura ein, wenn die Seminarstunden am späten 
        Nachmittag beendet sind und dann der Swimmingpool in der Clubanlage oder 
        das Sonnenbad am Strand lockt. <br>
        <br>
        Hubertus Kost </font> </p>
