      <p><font color="#000000"><b>Unternehmensberatungsgesellschaft Pluspunkt 
        propagiert Offenheit und Vertrauen durch Kommunikation</b></font></p>
      <p></p>
      <p><font color="#000000"><b>afz/red, SENDEN</b>. Motivation und Kommunikation 
        sind die Schwerpunkte des Einstiegsseminars, das die beiden Inhaber der 
        Unternehmensberatungsgesellschaft Pluspunkt, Ralf Lohe und Manfred Rumi 
        auf Fuerteventura anbieten. Davon f&uuml;hlte sich auch Burkhard M&uuml;ller, 
        Fleischermeister aus Erndtebr&uuml;ck, angesprochen, der &quot;im Urlaub 
        etwas Sinnvolles&quot; machen wollte:</font></p>
      <p><font color="#000000">Als Unternehmensberater, die zum Beispiel Marketing- 
        oder Finanzierungsprobleme l&ouml;sen, sehen sich Ralf Lohe und Manfred 
        Rumi nicht. Ihre Beratung hat einen anderen Ansatz: Der Unternehmer soll 
        seine eigenen Werte erkennen und sein Unternehmen danach ausrichten.<br>
        Offenheit, Vertrauen, Verantwortung - &quot;das steckt in den meisten 
        Betriebsinhabern drin&quot;, wissen Lohe und Rumi aus langj&auml;hriger 
        Erfahrung als Trainer f&uuml;r F&uuml;hrungskr&auml;fte. Vielen sei dieses 
        Potenzial aber nicht bewusst, &quot;und deshalb entwickeln wir es.&quot; 
        In Seminaren mit wenigen Teilnehmern geht es den beiden Inhabern der Firma 
        Pluspunkt im m&uuml;nsterl&auml;ndischen Senden zun&auml;chst darum, das 
        Selbstvertrauen und die Kommunikation ihrer Kunden zu st&auml;rken. Erst 
        sp&auml;ter - bei der Begleitung im betrieblichen Alltag - werden Fragen 
        der klassischen Unternehmensberatung beantwortet. Dabei stehen je nach 
        Bedarf weitere Partner zur Verf&uuml;gung.</font></p>
      <p><font color="#000000">Die Kunden - das sind &uuml;berwiegend Handwerksunternehmer 
        mit Betrieben in einer Gr&ouml;&szlig;enordnung von zehn bis 200 Besch&auml;ftigten. 
        Dazu geh&ouml;rt der Fleischermeister Burkhard M&uuml;ller aus Erndtebr&uuml;ck 
        bei Siegen, der zun&auml;chst lernte, &uuml;ber sich selbst intensiv nachzudenken. 
        Seine Erfahrung: Positives Denken ist ebenso erlernbar wie &uuml;bertragbar 
        - und dabei muss ein Prozess in Gang gesetzt werden.<br>
        Ein solcher Prozess k&ouml;nne sich im betrieblichen Alltag aber erst 
        entwickeln, wenn Unternehmer und F&uuml;hrungskr&auml;fte selbst positiv 
        d&auml;chten und - so wie Burkhard M&uuml;ller - das auch mitteilten, 
        betonte Ralf Lohe. Motivation und Kommunikation sind deshalb Schwerpunkte 
        des Einstiegsseminars bei Pluspunkt, das die beiden Trainer auf Fuerteventura 
        anbieten. Damit schaffen sie eine entspannte Atmosph&auml;re weg vom Alltag 
        und zugleich etwas Urlaubsgef&uuml;hl. Davon f&uuml;hlte sich auch Burkhard 
        M&uuml;ller angesprochen, der &quot;im Urlaub etwas sinnvolles machen&quot; 
        wollte: Vor zwei Jahren nachm der Fleischermeister an seinem ersten Seminar 
        auf der Kanaren-insel teil. Dabei habe er auch gelernt, sich in seinem 
        Betrieb aus der Funktion des produktiv t&auml;tigen Handwerksmeisters 
        zu l&ouml;sen und mehr F&uuml;hrungsverantwortung zu &uuml;bernehmen. 
        Bei 65 Mitarbeitern und sieben Verkaufsstellen nehme diese Aufgabe zu, 
        betonte M&uuml;ller.<br>
        Den Betrieb hat der Handwerksunternehmer, der auch im Vorstand der Fleischerinnung 
        Siegen-Wittgenstein mitarbeitet, von seinen Eltern &uuml;bernommen - und 
        die &Uuml;bergabe wurde von Pluspunkt begleitet, denn &uuml;ber die Seminare 
        hinaus bieten lohe und Rumi auch individuelle Beratung an und vermitteln 
        dabei sachkundige Partner zum Beispiel zu speziellen betriebswirtschaftlichen 
        Problemen. So arbeitet Pluspunkt unter anderem mit der Zentralstelle f&uuml;r 
        Weiterbildung im Handwerk und mit einigen Handwerkskammern zusammen.<br>
        Pluspunkt legt gro&szlig;en Wert darauf, zu allen Kunden auch ein pers&ouml;nliches 
        Verh&auml;ltnis aufzubauen. Daraus entwickeln sich Gespr&auml;che, die 
        weit &uuml;ber betriebliche Themen hinausgehen. &quot;Auch im privatem 
        Bereich k&ouml;nnen wir h&auml;ufig Hilfestellung leisten,&quot; betont 
        Manfred Rumi. Positives Denken im privaten Umfeld sehen die beiden Trainer 
        deshalb auch als Erfolgsfaktor f&uuml;r unternehmerisches Handeln.</font></p>
