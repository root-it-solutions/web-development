<h1>Krisenmanagement, Krisen, Manager, Management, Führungskräfte, Unternehmen, Mitarbeiter, Motivation, Verantwortung</h1>

Häufig haben Manager und andere Führungskräfte die Angewohnheit, weder vermeintliche Schwächen zu zeigen noch Hilfe anzunehmen. Nicht selten führt ein solcher selbstherrlicher Führungsstil das Unternehmen in eine handfeste Krise. Und aus dieser Unternehmenskrise findet das Management, dass seine Mitarbeiter nie um Rat gebeten hat, erst recht nicht alleine heraus. Das Unternehmen steckt in einer Sackgasse.

Doch guter Rat ist nicht teuer! Frühzeitige Prävention und gemeinsames Krisenmanagement sind ein erfolgreicher Weg, wie Unternehmenskrisen verhindert beziehungsweise überwunden werden können. Ein gesundes Unternehmen besteht aus Mitarbeitern, die von der Führungskraft bis zur Reinigungskraft mit Engagement und Leidenschaft ihrer Arbeit nachgehen. Motivation und Verantwortung sind das Erfolgsrezept.

Und beides bedingt sich. Wer Verantwortung trägt, der ist auch motiviert. Und wer mit Motivation bei der Sache ist, der trägt auch gerne die Verantwortung für sein Handeln. Wenn eine Führungskraft aber alles an sich reißt, ihren Mitarbeitern also auch jegliche Verantwortung nimmt, demotiviert sie ihre Untergebenen.

Viele Führungskräfte merken nicht einmal, dass sie ihren Mitarbeitern mit ihrem Verhalten jegliche Motivation nehmen. Gleichzeitig kritisieren sie oft das fehlende Verantwortungsgefühl und die mangelnde Motivation.

In der Regel besitzen die Mitarbeiter die Ressourcen und Fähigkeiten, ihr Unternehmen erfolgreich voran zu bringen. Oftmals kommen diese Fähigkeiten und Fertigkeiten allerdings nicht zum Tragen, weil sie nicht gefordert beziehungsweise gefördert werden.

In der Regel haben die eigenen Mitarbeiter gute Ideen und Lösungen, wie das Unternehmen erfolgreich wirtschaften kann. Sie werden vom Management bloß nicht gefragt. Kein externer Experte verfügt über ein ähnlich gutes Insiderwissen wie ein langjähriger Mitarbeiter. Dieses Wissen sollte man als Führungskraft kennen und nutzen.

In unseren Seminaren für mittelständische Führungskräfte vermitteln wir, wie Unternehmer, Manager und Mitarbeiter durch offene Kommunikation Unternehmenskrisen vermeiden und Krisen bewältigen können.

