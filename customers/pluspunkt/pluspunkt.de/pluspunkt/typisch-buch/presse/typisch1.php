            <h1><b>Presse</b></h1>
            <p><b><font size="2">Erfolgreiche Kommunikation will gelernt sein</font></b> 
              - 14.07.03<br>
              Netzwerk von Praktikern und Unternehmern um Unternehmensentwicklung
              Pluspunkt vermittelt neue Perspektiven: Interessantes 
              Managementbuch: &quot;Typisch - Potenziale erlebter 
              Kommunikation&quot;<br>
              <br>
              Was soll man tun, wenn man einen Gesch&auml;ftskunden von den Vorteilen 
              einer Zusammenarbeit &uuml;berzeugen m&ouml;chte? Auf alte Weisheiten 
              zur&uuml;ckgreifen? &quot;Reden ist Silber, Schweigen ist Gold&quot;? 
              Hm, das passt in der Gesch&auml;ftswelt wohl nicht immer. &quot;Ein 
              Bild sagt mehr als tausend Worte&quot;? Tja, der Satz k&ouml;nnte 
              bei eine Pr&auml;sentation eine Rolle spielen, bei einem Kundengespr&auml;ch 
              unter vier Augen eher weniger. Sprichw&ouml;rter gibt es wie Sand 
              am Meer, und gute Ratschl&auml;ge, die schlecht ankommen, ebenso. 
            </p>
            <p>Der Erste-Hilfe-Kasten in Form einer kurzweiligen Aufsatzsammlung 
              f&uuml;r die richtige Kommunikation im Gesch&auml;ftsleben ist seit 
              neuestem auf den Markt. &quot;Typ[isch]! Potenziale erlebter Kommunikation&quot; 
              hei&szlig;t das Managementbuch, das bei W. Bertelsmann erschienen 
              ist (Preis 24,90 &euro;) und in dem 12 Fachleute und Praktiker einige 
              Aspekte von Kommunikation f&uuml;r allem f&uuml;r F&uuml;hrungskr&auml;fte 
              beleuchten. Da die Autoren sich auf verschiedene Fachgebiete spezialisiert 
              haben, ist die Themen-Palette abwechslungsreich und vielf&auml;ltig. 
              Das, was sich in dem Managementbuch auf den ersten Blick wie ein 
              kunterbunt gef&uuml;llter Bauchladen gibt, wird bei intensiverem 
              Studium zu einer &auml;u&szlig;erst anregenden Lekt&uuml;re.</p>
            <p>Sprichw&ouml;rter wie &quot;Kleider machen Leute&quot; stehen hier 
              nicht f&uuml;r sich. Die Signale, die man mit seinem Outfit sendet, 
              werden beispielsweise in einem eigenen Kapitel analysiert. Und wieso 
              der erste Eindruck sich nicht nur auf die richtige Businesskleidung 
              beschr&auml;nkt, wird bei der weiteren Lekt&uuml;re des kleinen 
              B&auml;ndchens ebenfalls offensichtlich. Aspekte wie die richtige 
              K&ouml;rpersprache oder das Corporate Design des eigenen Unternehmens 
              geh&ouml;ren bei einem durchdachten Auftreten eben dazu.</p>
            <p>Die Bandbreite der Themen in dem Managementbuch erstreckt sich 
              von den Unterschieden zwischen M&auml;nnern und Frauen &uuml;ber 
              Corporate Design und Corporate Identity bis hin zum professionell 
              organisierten Betriebsfest. Gerade als Randbereiche der tagt&auml;glichen 
              Kommunikation erscheinende Themen werden hier ins rechte Licht ger&uuml;ckt. 
              Durchaus mit erstaunlichen Tipps und Ratschl&auml;gen.</p>
            <p>Auf einer mitgelieferten DVD stellen sich die Autoren auch in Bild 
              vor. Es sind allesamt Unternehmer und Praktiker mit langj&auml;hrigen 
              Berufserfahrungen und auch als Trainer und Ausbilder t&auml;tig. 
              Das sich damit das funktionierende Netzwerk von Unternehmern gleichzeitig 
              als erfahrene Berater empfiehlt, versteht sich von selbst. Das, 
              was sie in ihrem Buch zu Papier gebracht haben, macht eigentlich 
              nur neugierig, die Autoren leibhaftig als Trainer und Ausbilder 
              zu erleben. Man merkt zu deutlich, dass sich hinter den Zeilen ein 
              langj&auml;hriges Praxiswissen vor allem in der Personalentwicklung 
              verbirgt, das man nur zu gerne anzapfen m&ouml;chte.</p>
            <p>Ralf Lohe, der Verfasser des Vorworts war beispielsweise acht Jahre 
              lang selbstst&auml;ndiger Trainer bei &quot;Dale Carnegie Training&quot; 
              in Deutschland und ist jetzt seit f&uuml;nf Jahren Gesch&auml;ftsf&uuml;hrender 
              Gesellschafter der Pluspunkt Unternehmensentwicklung. F&uuml;r 
              ihn wie f&uuml;r die anderen Autoren steht im Gesch&auml;ftsleben 
              der Mensch im Mittelpunkt, ganz gleich ob als Gesch&auml;ftspartner 
              oder als Mitarbeiter. Eine interessante Erkenntnis f&uuml;r F&uuml;hrungskr&auml;fte 
              mag ein Beispiel geben. &quot;Menschen, die nicht gefragt werden, 
              glauben bald, dass sie nichts zu sagen haben&quot;, wei&szlig; Lohe 
              aus Erfahrung. Viele der &Uuml;berlegungen und Tipps bef&ouml;rdern 
              geradezu einen Umdenkungsprozess.</p>
            <p>Dieses Wissen wollen Lohe und die anderen Fachleute an die F&uuml;hrungsetagen 
              von kleinen und mittelst&auml;ndischen und mit Unternehmen vermitteln. 
              Ihr Schicksal liegt den Autoren besonders am Herzen, zumal hier 
              das Wohl und Wehe der gesamtwirtschaftlichen Entwicklung auf dem 
              Spiel steht. Aber auch f&uuml;r den normalen Angestellten h&auml;lt 
              das Buch wertvolle Tipps bereit. &quot;Der fr&uuml;he Vogel f&auml;ngt 
              den Wurm&quot; geh&ouml;rt nicht dazu. Eine baldige Lekt&uuml;re 
              von &quot;Typ[isch]! Potenziale erlebter Kommunikation&quot; kann 
              sich in diesen wirtschaftlich mauen Zeiten dennoch auch f&uuml;r 
              sie lohnen. Empfehlenswert!</p>
