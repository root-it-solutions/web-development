                  <i><b>Was unterscheidet ihre Arbeit von anderen Beratern?<br>
                  <br>
                  </b></i>Wir nehmen alle betroffene Menschen mit auf die Reise. 
                  Wir sehen zu, dass wir zu dem Punkt kommen: "Wir wollen!" Wenn 
                  wir diesen Punkt nicht erreichen - "Wir wollen!" - dann werden 
                  wir auch nichts umsetzen. Nehmen wir zum Beispiel ein Projekt, 
                  bei dem es darum geht, eine neue Software einzuführen. Das ist 
                  ein Projekt, von dem die Meisten im Unternehmen betroffen sind. 
                  Man kann nicht die Frage stellen: "Wer ist da betroffen?", sondern 
                  man muss eher die Frage stellen: "Wer ist eigentlich nicht betroffen?". 
                  Und dann wird deutlich, dass das eine sehr große Gruppe von 
                  Menschen ist. Die alle zu integrieren, das ist unsere Zielsetzung. 
                  Und das schaffen wir durch große Runden, wo die Kosten der Berater 
                  keine Rolle mehr spielen, wo die Kosten der Mitarbeiter im Vordergrund 
                  stehen, und wo am Ende die Mitarbeiter sagen: "Ja, das ist eine 
                  gute Sache." 
                  <p><b><i>Wie haben Sie zu Ihrem Thema gefunden?<br>
                    <br>
                    </i></b>Der Mensch steht im Mittelpunkt. Das ist kein banales 
                    Thema, sondern ein sehr bewegendes Thema, das sich sicherlich 
                    viele Unternehmer auf die Fahne geschrieben haben. Wobei man 
                    sehr deutlich dabei unterscheiden muss zwischen denen, die 
                    es nur auf der Fahne stehen haben und denen, die es wirklich 
                    tun. Und wir tun es, weil wir erkannt haben, dass der Mensch 
                    der Schlüssel zum Erfolg ist. <br>
                    <br>
                    Also wenn ich heute analysiere, dann frage ich mich wodurch 
                    unterscheiden sich die Unternehmen? Ich kann überall Produkte 
                    kaufen, ich kann überall Mitarbeiter bekommen, ich kann Verfahren 
                    kaufen. <br>
                    <br>
                    Wie können wir denn hier am Standort, wo der Lohn sehr teuer 
                    ist, überhaupt erfolgreich sein? Warum ist das eine Unternehmen 
                    erfolgreich, das andere nicht? Und da stellen wir immer wieder 
                    fest, der Schlüssel ist die Kraft der Mitarbeiter. <font face="Arial, Helvetica, sans-serif"></font></p>
