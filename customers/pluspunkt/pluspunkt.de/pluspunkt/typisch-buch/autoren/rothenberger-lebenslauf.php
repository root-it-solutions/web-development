                  Geboren wurde ich am 8. Juli 1960 in Gernsbach. Ich bin der 
                  älteste Sohn von weiteren 4 Geschwistern. Alle besuchten das 
                  Gymnasium und schlossen es mit Abitur ab. Nur ich wollte mit 
                  16 Jahren eine Kellnerlehre beginnen. Für meinen konservativen 
                  und sicherheitsbewussten Vater eine Katastrophe. Wenigstens 
                  begann ich meinen Berufsstart in einem der besten Hotels der 
                  Welt, im Brenner´s Parkhotel Baden Baden. Die richtige Etikette 
                  ging dabei in Fleisch und Blut über. 
                  <p>1979 - 1981 nutzte ich meine Bundeswehrzeit und leitete das 
                    Offizierskasino. </p>
                  <p>Danach zog es mich nach Paris, als Etagenkellner in das Hotel 
                    Le Bristol, natürlich lernte ich neben Französisch auch das 
                    Savoir vivre. </p>
                  <p>1982, als Wein-Steward auf der Vistafjord ( Cunard Line ) 
                    trimmte ich meine Weinkenntnisse auf hohes Niveau und erweiterter 
                    gewaltig meinen Horizont. Faszinierend, die ständig wechselnden 
                    Länder mit eigenen Kulturen, Sitten und Essgewohnheiten. </p>
                  <p>Von 1983 bis 1985 besuchte ich die Heidelberger Hotelfachschule 
                    und bestand die Prüfung zum Betriebswirt und die Ausbildereignung. 
