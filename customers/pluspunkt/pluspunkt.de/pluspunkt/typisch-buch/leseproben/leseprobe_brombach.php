            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>&quot;Ganz typisch ...&quot;<br>
              Kommunikation, Spiegel der Pers&ouml;nlichkeit</b></p>
            <p>&quot;Nichts ist best&auml;ndiger als der Wandel ...&quot;</p>
            <p>... oder bleibt, wie es ist. Wenn diese S&auml;tze von jeher ihre 
              Bedeutung haben, gelten sie heute umso mehr: Wir erleben eine Zeit 
              des rasanten Wandels in allen Bereichen des Lebens. Technische Entwicklungen 
              revolutionieren die Rahmenbedingungen des Arbeitens. In dem Ma&szlig;e, 
              wie Unternehmen oder &ouml;ffentliche Verwaltungen darauf reagieren 
              und ihre Strukturen und Organisationsprinzipien ver&auml;ndern, 
              wachsen die Anforderungen an den Einzelnen drastisch. Knappe Ressourcen 
              sowie Leistungs- und Konkurrenzdruck tun das ihre dazu. Nicht zuletzt 
              die vielgepriesene Arbeit im Team fordert in nie da gewesenem Umfang 
              sozialkommunikative Kompetenzen, um Kreativit&auml;t freizusetzen, 
              Ressourcen zu managen und mit St&ouml;rfaktoren oder Konflikten 
              virtuos umzugehen.</p>
            <p>So ist es l&auml;ngst kein Geheimnis mehr: Auf die Pers&ouml;nlichkeit 
              des Einzelnen kommt es an. Eine gute Ausbildung oder das vielbeschworene 
              Fachwissen allein gen&uuml;gen nicht mehr. Stattdessen treten pers&ouml;nliche 
              und soziale Kompetenz in den Vordergrund. Und an erster Stelle dieser 
              sogenannten &quot;soft skills&quot;, wie diese &quot;weichen&quot; 
              Faktoren genannt werden, steht das Zauberwort &quot;Kommunikationsf&auml;higkeit&quot;, 
              womit die vielf&auml;ltigen Anforderungen gemeistert werden sollen. 
              Gemeint ist die grunds&auml;tzliche Bereitschaft zu kommunizieren 
              und zwar unabh&auml;ngig von Alter, Geschlecht, Hierarchie oder 
              Herkunft. Es geht um angemessene und verstehende Kommunikation, 
              die auf aktivem Zuh&ouml;ren fu&szlig;t, mit der nonverbalen Sprache 
              im Einklang steht und moderne Medien zur elektronischen Kommunikation 
              zu nutzen wei&szlig;. Nicht zuletzt geh&ouml;rt zur Kommunikationsf&auml;higkeit 
              die Kompetenz, sich verst&auml;ndlich und konkret in mindestens 
              einer Sprache ausdr&uuml;cken zu k&ouml;nnen. Eine wahrlich gro&szlig;e 
              Herausforderung, denn von gelungener sprachlicher Selbstdarstellung 
              und Vermittlung h&auml;ngt das private und gesellschaftliche Ansehen 
              ab. Und auch der berufliche Erfolg steht und f&auml;llt mit der 
              F&auml;higkeit zur Kommunikation. Oder anders formuliert: Nirgendwo 
              sonst als in der Kommunikation zeigt sich die pers&ouml;nliche und 
              soziale Kompetenz eines Menschen so deutlich.</p>
