            <h1><b>Leseprobe Regine Brombach</b></h1>
            <p><b>Motivierende Besprechungen</b></p>
            <p><b>Grunds&auml;tzliches</b><br>
              &Uuml;ber das Motivieren ist viel geschrieben worden. Die Quantit&auml;t 
              der Publikationen entspricht der Wichtigkeit dieses Themas. Jede 
              F&uuml;hrungskraft wei&szlig; aus eigener Erfahrung, wie einfach 
              sich Hochleistungen einstellen, wenn Motivation vorherrscht. Aber 
              jede F&uuml;hrungskraft wei&szlig; auch aus eigener Erfahrung, wie 
              schnell durch eine Bemerkung oder Aktion die Motivation enden kann, 
              und wie die Leistung der Mitarbeiter dann absackt. Dank Reinhard 
              Sprengers Buch &quot;Mythos Motivation&quot;, das ich jedem nur 
              w&auml;rmstens ans Herz legen kann, wissen wir um den Unterschied 
              von Motivation und Demotivation durch das Handeln des Vorgesetzten.</p>
            <p>Was liegt nun n&auml;her, als nach den F&uuml;hrungselementen zu 
              fragen, die eine gr&ouml;&szlig;tm&ouml;gliche Motivation erm&ouml;glicht. 
              F&uuml;r mich steht hier an erster Stelle das Prinzip, die Betroffenen 
              zu Beteiligten zu machen. Denn jeder, der eine Idee mit geboren 
              hat, steht f&uuml;r diese auch voll und ganz ein. F&uuml;r den Vorgesetzten 
              bedeutet dies zwar weiterhin vorzudenken aber zugleich die Analysen, 
              Ideenfindungen und Entscheidungen gemeinsam mit seinen Mitarbeitern 
              zu erarbeiten.</p>
            <p>Denken Sie doch mal selber nach und erinnern sich an eine Situation, 
              in der Ihr Vorgesetzter Sie mit einer neuen Entscheidung konfrontierte, 
              die es galt, auch von Ihnen in die Tat umzusetzen. Brauchten Sie 
              auch etwas Zeit, sich mit der neuen Situation abzufinden bzw. anzufreunden? 
              Hatten Sie auch M&uuml;he gegen&uuml;ber Ihren Mitarbeitern die 
              passende Formulierung f&uuml;r die Informationsweitergabe zu finden? 
              Und welches Gef&uuml;hl hatten Sie, als bei der Umsetzung genau 
              das schief lief, was Sie zu bedenken gegeben hatten? Das wohl markanteste 
              Gef&uuml;hl von fehlender Motivation wissen wohl die zu beschreiben, 
              die eine Unternehmensentscheidung zuerst in der Zeitung lasen, bevor 
              sie offiziell informiert wurden.</p>
            <p><i>(...) Ralf Lohe </i></p>
