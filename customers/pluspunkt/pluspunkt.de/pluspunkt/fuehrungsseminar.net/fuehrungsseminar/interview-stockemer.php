                        <p><span class="titel">Interview mit Herrn Wolfgang Stockmeyer</span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Betriebsleiter einer M&ouml;belschreinerei in Beaufort, 
                          Luxemburg, 9 Mitarbeiter.</p>
                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          2002 auf Fuerteventura.</p>
                        <p><b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Gemischte, ich wusste nicht, was mich dort erwartet. 
                          Ich wurde von meinem Chef beauftragt, an dem F&uuml;hrungsseminar 
                          teilzunehmen. Von daher hatte ich au&szlig;er Neugier 
                          keine gro&szlig;en Erwartungen. <br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Das viele Probleme in der eigenen Person begr&uuml;ndet 
                          liegen. Dass man viele Sachen selbst &auml;ndern muss, 
                          um bei sich und bei anderen etwas zu ver&auml;ndern.<br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Ich gehe anders mit mir selbst und mit meinen Mitarbeitern 
                          um. Ich gehe eher auf andere Leute zu. Bei Problemen 
                          h&ouml;re ich mehr zu, und fasse nicht gleich nach zwei 
                          S&auml;tzen ein Urteil. Meine Kommunikation mit den 
                          Mitarbeitern hat sich deutlich ver&auml;ndert.<br>
                          <br>
                          <b>Wie fanden Sie die Trainer?</b><br>
                          Manfred empfand ich als sehr einf&uuml;hlsam, sehr auf 
                          einen zugehend. Er war bem&uuml;ht, dass bei jedem Einzelnen 
                          etwas h&auml;ngen bleibt. Zu Ralf hatte ich pers&ouml;nlich 
                          nicht so den Zugang. Er war viel distanzierter und mir 
                          zu theoretisch.<br>
                          <br>
                          <b>Was halten Sie von dem Austragungsort?</b><br>
                          Sehr exklusiv, die rundum sorglos Betreuung. Man konnte 
                          dort wirklich abschalten und sich erholen. Der Ort war 
                          aber nicht f&uuml;r den Erfolg des F&uuml;hrungsseminars 
                          entscheidend. <br>
                          <br>
                          <b>H&auml;tten Sie das F&uuml;hrungsseminar gebucht, 
                          wenn es hier in Deutschland durchgef&uuml;hrt worden 
                          w&auml;re?</b><br>
                          Ja.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja.<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          So interessant es auch war, dass die Teilnehmer ans 
                          den unterschiedlichsten Branchen kamen und wir so viel 
                          quer denken mussten, h&auml;tte ich es lieber gehabt, 
                          Leute mit &auml;hnlichen Problemen zu sehen, die mich 
                          betroffen haben. Wir h&auml;tten so gewisse Problematiken 
                          mehr vertiefen k&ouml;nnen. <br>
                          <br>
                          <b>Wie fanden Sie die Zusammenarbeit der Trainer?</b><br>
                          Die haben sich sehr gut erg&auml;nzt.<br>
                          <br>
                          <b>K&ouml;nnten Sie eine Weiterempfehlung geben?</b><br>
                          Ja, auf jeden Fall.<br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Sehr nett und sehr interessant.<br>
                          <br>
                          <b>Sollten Partner (Ehepartner) daran teilnehmen?</b><br>
                          Kann ich generell nicht sagen. F&uuml;r mich war es 
                          gut, dass meine Frau dabei war. <br>
                          <br>
                          <b>Was ist Ihrer Meinung nach die optimale Teilnehmerzahl?</b><br>
                          6 Leute.</p>
