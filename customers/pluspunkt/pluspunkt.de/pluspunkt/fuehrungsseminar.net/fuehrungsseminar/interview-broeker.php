                        <p><span class="titel">Interview mit Herrn Josef Br&ouml;ker</span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Mitinhaber eines Event und Catering Services, M&uuml;nster, 
                          80 feste Mitarbeiter plus ca. 100 Aushilfen</p>
                        <p> <b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Ich habe an diversen Seminaren der Firma Pluspunkt teilgenommen. 
                          Verkaufstraining, Pr&auml;sentationstraining, F&uuml;hrungsseminare, 
                          interne F&uuml;hrungsseminare. Ich habe allein und mit 
                          diversen Personen aus meiner Firma. an diesen Fortbildungen 
                          teilgenommen. Ich kann nicht mehr sagen, wann welches 
                          Seminar konkret war. Ich betrachte dieses als st&auml;ndige 
                          Weiterbildungen, ohne die man kein Unternehmen f&uuml;hren 
                          kann.<br>
                          <br>
                          <b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Meine gr&ouml;&szlig;te Erwartung war der bessere Umgang 
                          miteinander - mit meinen Mitarbeitern und mit meinen 
                          Kunden. Ein sehr guter Weg zum besseren Miteinander 
                          sind best&auml;ndiges Loben und gegenseitige Anerkennung.<br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Ich bin gereift aus dem Seminar zur&uuml;ckgekommen. 
                          Ich habe durch das Seminar erfahren, dass es noch viele 
                          M&ouml;glichkeiten gibt, sich weiterzuentwickeln. Gerade 
                          die Punkte, bei denen ich glaubte, fit und gut zu sein, 
                          waren verbesserungsf&auml;hig. H&auml;tte ich nicht 
                          so ein gesundes Selbstvertrauen, w&auml;re ich voller 
                          Selbstzweifel aus dem Seminar gegangen. Mein st&auml;rkster 
                          Eindruck war aber, dass man die Dinge laufen lassen 
                          soll, dass man nicht alles kontrollieren soll.<br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Bei uns standen und stehen einige Zukunftsentscheidungen 
                          an, etwa die Ausrichtung unseres Unternehmens oder die 
                          Generationsnachfolge. Das F&uuml;hrungsseminar hat mit 
                          dazu beigetragen, diese Entscheidungsprozesse in Gang 
                          zu setzen. Weiterhin haben wir die Eigenverantwortung 
                          der Mitarbeiter gest&auml;rkt und geben uns gegenseitig 
                          mehr Anerkennung.<br>
                          <br>
                          <b>Wie fanden Sie die Trainer und deren Zusammenarbeit?</b><br>
                          Hervorragend. Jeder auf seine Art. Ralf, sehr reif und 
                          aufrichtig. Der lebt, was er sagt. Manfred mit seinen 
                          Qualit&auml;ten als Mediator, der Auseinandersetzungen 
                          sehr konstruktiv zu l&ouml;sen versteht.<br>
                          Die Zusammenarbeit war nach au&szlig;en gut. Ich habe 
                          aber gemerkt, das die beiden zu dem Zeitpunkt noch nicht 
                          lange zusammengearbeitet haben. Ich hatte den Eindruck, 
                          das Ralf Manfred h&auml;ufiger korrigiert hat, weil 
                          er noch nicht seine Erfahrungen auf diesem Gebiet hatte. 
                          Das lief so nach dem Motto: alter Hase, junger Hase.<br>
                          <br>
                          <b>Wie hat Ihnen der Veranstaltungsort gefallen?</b><br>
                          Also ich war in der Wei&szlig;enburg, im Mauritzhof, 
                          in der Handwerkskammer, in Dortmund und auf der Insel. 
                          Man kann &uuml;berall lernen, aber Fuerteventura hat 
                          schon etwas Einmaliges. Man hat keinen Alltag, man kann 
                          aber auch nicht weglaufen. Man kann und muss sich ganz 
                          auf dieses F&uuml;hrungsseminar konzentrieren.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja, es ist so etwas wie eine pers&ouml;nliche Bestandsaufnahme 
                          - und die Visionsentwicklung. Man wei&szlig;, was ist 
                          in 5 Jahren, was ist in 10 Jahren.<br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Gut. Au&szlig;er meiner Familie waren ja auch Inhaber 
                          anderer Unternehmen dabei. Man bekam mit, dass sie alle 
                          &auml;hnliche Probleme hatten, das keiner halt perfekt 
                          ist.<br>
                          <br>
                          <b>Sollten Ehefrauen daran teilnehmen? </b><br>
                          Nach M&ouml;glichkeit ja. Das muss aber jeder f&uuml;r 
                          sich entscheiden.<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          In meinem Fall waren zu viele Leute aus meiner Familie 
                          und meinem Betrieb mit dabei. Ich hatte das Gef&uuml;hl, 
                          alles und alle kontrollieren zu m&uuml;ssen. Konnte 
                          mich nicht so sehr auf das F&uuml;hrungsseminar konzentrieren. 
                          Aber das war mein Problem, nicht das des F&uuml;hrungsseminars.<br>
                          <br>
                          <b>K&ouml;nnten Sie eine Weiterempfehlung geben?</b><br>
                          Dringend, ganz hei&szlig;es Thema</p>
