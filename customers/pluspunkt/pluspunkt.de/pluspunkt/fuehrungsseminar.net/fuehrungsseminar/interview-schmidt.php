                        <p><span class="titel">Interview mit Frau Maria Schmidt</span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Maria Schmidt, Bauunternehmerin aus Wenden, hat den 
                          Betrieb von ihrem Vater &uuml;bernommen, 21 Mitarbeiter.</p>
                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Im Fr&uuml;hjahr 2002 auf Fuerteventura. </p>
                        <p><b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Im Prinzip keine, ich konnte mir nichts Konkretes darunter 
                          vorstellen. Es war mir ehrlich gesagt sogar suspekt, 
                          ein F&uuml;hrungsseminar auf Fuerteventura zu besuchen. 
                          Ich hatte die Seriosit&auml;t angezweifelt und h&auml;tte 
                          ohne eine entsprechende Empfehlung von jemandem, der 
                          das F&uuml;hrungsseminar schon besucht hatte, nicht 
                          daran teilgenommen. Da ich mit meinem Unternehmen gerade 
                          in einer sehr schwierigen Phase war, habe ich mich dann 
                          aber doch spontan entschieden. <br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Optimismus! Ich habe dort eine neue Perspektive gefunden 
                          und das Gef&uuml;hl bekommen, auf dem richtigen Weg 
                          zu sein. <br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Zun&auml;chst hat es sich auf meinen pers&ouml;nlichen 
                          Zustand ausgewirkt. Ich habe die Sicherheit bekommen, 
                          dass ich das, was ich mache, auch wirklich will und 
                          kann. Die Hochstimmung mit der man aus dem F&uuml;hrungsseminar 
                          kommt, relativiert sich nat&uuml;rlich im Alltag wieder. 
                          Aber dennoch hat sich in meinem Betrieb Einiges bewegt. 
                          Insbesondere weil wir die Arbeit mit Pluspunkt fortgesetzt 
                          haben und mit dieser Unterst&uuml;tzung die Struktur 
                          des Betriebes und die gesamte Arbeitsweise &auml;ndern 
                          (z.B. existieren jetzt Stellenbeschreibungen).<br>
                          <b><br>
                          Wie fanden Sie die Trainer?</b><br>
                          Gut - vor allen Dingen, weil sie von der Pers&ouml;nlichkeit 
                          sehr unterschiedliche Personen sind, die sich in ihrem 
                          Handeln sehr gut erg&auml;nzt haben.<br>
                          <br>
                          <b>Was halten Sie von dem Austragungsort?</b><br>
                          Im Nachhinein gut, weil ich mich dort unglaublich wohl 
                          gef&uuml;hlt habe.<br>
                          <b><br>
                          H&auml;tten Sie das F&uuml;hrungsseminar gebucht, wenn 
                          es hier in Deutschland durchgef&uuml;hrt worden w&auml;re?</b><br>
                          Ja klar, ich h&auml;tte es damals mit einem viel besseren 
                          Gef&uuml;hl gebucht.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja, es war bed&uuml;rfnisorientiert auf die Probleme 
                          der Teilnehmer zugeschnitten.<br>
                          <br>
                          Was waren die Schw&auml;chen des F&uuml;hrungsseminars?<br>
                          Es blieb mir zu wenig Zeit &uuml;brig, um das Ganze 
                          zu reflektieren und die gestellten Aufgaben zu erledigen.<br>
                          <br>
                          <b>Wie fanden Sie die Zusammenarbeit der Trainer?</b><br>
                          Gut, man merkt, dass sie diesen Job schon lange zusammen 
                          machen.<br>
                          K&ouml;nnten Sie eine Weiterempfehlung geben?<br>
                          Ja, sicher.<br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Bunt gemischt. Dies war einerseits total gut, da ich 
                          so mit den unterschiedlichsten Problemstellungen konfrontiert 
                          wurde, die ich sonst nicht erfahren h&auml;tte. Andererseits 
                          h&auml;tte ich gern Leute aus meinem Bereich in dem 
                          F&uuml;hrungsseminar gehabt.<br>
                          <b><br>
                          Sollten Partner (Ehepartner) daran teilnehmen?</b><br>
                          Ich habe es lieber allein gemacht, aber grunds&auml;tzlich 
                          schon.<br>
                          <br>
                          <b>Was ist Ihrer Meinung nach die optimale Teilnehmerzahl?</b><br>
                          7 - max. 10 Teilnehmer</p>
