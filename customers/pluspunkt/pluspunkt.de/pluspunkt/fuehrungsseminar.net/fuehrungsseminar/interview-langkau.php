                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Im Mai 2002 Fuerteventura<br>
                          <br>
                          <b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Mich zu kl&auml;ren, mich in meinem Dasein wohler zu 
                          f&uuml;hlen.<br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Das Zusammenwachsen von Menschen unterschiedlicher Couleur 
                          und Ausbildung zu einer Interessengemeinschaft.<br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Kurzfristig erfuhr ich eine Bewusstseins- und Selbstbewusstseinssteigerung. 
                          Langfristig sind mir wertvolle Kontakte zu einzelnen 
                          Seminarteilnehmern geblieben bzw. haben sich entwickelt.<br>
                          <br>
                          <b>Wie fanden Sie die Trainer?</b><br>
                          Sie beherrschen ihr Handwerk zu 100%. Das Zusammensein 
                          mit ihnen empfand ich als sehr angenehm. Jeder von ihnen 
                          hatte seinen eigenen Part, Manfred Rumi den gef&uuml;hlsm&auml;&szlig;igen, 
                          Ralf Lohe den sachlichen. <br>
                          <br>
                          <b>Was halten Sie von dem Austragungsort?</b><br>
                          Er muss nicht soweit entfernt sein. F&uuml;r die Kosten 
                          h&auml;tte man hier ein exklusives Sport oder Wellness 
                          Hotel w&auml;hlen k&ouml;nnen und h&auml;tte nicht so 
                          eine lange Anreise gehabt. <br>
                          <br>
                          <b>H&auml;tten Sie das F&uuml;hrungsseminar gebucht, 
                          wenn es hier in Deutschland durchgef&uuml;hrt worden 
                          w&auml;re?</b><br>
                          Ja.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja, absolut.<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          Das Tagesprogramm war so voll, dass man die Eindr&uuml;cke 
                          nicht genug nachwirken lassen konnte.<br>
                          <br>
