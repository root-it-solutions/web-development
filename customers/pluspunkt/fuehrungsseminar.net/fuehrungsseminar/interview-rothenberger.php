                        <p><span class="titel">Interview mit Herrn Bernhard Rothenberger</span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Bernhard Rothenberger ist Hoteldirektor und Inhaber 
                          des Parkhotel Schloss Hohenfeld in M&uuml;nster. Zum 
                          Zeitpunkt der F&uuml;hrungsseminare war er Gesch&auml;ftsf&uuml;hrer 
                          der Fa. Partyservice Br&ouml;ker in M&uuml;nster. </p>
                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Ich habe zwei Mal an dem F&uuml;hrungsseminar teilgenommen. 
                          1997 in der Weissenburg in Billerbeck und im Jahr 2000 
                          auf Fuerteventura.</p>
                        <p><b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Durch das F&uuml;hrungsseminar wollte ich meinen F&uuml;hrungsstil 
                          komplett verbessern. Mir war klar, dass im Dienstleistungsbereich 
                          der Schl&uuml;ssel zum Erfolg bei der Motivierung der 
                          Mitarbeiter liegt. <br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Bei mir fand ein vollst&auml;ndiger Paradigmenwechsel 
                          statt. Mir wurde bewusst, dass mit Vertrauen ein Team 
                          viel besser zu f&uuml;hren ist als durch Kontrolle und 
                          Anweisungen.<br>
                          <br>
                          <b>Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Es hat mich bewogen, knallharte Entscheidungen f&uuml;r 
                          meinen Berufsweg zu treffen. Eine Konsequenz war, bei 
                          meinem damaligen Arbeitgeber zu k&uuml;ndigen. Weiterhin 
                          hat sich mein F&uuml;hrungsstil nachhaltig ver&auml;ndert.<br>
                          <br>
                          <b>Wie fanden Sie die Trainer?</b><br>
                          Es war sch&ouml;n zu sehen, dass sie auch nur Menschen 
                          sind, die das Ideal zwar kennen und lehren, es aber 
                          nicht schaffen, diesen Idealzustand permanent zu erreichen.<br>
                          <br>
                          <b>Was halten Sie von dem Austragungsort?</b><br>
                          Ein Ortswechsel ist wichtig, um sich auf das F&uuml;hrungsseminar 
                          konzentrieren zu k&ouml;nnen. Es muss aber nicht Fuerteventura 
                          sein, ein sch&ouml;ner Ort beispielsweise im Schwarzwald 
                          kann genau so viel bringen, w&auml;re wahrscheinlich 
                          sogar kosteng&uuml;nstiger. <br>
                          <br>
                          <b>H&auml;tten Sie das F&uuml;hrungsseminar gebucht, 
                          wenn es hier in Deutschland durchgef&uuml;hrt worden 
                          w&auml;re?</b><br>
                          Ja.<br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja, uneingeschr&auml;nkt.<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          Die dargebotene Stoffmenge war f&uuml;r die Dauer des 
                          F&uuml;hrungsseminars zu umfangreich. Es war nicht m&ouml;glich, 
                          das alles auch psychisch zu verarbeiten.<br>
                          <br>
                          <b>Wie fanden Sie die Zusammenarbeit der Trainer?</b><br>
                          OK, ausgesprochen gut. Sie sind sehr harmonisch miteinander 
                          umgegangen.<br>
                          <br>
                          <b>K&ouml;nnten Sie eine Weiterempfehlung geben?</b><br>
                          Ja, unbedingt.<br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Sehr positiv, es ist w&auml;hrend der Dauer des F&uuml;hrungsseminars 
                          ein Zusammengeh&ouml;rigkeitsgef&uuml;hl entstanden.<br>
                          <br>
                          <b>Sollten Partner (Ehepartner) daran teilnehmen?</b><br>
                          Ja, das halte ich f&uuml;r sehr gut. Es gab jedoch Frauen, 
                          die nur sporadisch an den Sitzungen teilnahmen. Das 
                          fand ich nicht so gut. Entweder ganz oder gar nicht..<br>
                          <br>
                          <b>Was ist Ihrer Meinung nach die optimale Teilnehmerzahl?</b><br>
                          Ca. 12 Teilnehmer</p>
