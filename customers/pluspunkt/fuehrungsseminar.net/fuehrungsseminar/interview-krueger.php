                        <p><span class="titel">Interview mit Herrn Karsten Kr&uuml;ger</span><b><font color="#FFD9A2"><br>
                          </font></b><img src="../_images/trans.gif" width="1" height="5"><br>
                          Karsten Kr&uuml;ger, einer von 2 Gesch&auml;ftsf&uuml;hrern 
                          von Karlchens Backstube, einem B&auml;ckerei Filial-Unternehmen 
                          mit 26 Filialen und 160 Mitarbeitern. Firmensitz ist 
                          L&ouml;hne in NRW.</p>
                        <p><b>Wann haben Sie an dem F&uuml;hrungsseminar teilgenommen?</b><br>
                          Das erste Mal im Oktober 2002 in der T&uuml;rkei und 
                          das zweite Mal im April 2003 auf Fuerteventura, wobei 
                          f&uuml;r mich das F&uuml;hrungsseminar in der T&uuml;rkei 
                          das Wichtigere war.<br>
                          <br>
                          <b>Welche Erwartungen hatten Sie im Vorfeld?</b><br>
                          Dadurch, das ich im Vorfeld schon an einem SEAL - Training 
                          der Firma Pluspunkt teilgenommen hatte, wusste ich ziemlich 
                          gut, was mich in dem F&uuml;hrungsseminar erwarten w&uuml;rde. 
                          Meine Haupterwartung war daher, eine 10 Jahres Vision 
                          f&uuml;r unser Unternehmen zu entwickeln und meine unternehmerischen 
                          Ziele klarer zu definieren. <br>
                          <br>
                          <b>Was ist der st&auml;rkste Eindruck, den das F&uuml;hrungsseminar 
                          hinterlassen hat?</b><br>
                          Mein st&auml;rkster Eindruck war die Ver&auml;nderung 
                          meines Denkens, frei von Blockaden zu denken und dadurch 
                          entsprechende Kreativit&auml;t beim Denken freizusetzen 
                          und zu entwickeln. Ich hatte bis dahin z.B. bei Vorschl&auml;gen 
                          von Mitarbeitern oft schon die Blockade &quot;Geht nicht 
                          - kann nicht funktionieren&quot; im Kopf und war sehr 
                          &uuml;berrascht, was alles m&ouml;glich ist, wenn ich 
                          ein anderes Denken zulasse.<br>
                          <b><br>
                          Wie hat sich das F&uuml;hrungsseminar in Ihrem Berufsleben 
                          ausgewirkt?</b><br>
                          Ich mache jetzt fast nur noch Sachen, an denen ich Spa&szlig; 
                          habe und die zu meinen Kernaufgaben geh&ouml;ren, z.B. 
                          das Erarbeiten von Zielen mit den Mitarbeitern und zu 
                          schauen, ob sie auch erreicht werden. Au&szlig;erdem 
                          gebe ich Ziele vor, die ich mit dem Unternehmen erreichen 
                          m&ouml;chte. <br>
                          <br>
                          <b>Welche Inhalte des F&uuml;hrungsseminars haben Sie 
                          in Ihrem Betrieb umgesetzt?</b><br>
                          Wir haben die 10 Jahres Vision des Unternehmens von 
                          jedem Mitarbeiter selber erstellen lassen. Dadurch haben 
                          wir F&auml;higkeiten, Eigenschaften, Kenntnisse und 
                          auch &Auml;ngste der Mitarbeiter entdeckt, die wir vorher 
                          nicht kannten, jetzt aber zum Wohle aller entsprechend 
                          einsetzen k&ouml;nnen. <br>
                          <br>
                          <b>Wie fanden Sie die Trainer und deren Zusammenarbeit?</b><br>
                          Exzellent, sie erg&auml;nzen sich sehr gut. Sie sind 
                          beide sehr sympathisch und in der Lage, bei den Teilnehmern 
                          Horizonte zu &ouml;ffnen, bez&uuml;glich der M&ouml;glichkeiten, 
                          die man hat und vor allem der Denkweise, wie man an 
                          Probleme oder Konflikte herangeht. <br>
                          <br>
                          <b>Wie hat Ihnen der Veranstaltungsort gefallen?</b><br>
                          Sehr gut. Ich habe am Anfang nicht verstanden, warum 
                          ein F&uuml;hrungsseminar in der T&uuml;rkei stattfindet. 
                          Ich war der Meinung, das treibt nur die Kosten hoch. 
                          Ich habe inzwischen aber begriffen, dass wenn man gro&szlig;e 
                          Ver&auml;nderungen durchf&uuml;hren will, man aus dem 
                          Alltagstrott heraus muss. Dazu geh&ouml;rt sowohl die 
                          gro&szlig;e Entfernung als auch die entspannte Urlaubsatmosph&auml;re, 
                          in der das F&uuml;hrungsseminar stattfindet. <br>
                          <br>
                          <b>Hat Sie der Inhalt des F&uuml;hrungsseminars &uuml;berzeugt?</b><br>
                          Ja. Es war das beste Seminar, das ich je mitgemacht 
                          habe. <br>
                          <br>
                          <b>Wie waren die anderen Teilnehmer?</b><br>
                          Es war sehr sch&ouml;n, dass die Teilnehmer aus ganz 
                          unterschiedlichen Bereichen kamen, so dass wir mit den 
                          unterschiedlichsten Problemen konfrontiert wurden. Im 
                          Laufe der Woche hat sich innerhalb der Gruppe ein starkes 
                          Zusammengeh&ouml;rigkeitsgef&uuml;hl entwickelt. Wir 
                          halten alle noch Kontakt untereinander und haben uns 
                          auch schon einmal getroffen. <br>
                          <br>
                          <b>Sollten Ehefrauen daran teilnehmen?</b><br>
                          Wenn man nicht mit den Gedanken einer Trennung spielt, 
                          schon.<br>
                          <br>
                          <b>Was waren die Schw&auml;chen des F&uuml;hrungsseminars?</b><br>
                          Ich kann keine Schw&auml;chen erkennen. <br>
                          <br>
                          <b>K&ouml;nnten Sie eine Weiterempfehlung geben?</b><br>
                          Ja, jederzeit, das mach ich auch schon. Allerdings nur 
                          bei Menschen, die mir wirklich wichtig sind.</p>
